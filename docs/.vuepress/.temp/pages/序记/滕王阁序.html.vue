<h1 id="滕王阁序" tabindex="-1"><a class="header-anchor" href="#滕王阁序"><span>滕王阁序</span></a></h1>
<ul>
<li>作者：王勃（唐）</li>
<li>体裁：序/记</li>
</ul>
<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p>豫章故郡，洪都新府。星分翼軫，地接衡廬。襟三江而帶五湖，控蠻荊而引甌越。物華天寶，龍光射牛斗之墟；人傑地靈，徐孺下陳蕃之榻。雄州霧列，俊彩星馳。臺隍枕夷夏之交，賓主盡東南之美。都督閻公之雅望，棨戟遙臨；宇文新州之懿範，襜帷暫駐。十旬休暇，勝友如雲。千里逢迎，高朋滿座。騰蛟起鳳，孟學士之詞宗；紫電青霜，王將軍之武庫。家君作宰，路出名區。童子何知？躬逢勝餞。</p>
<p>時維九月，序屬三秋。潦水盡而寒潭清，煙光凝而暮山紫。儼驂騑於上路，訪風景於崇阿。臨帝子之長洲，得仙人之舊館。層臺聳翠，上出重霄；飛閣流丹，下臨無地。鶴汀鳧渚，窮島嶼之縈廻；桂殿蘭宮，即岡巒之體勢。</p>
<p>披繡闥，俯雕甍。山原曠其盈視，川澤紆其駭矚。閭閻撲地，鐘鳴鼎食之家；舸艦弥津，青雀黃龍之舳。雲銷雨霽，彩徹區明。落霞與孤鶩齊飛，秋水共長天一色。漁舟唱晚，響窮彭蠡之濱；雁陣驚寒，聲斷衡陽之浦。</p>
<p>遙襟甫暢，逸興遄飛。爽籟發而清風生，纖歌凝而白雲遏。睢園綠竹，氣凌彭澤之樽；鄴水朱華，光照臨川之筆。四美具，二難并。窮睇眄於中天，極娛遊於暇日。天高地迥，覺宇宙之無窮；興盡悲來，識盈虛之有數。望長安於日下，目吳會於雲間。地勢極而南溟深，天柱高而北辰遠。關山難越，誰悲失路之人；萍水相逢，盡是他鄉之客。懷帝閽而不見，奉宣室以何年？</p>
<p>嗟乎！時運不齊，命途多舛。馮唐易老，李廣難封。屈賈誼於長沙，非無聖主；竄梁鴻於海曲，豈乏明時？所賴君子安貧，達人知命。老當益壯，寧移白首之心；窮且益堅，不墜青雲之志。酌貪泉而覺爽，處涸轍以猶懽。北海雖賒，扶搖可接；東隅已逝，桑榆非晚。孟嘗高潔，空餘報國之心；阮籍猖狂，豈效窮途之哭？</p>
<p>勃，三尺微命，一介書生，無路請纓，等終軍之弱冠；有懷投筆，慕宗慤之長風。捨簪笏於百齡，奉晨昏於萬里。非謝家之寶樹，接孟氏之芳鄰。他日趨庭，叨陪鯉對；今茲捧袂，喜托龍門。楊意不逢，撫凌雲而自惜；鍾期既遇，奏流水以何慚？</p>
<p>嗚呼！勝地不常，盛筵難再。蘭亭已矣，梓澤丘墟。臨別贈言，幸承恩於偉餞；登高作賦，是所望於群公。敢竭鄙誠，恭疏短引。一言均賦，四韻俱成。請灑潘江，各傾陸海云爾。</p>
<p>滕王高閣臨江渚，佩玉鳴鸞罷歌舞。
畫棟朝飛南浦雲，珠簾暮捲西山雨。
閒雲潭影日悠悠，物換星移幾度秋。
閣中帝子今何在？檻外長江空自流！</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<p><strong>豫章故郡，洪都新府</strong>：豫章是古代的郡名，洪都是新设的府名。豫章，今江西南昌一带。洪都，唐代洪州的别称。</p>
<p><strong>星分翼軫</strong>：按照古代星宿分野的说法，这里属于翼宿和轸宿的分野。翼、轸，二十八宿中的星宿名。</p>
<p><strong>地接衡廬</strong>：地域连接着衡山和庐山。衡，衡山。廬，庐山。</p>
<p><strong>襟三江而帶五湖</strong>：以三江为衣襟，以五湖为腰带。形容地理位置重要。三江，泛指长江中下游的江河。五湖，泛指太湖流域的湖泊。</p>
<p><strong>控蠻荊而引甌越</strong>：控制着荆楚，连接着甌越。蠻荊，指荆楚一带。甌越，指浙江一带。</p>
<p><strong>物華天寶</strong>：物产丰富，天降珍宝。</p>
<p><strong>龍光射牛斗之墟</strong>：宝剑的光芒直射牛宿、斗宿的区域。传说丰城有宝剑，光芒射向天空。</p>
<p><strong>人傑地靈</strong>：人才杰出，地方灵秀。</p>
<p><strong>徐孺下陳蕃之榻</strong>：徐孺子来访时，陈蕃为他放下专用的床榻。徐孺，东汉高士徐稚，字孺子。陈蕃，东汉名臣。</p>
<p><strong>雄州霧列，俊彩星馳</strong>：雄伟的州郡像雾一样密布，杰出的人才像星一样奔驰。</p>
<p><strong>臺隍枕夷夏之交</strong>：城池坐落在华夷交界的地方。臺隍，城池。夷夏，指边疆和中原。</p>
<p><strong>都督閻公之雅望</strong>：都督阎公的高雅声望。閻公，指阎伯屿，当时的洪州都督。</p>
<p><strong>棨戟遙臨</strong>：仪仗队从远方来到。棨戟，古代官员出行时的仪仗。</p>
<p><strong>宇文新州之懿範</strong>：宇文新州的美好风范。宇文新州，指宇文化及，当时的新州刺史。</p>
<p><strong>襜帷暫駐</strong>：车帷暂时停留。襜帷，车上的帷幕。</p>
<p><strong>十旬休暇</strong>：一百天的休假。十旬，一百天。</p>
<p><strong>勝友如雲</strong>：优秀的朋友如云聚集。</p>
<p><strong>千里逢迎</strong>：从千里之外赶来迎接。</p>
<p><strong>高朋滿座</strong>：高贵的朋友坐满了席位。</p>
<p><strong>騰蛟起鳳</strong>：如蛟龙腾跃，凤凰起舞。比喻文才出众。</p>
<p><strong>孟學士之詞宗</strong>：孟学士是文坛的宗师。</p>
<p><strong>紫電青霜</strong>：宝剑的代称。紫電，宝剑名。青霜，形容剑刃锋利。</p>
<p><strong>王將軍之武庫</strong>：王将军的武器库。</p>
<p><strong>家君作宰</strong>：我的父亲担任县令。家君，对自己父亲的敬称。作宰，担任县令。</p>
<p><strong>路出名區</strong>：路过著名的地区。</p>
<p><strong>童子何知</strong>：我这个年轻人懂得什么。</p>
<p><strong>躬逢勝餞</strong>：亲自遇到这次盛大的饯别宴会。</p>
<p><strong>時維九月，序屬三秋</strong>：时间是九月，正值深秋。序，季节。三秋，深秋。</p>
<p><strong>潦水盡而寒潭清</strong>：积水退尽，寒潭清澈。潦水，积水。</p>
<p><strong>煙光凝而暮山紫</strong>：烟雾凝聚，暮色中的山峦呈现紫色。</p>
<p><strong>儼驂騑於上路</strong>：整齐地驾着马车在大路上行驶。驂騑，古代驾车用的马。</p>
<p><strong>訪風景於崇阿</strong>：到高山上寻访风景。崇阿，高山。</p>
<p><strong>臨帝子之長洲</strong>：来到滕王的长洲。帝子，指滕王李元婴，唐高祖李渊之子。</p>
<p><strong>得仙人之舊館</strong>：得到了仙人的旧居。</p>
<p><strong>層臺聳翠</strong>：层层楼台高耸入云，一片翠绿。</p>
<p><strong>上出重霄</strong>：高出重重云霄。</p>
<p><strong>飛閣流丹</strong>：飞檐流彩，色彩绚丽。</p>
<p><strong>下臨無地</strong>：下面深不见底。</p>
<p><strong>鶴汀鳧渚</strong>：白鹤栖息的沙洲，野鸭游弋的小岛。汀，水边平地。渚，水中小洲。</p>
<p><strong>窮島嶼之縈廻</strong>：穷尽了岛屿的回环曲折。</p>
<p><strong>桂殿蘭宫</strong>：桂花飘香的宫殿，兰草芬芳的楼阁。</p>
<p><strong>即岡巒之體勢</strong>：依照山岗的地势而建。</p>
<p><strong>披繡闥</strong>：推开华美的门扉。闥，门。</p>
<p><strong>俯雕甍</strong>：俯视雕饰的屋脊。甍，屋脊。</p>
<p><strong>山原曠其盈視</strong>：山川原野开阔，充满视野。</p>
<p><strong>川澤紆其駭矚</strong>：河流湖泊弯曲，令人惊叹。紆，弯曲。駭矚，令人惊叹。</p>
<p><strong>閭閻撲地</strong>：房屋密集，遍布大地。閭閻，房屋。</p>
<p><strong>鐘鳴鼎食之家</strong>：钟鸣鼎食的富贵人家。</p>
<p><strong>舸艦弥津</strong>：大船小舟遍布渡口。舸艦，大船小舟。弥津，遍布渡口。</p>
<p><strong>青雀黃龍之舳</strong>：装饰着青雀、黄龙的船头。舳，船头。</p>
<p><strong>雲銷雨霁</strong>：云消雨散。</p>
<p><strong>彩徹區明</strong>：彩虹贯穿天空，天地明朗。</p>
<p><strong>落霞與孤鶩齊飛</strong>：落日的彩霞与孤独的野鸭一起飞翔。</p>
<p><strong>秋水共長天一色</strong>：秋天的江水与长空连成一片，颜色相同。</p>
<p><strong>漁舟唱晚</strong>：渔船在傍晚时分唱起歌来。</p>
<p><strong>響窮彭蠡之濱</strong>：歌声传遍鄱阳湖畔。彭蠡，鄱阳湖的古称。</p>
<p><strong>雁陣驚寒</strong>：雁群因寒冷而惊飞。</p>
<p><strong>聲斷衡陽之浦</strong>：叫声在衡阳的水边断绝。</p>
<p><strong>遙襟甫暢</strong>：远大的胸怀刚刚舒展。</p>
<p><strong>逸興遄飛</strong>：高雅的兴致迅速高涨。遄飞，迅速飞扬。</p>
<p><strong>爽籟發而清風生</strong>：清脆的音乐响起，清风徐来。籟，音乐。</p>
<p><strong>纖歌凝而白雲遏</strong>：细腻的歌声凝聚，白云为之停留。</p>
<p><strong>睢園綠竹</strong>：睢园的绿竹。睢园，汉代梁孝王的园林。</p>
<p><strong>氣凌彭澤之樽</strong>：气势超过陶渊明的酒杯。彭澤，陶渊明曾任彭泽县令。</p>
<p><strong>鄴水朱華</strong>：邺水边的红花。鄴水，在今河北临漳一带。</p>
<p><strong>光照臨川之筆</strong>：光彩照耀谢灵运的文笔。臨川，谢灵运的封地。</p>
<p><strong>四美具，二難并</strong>：四种美好的事物齐备，两种难得的条件并存。四美：良辰、美景、赏心、乐事。二难：贤主、嘉宾。</p>
<p><strong>窮睇眄於中天</strong>：极目远望天空。睇眄，远望。</p>
<p><strong>極娛遊於暇日</strong>：在闲暇的日子里尽情游乐。</p>
<p><strong>天高地迥</strong>：天空高远，大地辽阔。迥，远。</p>
<p><strong>覺宇宙之無窮</strong>：感觉到宇宙的无穷无尽。</p>
<p><strong>興盡悲來</strong>：兴致尽了，悲伤袭来。</p>
<p><strong>識盈虛之有數</strong>：认识到盛衰是有定数的。</p>
<p><strong>望長安於日下</strong>：在夕阳下遥望长安。</p>
<p><strong>目吳會於雲間</strong>：在云雾中眺望吴会。吳會，今苏州一带。</p>
<p><strong>地勢極而南溟深</strong>：地势到了尽头，南海深不可测。南溟，南海。</p>
<p><strong>天柱高而北辰遠</strong>：天柱高耸，北极星遥远。天柱，传说中支撑天空的柱子。北辰，北极星。</p>
<p><strong>關山難越</strong>：关隘山岭难以越过。</p>
<p><strong>誰悲失路之人</strong>：谁来同情迷路的人。</p>
<p><strong>萍水相逢</strong>：像浮萍在水上相遇一样偶然。</p>
<p><strong>盡是他鄉之客</strong>：都是异乡的客人。</p>
<p><strong>懷帝閽而不見</strong>：怀念皇宫却见不到。帝閽，皇宫的门。</p>
<p><strong>奉宣室以何年</strong>：什么时候才能到宣室朝见皇帝。宣室，汉代皇帝召见臣子的宫殿。</p>
<p><strong>嗟乎</strong>：感叹词。</p>
<p><strong>時運不齊</strong>：时运不济。</p>
<p><strong>命途多舛</strong>：命运多有不顺。舛，不顺。</p>
<p><strong>馮唐易老</strong>：冯唐容易衰老。冯唐，汉代人，年老才得到重用。</p>
<p><strong>李廣難封</strong>：李广难以封侯。李广，汉代名将，终身未能封侯。</p>
<p><strong>屈賈誼於長沙</strong>：贾谊被贬到长沙。賈誼，汉代文学家，曾被贬为长沙王太傅。</p>
<p><strong>非無聖主</strong>：并非没有圣明的君主。</p>
<p><strong>竄梁鴻於海曲</strong>：梁鸿隐居在海边。梁鴻，东汉隐士。海曲，海边。</p>
<p><strong>豈乏明時</strong>：难道缺少清明的时代吗？</p>
<p><strong>所賴君子安貧</strong>：所依靠的是君子能安于贫困。</p>
<p><strong>達人知命</strong>：通达的人知道天命。</p>
<p><strong>老當益壯</strong>：年老了应当更加雄壮。</p>
<p><strong>寧移白首之心</strong>：怎能改变老年的志向。</p>
<p><strong>窮且益堅</strong>：穷困反而更加坚定。</p>
<p><strong>不墜青雲之志</strong>：不放弃高远的志向。</p>
<p><strong>酌貪泉而覺爽</strong>：喝了贪泉的水反而觉得清爽。貪泉，传说中的泉名，喝了会变贪婪。</p>
<p><strong>處涸轍以猶懽</strong>：处在干涸的车辙中仍然快乐。涸轍，干涸的车辙，比喻困境。</p>
<p><strong>北海雖賒</strong>：北海虽然遥远。賒，远。</p>
<p><strong>扶搖可接</strong>：乘着旋风可以到达。扶搖，旋风。</p>
<p><strong>東隅已逝</strong>：东方的时光已经过去。東隅，东方，指早晨。</p>
<p><strong>桑榆非晚</strong>：桑榆时光并不算晚。桑榆，指傍晚。</p>
<p><strong>孟嘗高潔</strong>：孟尝君品格高洁。孟嘗，战国时齐国的孟尝君。</p>
<p><strong>空餘報國之心</strong>：只剩下报国的心愿。</p>
<p><strong>阮籍猖狂</strong>：阮籍行为狂放。阮籍，魏晋时期的文学家。</p>
<p><strong>豈效窮途之哭</strong>：难道要效仿穷途末路的哭泣吗？</p>
<p><strong>勃，三尺微命</strong>：我王勃，身份微贱。三尺，指身高，代指人。</p>
<p><strong>一介書生</strong>：一个书生。</p>
<p><strong>無路請纓</strong>：没有机会请求出征。請纓，请求出征。</p>
<p><strong>等終軍之弱冠</strong>：等同于终军的弱冠之年。終軍，汉代人，年轻时即有才名。弱冠，二十岁。</p>
<p><strong>有懷投筆</strong>：有投笔从戎的志向。</p>
<p><strong>慕宗慤之長風</strong>：仰慕宗悫的长风之志。宗慤，南朝宋将领，少时有&quot;乘长风破万里浪&quot;之志。</p>
<p><strong>捨簪笏於百齡</strong>：在百年人生中舍弃官职。簪笏，官员的服饰用品。</p>
<p><strong>奉晨昏於萬里</strong>：在万里之外侍奉父母。</p>
<p><strong>非謝家之寶樹</strong>：不是谢家的宝树。謝家之寶樹，指谢玄，东晋名将，被称为谢家宝树。</p>
<p><strong>接孟氏之芳鄰</strong>：结交孟家的好邻居。</p>
<p><strong>他日趨庭</strong>：将来到父亲面前请安。趨庭，到父亲面前请安。</p>
<p><strong>叨陪鯉對</strong>：有幸陪同孔鲤对话。鯉對，孔子之子孔鲤与父亲的对话。</p>
<p><strong>今茲捧袂</strong>：现在捧着衣袖。</p>
<p><strong>喜托龍門</strong>：高兴地依托龙门。龍門，比喻显贵的门第。</p>
<p><strong>楊意不逢</strong>：没有遇到杨意这样的人。楊意，汉代人，曾推荐司马相如。</p>
<p><strong>撫凌雲而自惜</strong>：抚摸着《凌云赋》而自怜。凌雲，司马相如的赋名。</p>
<p><strong>鍾期既遇</strong>：既然遇到了钟期。鍾期，春秋时期的钟子期，伯牙的知音。</p>
<p><strong>奏流水以何慚</strong>：演奏《流水》有什么可惭愧的。流水，古琴曲名。</p>
<p><strong>嗚呼</strong>：感叹词。</p>
<p><strong>勝地不常</strong>：美好的地方不能常有。</p>
<p><strong>盛筵難再</strong>：盛大的宴会难以再有。</p>
<p><strong>蘭亭已矣</strong>：兰亭的聚会已经过去了。蘭亭，指王羲之等人的兰亭聚会。</p>
<p><strong>梓澤丘墟</strong>：梓泽已成废墟。梓澤，古代楚国的园林。</p>
<p><strong>臨別贈言</strong>：临别赠送言辞。</p>
<p><strong>幸承恩於偉餞</strong>：有幸在盛大的饯别宴上承受恩惠。</p>
<p><strong>登高作賦</strong>：登高作赋。</p>
<p><strong>是所望於群公</strong>：这是对各位公卿的期望。</p>
<p><strong>敢竭鄙誠</strong>：敢于竭尽我的诚意。</p>
<p><strong>恭疏短引</strong>：恭敬地写下这篇短序。</p>
<p><strong>一言均賦</strong>：每人都作一首赋。</p>
<p><strong>四韻俱成</strong>：四韵都要完成。</p>
<p><strong>請灑潘江</strong>：请挥洒潘岳、江淹那样的文才。潘江，指潘岳的文才。</p>
<p><strong>各傾陸海云爾</strong>：各自倾吐陆机、海上那样的文才。陸海，指陆机的文才。</p>
<h2 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h2>
<p>这里是古代的豫章郡，如今是洪都府。按星宿分野，属于翼宿、轸宿的分野，地面连接着衡山、庐山。以三江为衣襟，以五湖为腰带，控制着荆楚，连接着瓯越。物产丰富，天降珍宝，宝剑的光芒直射牛宿、斗宿的区域；人才杰出，地方灵秀，徐孺子来访时，陈蕃为他放下专用的床榻。雄伟的州郡像雾一样密布，杰出的人才像星一样奔驰。城池坐落在华夷交界的地方，宾客和主人都是东南地区的俊美之士。都督阎公声望高雅，仪仗队从远方来到；宇文新州风范美好，车帷暂时停留。正值十旬休假，优秀的朋友如云聚集；千里迎接，高贵的朋友坐满了席位。文才出众如蛟龙腾跃、凤凰起舞，孟学士是文坛的宗师；武艺精湛如紫电青霜，王将军拥有武器库。我的父亲在交趾担任县令，我路过这个著名的地方。我这个年轻人懂得什么？却亲自遇到了这次盛大的饯别宴会。</p>
<p>时间是九月，正值深秋。积水退尽，寒潭清澈；烟雾凝聚，暮色中的山峦呈现紫色。整齐地驾着马车在大路上行驶，到高山上寻访风景。来到滕王的长洲，得到了仙人的旧居。层层楼台高耸入云，一片翠绿，高出重重云霄；飞檐流彩，色彩绚丽，下面深不见底。白鹤栖息的沙洲，野鸭游弋的小岛，穷尽了岛屿的回环曲折；桂花飘香的宫殿，兰草芬芳的楼阁，依照山岗的地势而建。</p>
<p>推开华美的门扉，俯视雕饰的屋脊。山川原野开阔，充满视野；河流湖泊弯曲，令人惊叹。房屋密集，遍布大地，都是钟鸣鼎食的富贵人家；大船小舟遍布渡口，都是装饰着青雀、黄龙的船头。云消雨散，彩虹贯穿天空，天地明朗。落日的彩霞与孤独的野鸭一起飞翔，秋天的江水与长空连成一片，颜色相同。渔船在傍晚时分唱起歌来，歌声传遍鄱阳湖畔；雁群因寒冷而惊飞，叫声在衡阳的水边断绝。</p>
<p>远大的胸怀刚刚舒展，高雅的兴致迅速高涨。清脆的音乐响起，清风徐来；细腻的歌声凝聚，白云为之停留。这里有睢园绿竹的雅致，气势超过陶渊明的酒杯；有邺水红花的美丽，光彩照耀谢灵运的文笔。四种美好的事物齐备，两种难得的条件并存。极目远望天空，在闲暇的日子里尽情游乐。天空高远，大地辽阔，感觉到宇宙的无穷无尽；兴致尽了，悲伤袭来，认识到盛衰是有定数的。在夕阳下遥望长安，在云雾中眺望吴会。地势到了尽头，南海深不可测；天柱高耸，北极星遥远。关隘山岭难以越过，谁来同情迷路的人？萍水相逢，都是异乡的客人。怀念皇宫却见不到，什么时候才能到宣室朝见皇帝？</p>
<p>唉！时运不济，命运多有不顺。冯唐容易衰老，李广难以封侯。贾谊被贬到长沙，并非没有圣明的君主；梁鸿隐居在海边，难道缺少清明的时代吗？所依靠的是君子能安于贫困，通达的人知道天命。年老了应当更加雄壮，怎能改变老年的志向？穷困反而更加坚定，不放弃高远的志向。喝了贪泉的水反而觉得清爽，处在干涸的车辙中仍然快乐。北海虽然遥远，乘着旋风可以到达；东方的时光已经过去，桑榆时光并不算晚。孟尝君品格高洁，只剩下报国的心愿；阮籍行为狂放，难道要效仿穷途末路的哭泣吗？</p>
<p>我王勃，身份微贱，一个书生。没有机会请求出征，等同于终军的弱冠之年；有投笔从戎的志向，仰慕宗悫的长风之志。在百年人生中舍弃官职，在万里之外侍奉父母。不是谢家的宝树，结交孟家的好邻居。将来到父亲面前请安，有幸陪同孔鲤对话；现在捧着衣袖，高兴地依托龙门。没有遇到杨意这样的人，抚摸着《凌云赋》而自怜；既然遇到了钟期，演奏《流水》有什么可惭愧的？</p>
<p>唉！美好的地方不能常有，盛大的宴会难以再有。兰亭的聚会已经过去了，梓泽已成废墟。临别赠送言辞，有幸在盛大的饯别宴上承受恩惠；登高作赋，这是对各位公卿的期望。敢于竭尽我的诚意，恭敬地写下这篇短序。每人都作一首赋，四韵都要完成。请挥洒潘岳、江淹那样的文才，各自倾吐陆机那样的文才吧！</p>
<p>滕王的高阁面临江心的沙洲，佩玉鸣鸾停止了歌舞。
画栋早晨飞来南浦的云彩，珠帘傍晚卷起西山的雨。
悠闲的云彩和潭中的倒影日日悠悠，物换星移过了几度秋。
阁中的滕王如今在哪里？只有栏杆外的长江空自流淌！</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<h3 id="历史背景" tabindex="-1"><a class="header-anchor" href="#历史背景"><span>历史背景</span></a></h3>
<p>《滕王阁序》作于唐高宗上元二年（675年）秋，当时王勃年仅26岁。滕王阁位于今江西南昌，是唐高祖李渊之子滕王李元婴任洪州都督时所建。王勃路过南昌，正值都督阎伯屿重修滕王阁后在阁上宴请宾客，王勃应邀参加，即席作了这篇著名的骈文。</p>
<h3 id="结构分析" tabindex="-1"><a class="header-anchor" href="#结构分析"><span>结构分析</span></a></h3>
<p>全文可分为四个部分：</p>
<p><strong>第一部分</strong>（开头至&quot;躬逢胜饯&quot;）：写洪州的地理位置、人文历史和宴会的盛况，为下文的抒情做铺垫。</p>
<p><strong>第二部分</strong>（&quot;时维九月&quot;至&quot;声断衡阳之浦&quot;）：写滕王阁的壮丽景色和周围的自然风光，是全文的重点。</p>
<p><strong>第三部分</strong>（&quot;遥襟甫畅&quot;至&quot;奏流水以何惭&quot;）：由写景转入抒情，表达了作者的人生感慨和政治理想。</p>
<p><strong>第四部分</strong>（&quot;呜呼&quot;至结尾）：写作这篇序文的缘由，并以诗作结。</p>
<h3 id="思想内容" tabindex="-1"><a class="header-anchor" href="#思想内容"><span>思想内容</span></a></h3>
<p><strong>壮志难酬的感慨</strong>：作者虽然年轻，但已经感受到了人生的不如意，表达了壮志难酬的感慨。</p>
<p><strong>积极进取的精神</strong>：尽管遭遇挫折，但作者仍然保持着&quot;老当益壮&quot;、&quot;穷且益坚&quot;的积极进取精神。</p>
<p><strong>对自然美的赞颂</strong>：文章对滕王阁周围自然景色的描写，体现了作者对自然美的深深热爱。</p>
<p><strong>怀才不遇的苦闷</strong>：作者通过古人的典故，表达了自己怀才不遇的苦闷心情。</p>
<h3 id="艺术特色" tabindex="-1"><a class="header-anchor" href="#艺术特色"><span>艺术特色</span></a></h3>
<p><strong>骈文的典型代表</strong>：全文采用骈文体，对偶工整，音韵和谐，是初唐骈文的代表作。</p>
<p><strong>情景交融</strong>：文章将对自然景物的描写与作者的思想感情巧妙结合，情景交融。</p>
<p><strong>典故运用娴熟</strong>：文章大量运用历史典故，既增加了文章的文化内涵，又表达了作者的思想感情。</p>
<p><strong>语言华美</strong>：文章语言华美典雅，词藻丰富，体现了初唐文学的特色。</p>
<h3 id="写作技巧" tabindex="-1"><a class="header-anchor" href="#写作技巧"><span>写作技巧</span></a></h3>
<p><strong>层次分明</strong>：从写地理位置到写自然景色，再到抒发感情，层次清晰，逻辑严密。</p>
<p><strong>虚实结合</strong>：既有对现实景物的描写，又有对历史人物的追忆，虚实结合。</p>
<p><strong>动静相宜</strong>：既有静态的景物描写，又有动态的场面描绘，动静相宜。</p>
<p><strong>远近结合</strong>：既有近景的细致描绘，又有远景的宏观展现，远近结合。</p>
<h3 id="名句赏析" tabindex="-1"><a class="header-anchor" href="#名句赏析"><span>名句赏析</span></a></h3>
<p><strong>&quot;落霞与孤鹜齐飞，秋水共长天一色&quot;</strong>：这是全文最著名的句子，以工整的对偶，描绘了一幅色彩绚丽、意境深远的画面，成为千古传诵的名句。</p>
<p><strong>&quot;老当益壮，宁移白首之心；穷且益坚，不坠青云之志&quot;</strong>：表达了作者不因年老而衰退，不因贫穷而改变志向的坚强意志。</p>
<p><strong>&quot;时运不齐，命途多舛&quot;</strong>：概括了作者对人生际遇的感慨，成为后世描述命运不济的经典表达。</p>
<h3 id="文学价值" tabindex="-1"><a class="header-anchor" href="#文学价值"><span>文学价值</span></a></h3>
<p>《滕王阁序》是中国古代骈文的巅峰之作，其华美的语言、精巧的结构、深刻的思想，使它成为历代文人学习的典范。文章中体现的积极进取精神和对美好事物的追求，对后世产生了深远影响。</p>
<h3 id="历史影响" tabindex="-1"><a class="header-anchor" href="#历史影响"><span>历史影响</span></a></h3>
<p>这篇文章使滕王阁名扬天下，成为江南三大名楼之一。&quot;落霞与孤鹜齐飞，秋水共长天一色&quot;等名句，至今仍被广泛传诵。文章也确立了王勃在文学史上的地位，被誉为&quot;初唐四杰&quot;之首。</p>
<h3 id="现实意义" tabindex="-1"><a class="header-anchor" href="#现实意义"><span>现实意义</span></a></h3>
<p>文章中体现的&quot;老当益壮&quot;、&quot;穷且益坚&quot;的精神，在今天仍有重要的现实意义。它告诉我们，面对困难和挫折，应该保持积极向上的心态，不放弃理想和追求。同时，文章对自然美的赞颂，也提醒我们要热爱自然，保护环境。</p>

<h1 id="岳阳楼记" tabindex="-1"><a class="header-anchor" href="#岳阳楼记"><span>岳阳楼记</span></a></h1>
<ul>
<li>作者：范仲淹（北宋）</li>
<li>体裁：记</li>
</ul>
<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p>慶曆四年春，滕子京謫守巴陵郡。越明年，政通人和，百廢具興，乃重修岳陽樓，增其舊制，刻唐賢、今人詩賦於其上；屬予作文以記之。</p>
<p>予觀夫巴陵勝狀，在洞庭一湖。銜遠山，吞長江，浩浩湯湯，橫無際涯；朝暉夕陰，氣象萬千；此則岳陽樓之大觀也，前人之述備矣。然則北通巫峽，南極瀟湘，遷客騷人，多會於此，覽物之情，得無異乎？</p>
<p>若夫霪雨霏霏，連月不開；陰風怒號，濁浪排空；日星隱曜，山岳潛形；商旅不行，檣傾楫摧；薄暮冥冥，虎嘯猿啼。登斯樓也，則有去國懷鄉，憂讒畏譏，滿目蕭然，感極而悲者矣。</p>
<p>至若春和景明，波瀾不驚，上下天光，一碧萬頃；沙鷗翔集，錦鱗游泳，岸芷汀蘭，郁郁青青。而或長煙一空，皓月千里，浮光躍金，靜影沉璧，漁歌互答，此樂何極。登斯樓也，則有心曠神怡，寵辱皆忘，把酒臨風，其喜洋洋者矣。</p>
<p>嗟夫！予嘗求古仁人之心，或異二者之為，何哉？不以物喜，不以己悲。居廟堂之高，則憂其民；處江湖之遠，則憂其君。是進亦憂，退亦憂；然則何時而樂耶？其必曰：「先天下之憂而憂，後天下之樂而樂」歟！噫！微斯人，吾誰與歸？</p>
<p>時六年九月十五日。</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<p><strong>慶曆四年</strong>：宋仁宗庆历四年（1044年）。</p>
<p><strong>滕子京</strong>：名宗谅，字子京，范仲淹的朋友。</p>
<p><strong>謫守</strong>：降职外放做太守。謫，贬官。</p>
<p><strong>巴陵郡</strong>：即岳州，治所在今湖南岳阳。</p>
<p><strong>越明年</strong>：到了第二年，即庆历五年（1045年）。越，到了。</p>
<p><strong>政通人和</strong>：政事顺利，人民和乐。</p>
<p><strong>百廢具興</strong>：各种荒废的事业都兴办起来了。具，同&quot;俱&quot;，全，都。</p>
<p><strong>增其舊制</strong>：扩大它旧有的规模。</p>
<p><strong>唐賢、今人</strong>：唐代和当代的名人。</p>
<p><strong>屬予作文以記之</strong>：嘱托我写文章来记述这件事。屬，同&quot;嘱&quot;，嘱托。</p>
<p><strong>予觀夫巴陵勝狀</strong>：我看那巴陵郡的美好景色。夫，那。勝狀，胜景，好景色。</p>
<p><strong>銜遠山，吞長江</strong>：衔接着远山，吞没了长江。形容洞庭湖的壮阔。銜，衔接。</p>
<p><strong>浩浩湯湯</strong>：水势壮阔的样子。</p>
<p><strong>橫無際涯</strong>：宽阔无边。橫，广远。際涯，边际。</p>
<p><strong>朝暉夕陰</strong>：或早或晚（一天里）阴晴变化。暉，日光。</p>
<p><strong>氣象萬千</strong>：景象变化万端。</p>
<p><strong>大觀</strong>：雄伟景象。</p>
<p><strong>前人之述備矣</strong>：前人的记述很详尽了。備，详尽，完备。</p>
<p><strong>然則</strong>：既然这样，那么。</p>
<p><strong>北通巫峽，南極瀟湘</strong>：北面通到巫峡，南面直到潇湘。巫峡，长江三峡之一。瀟湘，潇水和湘水，在今湖南境内。極，尽。</p>
<p><strong>遷客騷人</strong>：被贬谪流放的人和诗人。遷客，被贬谪流放的人。騷人，诗人。</p>
<p><strong>覽物之情，得無異乎</strong>：看了自然景物而触发的感情，能不有所不同吗？覽，观看。得無，能不，表示反问。</p>
<p><strong>若夫霪雨霏霏</strong>：像那连绵的雨纷纷地下着。若夫，像那。霪雨，连绵的雨。霏霏，雨（雪）繁密的样子。</p>
<p><strong>連月不開</strong>：连续几个月不放晴。開，放晴。</p>
<p><strong>陰風怒號</strong>：阴冷的风愤怒地呼啸。</p>
<p><strong>濁浪排空</strong>：浑浊的浪涛冲向天空。排空，冲向天空。</p>
<p><strong>日星隱曜</strong>：太阳和星星隐藏了光辉。曜，光辉。</p>
<p><strong>山岳潛形</strong>：山岳隐没了形体。潛，隐没。</p>
<p><strong>商旅不行</strong>：商人和旅客不能通行。</p>
<p><strong>檣傾楫摧</strong>：桅杆倒下，船桨折断。檣，桅杆。楫，船桨。</p>
<p><strong>薄暮冥冥</strong>：傍晚天色昏暗。薄暮，傍晚。冥冥，昏暗的样子。</p>
<p><strong>去國懷鄉</strong>：离开国都，怀念家乡。去，离开。</p>
<p><strong>憂讒畏譏</strong>：担心（人家）说坏话，惧怕（人家）批评指责。讒，说别人坏话。譏，嘲讽。</p>
<p><strong>滿目蕭然</strong>：满眼都是萧条的景象。蕭然，萧条的样子。</p>
<p><strong>感極而悲者矣</strong>：感慨到了极点而悲伤的了。</p>
<p><strong>至若春和景明</strong>：至于春风和煦，景色明朗。至若，至于。景，日光。</p>
<p><strong>波瀾不驚</strong>：湖面平静，没有惊涛骇浪。</p>
<p><strong>上下天光，一碧萬頃</strong>：天色湖光相接，一片碧绿，广阔无际。萬頃，极言其广。</p>
<p><strong>沙鷗翔集</strong>：沙洲上的鸥鸟，时而飞翔，时而停歇。集，栖止。</p>
<p><strong>錦鱗游泳</strong>：美丽的鱼儿游来游去。錦鱗，指美丽的鱼。</p>
<p><strong>岸芷汀蘭</strong>：岸上的香草，小洲上的兰花。芷，香草名。汀，小洲。</p>
<p><strong>郁郁青青</strong>：香气浓郁，颜色青葱。</p>
<p><strong>而或長煙一空</strong>：有时大片烟雾完全消散。而或，有时。一，全。</p>
<p><strong>皓月千里</strong>：皎洁的月光照耀千里。</p>
<p><strong>浮光躍金</strong>：波动的光闪着金色。這是描写月光照耀下的水波。</p>
<p><strong>靜影沉璧</strong>：静静的月影像沉入水中的玉璧。這是写无风时水中的月影。</p>
<p><strong>漁歌互答</strong>：渔夫的歌声此起彼伏。</p>
<p><strong>此樂何極</strong>：这种快乐哪有穷尽！極，穷尽。</p>
<p><strong>心曠神怡</strong>：心情开朗，精神愉快。</p>
<p><strong>寵辱皆忘</strong>：光荣和屈辱都忘了。</p>
<p><strong>把酒臨風</strong>：端着酒杯，迎着清风。把，持，执。</p>
<p><strong>其喜洋洋者矣</strong>：那心情真是快乐极了。洋洋，快乐的样子。</p>
<p><strong>嗟夫</strong>：唉！</p>
<p><strong>予嘗求古仁人之心</strong>：我曾经探求古代品德高尚的人的思想感情。求，探求。</p>
<p><strong>或異二者之為</strong>：或许不同于（以上）两种心情。或，或许。異，不同于。為，心理活动。</p>
<p><strong>不以物喜，不以己悲</strong>：不因外物（好坏）和自己（得失）而或喜或悲。以，因为。</p>
<p><strong>居廟堂之高</strong>：处在朝廷上做官。廟堂，宗庙明堂，指朝廷。</p>
<p><strong>處江湖之遠</strong>：处在僻远的江湖间。江湖，指民间。</p>
<p><strong>是進亦憂，退亦憂</strong>：这样来说在朝廷做官也担忧，在僻远的江湖也担忧。是，这样。進，指在朝廷做官。退，指不在朝廷做官。</p>
<p><strong>先天下之憂而憂，後天下之樂而樂</strong>：在天下人担忧之前先担忧，在天下人快乐之后才快乐。</p>
<p><strong>微斯人，吾誰與歸</strong>：（如果）没有这种人，我同谁一道呢？微，没有。斯人，这样的人。歸，归依。</p>
<p><strong>時六年九月十五日</strong>：时间是庆历六年九月十五日。</p>
<h2 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h2>
<p>庆历四年的春天，滕子京被贬官到巴陵郡做太守。到了第二年，政事顺利，百姓和乐，各种荒废的事业都兴办起来了。于是重新修建岳阳楼，扩大它原有的规模，把唐代名家和当代人的诗赋刻在它上面。嘱托我写一篇文章来记述这件事。</p>
<p>我观看那巴陵郡的美好景色，全在洞庭湖上。它衔接着远山，吞吐着长江，浩浩荡荡，无边无际；一天里阴晴变化，气象万千。这就是岳阳楼的雄伟景象，前人的记述已经很详尽了。既然这样，那么北面通到巫峡，南面直到潇湘，被贬谪的官吏和来往的诗人，大多在这里聚会，观赏自然景物而触发的感情能没有不同吗？</p>
<p>像那阴雨连绵，接连几个月不放晴，寒风怒吼，浑浊的浪涛冲向天空；太阳星星隐藏起光辉，山岳隐没了形体；商人和旅客（一路）走不成，船桅倒下，船桨折断；傍晚天色昏暗，虎在长啸，猿在悲啼，（这时）登上这座楼啊，就会有一种离开国都、怀念家乡，担心人家说坏话、惧怕人家批评指责，满眼都是萧条的景象，感慨到了极点而悲伤的心情了。</p>
<p>到了春风和煦，阳光明媚的时候，湖面平静，没有惊涛骇浪，天色与湖光相连，一片碧绿，广阔无际；沙洲上的鸥鸟，时而飞翔，时而停歇，美丽的鱼儿游来游去；岸上的香草，小洲上的兰花，香气浓郁，颜色青葱。有时大片烟雾完全消散，皎洁的月光一泻千里，波动的光闪着金色，静静的月影像沉入水中的玉璧，渔夫的歌声此起彼伏，这种乐趣（真是）无穷无尽啊！（这时）登上这座楼，就会感到心胸开阔、心情愉快，光荣和屈辱一并忘了，端着酒杯，吹着微风，那真是快乐高兴极了。</p>
<p>唉！我曾经探求古代品德高尚的人的心思，或许不同于（以上）两种人的心情，这是为什么呢？（是由于）不因外物（好坏）和自己（得失）而或喜或悲。在朝廷里做高官就为百姓忧虑；在江湖上不做官就为国君忧虑。这样来说在朝廷做官也忧虑，在僻远的江湖也忧虑。既然这样，那么什么时候才快乐呢？他们必定会说：&quot;在天下人忧虑之前先忧虑，在天下人快乐之后才快乐&quot;吧。唉！没有这样的人，我同谁一道呢？</p>
<p>写于庆历六年九月十五日。</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<h3 id="历史背景" tabindex="-1"><a class="header-anchor" href="#历史背景"><span>历史背景</span></a></h3>
<p>《岳阳楼记》作于宋仁宗庆历六年（1046年）。当时范仲淹正在邓州做知州，他的朋友滕子京被贬到岳州（今湖南岳阳）做知州。滕子京到任后，重修了岳阳楼，请范仲淹为此写一篇记文。范仲淹应邀而作，写成了这篇千古名文。</p>
<h3 id="结构分析" tabindex="-1"><a class="header-anchor" href="#结构分析"><span>结构分析</span></a></h3>
<p>全文可分为三个部分：</p>
<p><strong>第一部分</strong>（开头至&quot;得无异乎&quot;）：叙述重修岳阳楼的背景和写作缘由，并提出&quot;览物之情，得无异乎&quot;的问题，为下文的议论作铺垫。</p>
<p><strong>第二部分</strong>（&quot;若夫霪雨霏霏&quot;至&quot;其喜洋洋者矣&quot;）：分别描写了阴雨天和晴朗天登楼所见的不同景象，以及由此产生的不同感情。这是文章的主体部分。</p>
<p><strong>第三部分</strong>（&quot;嗟夫&quot;至结尾）：抒发议论，提出&quot;古仁人&quot;的胸襟和&quot;先天下之忧而忧，后天下之乐而乐&quot;的政治理想，点明全文主旨。</p>
<h3 id="思想内容" tabindex="-1"><a class="header-anchor" href="#思想内容"><span>思想内容</span></a></h3>
<p><strong>忧国忧民的情怀</strong>：文章通过对比一般人因景而生的不同感情，突出了古代仁人志士&quot;不以物喜，不以己悲&quot;的高尚品格和&quot;先天下之忧而忧，后天下之乐而乐&quot;的政治理想。</p>
<p><strong>积极的人生态度</strong>：虽然作者当时也是被贬之身，但他没有沉溺于个人的得失，而是表现出积极的人生态度和崇高的精神境界。</p>
<p><strong>对友人的勉励</strong>：文章表面上是记述岳阳楼，实际上是对友人滕子京的勉励，希望他能够超越个人得失，以天下为己任。</p>
<h3 id="艺术特色" tabindex="-1"><a class="header-anchor" href="#艺术特色"><span>艺术特色</span></a></h3>
<p><strong>情景交融</strong>：文章通过对洞庭湖不同天气下景象的描写，表现了不同的情感，情景交融，相得益彰。</p>
<p><strong>对比鲜明</strong>：文章通过阴雨天和晴朗天的对比，&quot;迁客骚人&quot;和&quot;古仁人&quot;的对比，突出了主题。</p>
<p><strong>语言优美</strong>：文章语言典雅优美，音韵和谐，如&quot;衔远山，吞长江，浩浩汤汤，横无际涯&quot;等，气势磅礴，读来朗朗上口。</p>
<p><strong>议论深刻</strong>：文章不仅写景抒情，更重要的是议论，提出了深刻的人生哲理和政治理想。</p>
<h3 id="写作技巧" tabindex="-1"><a class="header-anchor" href="#写作技巧"><span>写作技巧</span></a></h3>
<p><strong>虚实结合</strong>：范仲淹并没有亲自登过岳阳楼，但他通过丰富的想象和对洞庭湖的了解，写出了如此生动的景象描写。</p>
<p><strong>层层递进</strong>：从记叙到描写，从描写到议论，层层递进，逻辑严密。</p>
<p><strong>托物言志</strong>：表面写岳阳楼和洞庭湖，实际上是借景抒情，表达自己的政治理想和人生追求。</p>
<h3 id="名句赏析" tabindex="-1"><a class="header-anchor" href="#名句赏析"><span>名句赏析</span></a></h3>
<p><strong>&quot;先天下之忧而忧，后天下之乐而乐&quot;</strong>：这是全文的点睛之笔，体现了古代知识分子以天下为己任的崇高精神境界，成为千古传诵的名句。</p>
<p><strong>&quot;不以物喜，不以己悲&quot;</strong>：表现了古代仁人志士超越个人得失、不为外物所动的高尚品格。</p>
<p><strong>&quot;衔远山，吞长江，浩浩汤汤，横无际涯&quot;</strong>：运用拟人手法，生动地描写了洞庭湖的壮阔景象。</p>
<h3 id="文学价值" tabindex="-1"><a class="header-anchor" href="#文学价值"><span>文学价值</span></a></h3>
<p>《岳阳楼记》是中国古代散文的经典之作，其深刻的思想内容、优美的语言表达、精巧的艺术结构，使它成为历代文人学习的典范。文章中体现的忧国忧民情怀和积极的人生态度，对后世产生了深远影响。</p>
<h3 id="现实意义" tabindex="-1"><a class="header-anchor" href="#现实意义"><span>现实意义</span></a></h3>
<p>文章中提出的&quot;先天下之忧而忧，后天下之乐而乐&quot;的思想，体现了中华民族优秀的传统文化精神，即以天下为己任、心系国家和人民的责任感，这种精神在今天仍有重要的现实意义。</p>

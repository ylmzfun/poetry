<h2 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h2>
<h3 id="《始得西山宴游记》译文" tabindex="-1"><a class="header-anchor" href="#《始得西山宴游记》译文"><span>《始得西山宴游记》译文</span></a></h3>
<p>自从我成为被贬谪的人，住在这个州里，常常惊恐不安。在空闲的时候，就慢慢地行走，随意地游览。每天与同伴登高山，入深林，走遍曲折的溪流，奇异的泉水和怪石，没有远的地方不到。到了就拨开草丛坐下，倾尽壶中酒而醉。醉了就互相枕着躺下，躺下就做梦。心意达到什么境界，梦境也是同样的情趣。醒来就起身，起身就回去。以为这个州的山水中有奇异形态的，都被我游遍了，却不曾知道西山的奇异独特。</p>
<p>今年九月二十八日，因为坐在法华西亭，望见西山，才指出它的奇异。于是命令仆人渡过湘江，沿着染溪，砍伐杂草，烧掉茅草，一直到山的最高处才停止。攀援着登上去，张开腿坐着游览，那么几个州的土地，都在脚下。那高低的地势，深陷的样子，像蚁穴一样，方圆千里，聚集堆积，没有能够隐藏的。青山白云缭绕，外面与天边相接，四面望去都是一样。然后才知道这座山的独特卓立，不与小土山为同类。悠远地与浩然之气相伴，而不知道它的边际；广大地与造物者游玩，而不知道它的尽头。举杯痛饮，醉倒了，不知道太阳下山。苍茫的暮色，从远处来到，到了什么也看不见的地步，还不想回去。心神凝聚，形体消散，与万物的变化暗合。然后才知道我以前不曾真正游览过，游览从这里开始。所以写了这篇文章来记述。这一年，是元和四年。</p>
<h3 id="《钴鉧潭西小丘记》译文" tabindex="-1"><a class="header-anchor" href="#《钴鉧潭西小丘记》译文"><span>《钴鉧潭西小丘记》译文</span></a></h3>
<p>发现西山后第八天，寻找山口西北方向的道路二百步，又发现了钴鉧潭。向西二十五步，对着急流而深的地方是鱼梁。鱼梁上面有个小丘，生长着竹子和树木。那些石头突出愤怒、倾斜不平，背负着泥土露出来，争着呈现奇异形状的，几乎数不清。那些高峻地相互堆积向下的，像牛马在溪边饮水；那些向上冲、像兽角一样排列向上的，像熊罴登山。</p>
<p>小丘很小，不到一亩，可以全部拥有。问它的主人，说：&quot;这是唐氏的废弃土地，出卖而卖不出去。&quot;问它的价钱，说：&quot;只要四百钱。&quot;我怜惜它而买下了。李深源、元克己当时一同游览，都非常高兴，出乎意料。立即又拿来工具，铲除割掉杂草，砍去坏树，用烈火烧掉。好树显露出来，美竹露出来，奇石显现出来。从这里望去，那么山的高峻，云的飘浮，溪水的流淌，鸟兽的游玩，都和乐地回旋巧妙地献技，来为这个小丘效力。枕着席子躺下，那么清凉的景象与眼睛相配合，潺潺的声音与耳朵相配合，悠远而空虚的与精神相配合，渺远的思绪与心灵相配合。不到十天就得到奇异的地方两处，即使古代喜欢游览的人，也许不能到达。</p>
<p>唉！凭着这个小丘的优美，如果把它放到沣、镐、鄠、杜这些地方，那么达官贵人争着购买，每天增加千金的价钱还越来越买不到。现在被遗弃在这个州，农民渔民经过都嫌弃它，价值四百钱，连续几年都卖不出去。而我与深源、克己独自高兴得到它，这难道真的是有缘分吗！写在石头上，用来庆贺这个小丘的际遇。</p>

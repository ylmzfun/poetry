<h2 id="简介" tabindex="-1"><a class="header-anchor" href="#简介"><span>简介</span></a></h2>
<p>柳宗元（773-819），字子厚，河东（今山西运城）人，世称&quot;柳河东&quot;。唐代文学家、哲学家、散文家和思想家，与韩愈并称&quot;韩柳&quot;，是唐宋八大家之一。因参与王叔文集团的政治改革失败，被贬为永州司马，在永州十年间创作了著名的&quot;永州八记&quot;。</p>
<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<h3 id="一、始得西山宴游记" tabindex="-1"><a class="header-anchor" href="#一、始得西山宴游记"><span>一、始得西山宴游记</span></a></h3>
<p>自余为僇人，居是州，恒惴栗。其隙也，则施施而行，漫漫而游。日与其徒上高山，入深林，穷回溪，幽泉怪石，无远不到。到则披草而坐，倾壶而醉。醉则更相枕以卧，卧而梦。意有所极，梦亦同趣。觉而起，起而归。以为凡是州之山水有异态者，皆我有也，而未始知西山之怪特。</p>
<p>今年九月二十八日，因坐法华西亭，望西山，始指异之。遂命仆过湘江，缘染溪，斫榛莽，焚茅茷，穷山之高而止。攀援而登，箕踞而遨，则凡数州之土壤，皆在衽席之下。其高下之势，岈然洼然，若垤若穴，尺寸千里，攒蹙累积，莫得遁隐。萦青缭白，外与天际，四望如一。然后知是山之特立，不与培塿为类。悠悠乎与颢气俱，而莫得其涯；洋洋乎与造物者游，而不知其所穷。引觞满酌，颓然就醉，不知日之入。苍然暮色，自远而至，至无所见，而犹不欲归。心凝形释，与万化冥合。然后知吾向之未始游，游于是乎始。故为之文以志。是岁，元和四年也。</p>
<h3 id="二、钴鉧潭西小丘记" tabindex="-1"><a class="header-anchor" href="#二、钴鉧潭西小丘记"><span>二、钴鉧潭西小丘记</span></a></h3>
<p>得西山后八日，寻山口西北道二百步，又得钴鉧潭。西二十五步，当湍而浚者为鱼梁。梁之上有丘焉，生竹树。其石之突怒偃蹇，负土而出，争为奇状者，殆不可数。其嵚然相累而下者，若牛马之饮于溪；其冲然角列而上者，若熊罴之登于山。</p>
<p>丘之小不能一亩，可以笼而有之。问其主，曰：&quot;唐氏之弃地，货而不售。&quot;问其价，曰：&quot;止四百。&quot;余怜而售之。李深源、元克己时同游，皆大喜，出自意外。即更取器用，铲刈秽草，伐去恶木，烈火而焚之。嘉木立，美竹露，奇石显。由其中以望，则山之高，云之浮，溪之流，鸟兽之遨游，举熙熙然回巧献技，以效兹丘之下。枕席而卧，则清泠之状与目谋，瀯瀯之声与耳谋，悠然而虚者与神谋，渺然而思者与心谋。不匝旬而得异地者二，虽古好事之士，或未能至焉。</p>
<p>噫！以兹丘之胜，致之沣、镐、鄠、杜，则贵游之士争买者，日增千金而愈不可得。今弃是州也，农夫渔父过而陋之，贾四百，连岁不能售。而我与深源、克己独喜得之，是其果有遭乎！书于石，所以贺兹丘之遭也。</p>

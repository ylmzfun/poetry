<h1 id="诗经" tabindex="-1"><a class="header-anchor" href="#诗经"><span>诗经</span></a></h1>
<h2 id="简介" tabindex="-1"><a class="header-anchor" href="#简介"><span>简介</span></a></h2>
<p>《诗经》是中国最早的诗歌总集，收录了西周初年至春秋中叶（约公元前11世纪至前6世纪）的诗歌305篇，另有6篇有目无辞，共311篇。《诗经》分为&quot;风&quot;、&quot;雅&quot;、&quot;颂&quot;三个部分，是中国古代文学的源头，被列为五经之首，对后世文学发展产生了深远影响。</p>

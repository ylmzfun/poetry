<h2 id="经典篇目选录" tabindex="-1"><a class="header-anchor" href="#经典篇目选录"><span>经典篇目选录</span></a></h2>
<h3 id="关雎-周南" tabindex="-1"><a class="header-anchor" href="#关雎-周南"><span>关雎（周南）</span></a></h3>
<p>关关雎鸠，在河之洲。窈窕淑女，君子好逑。</p>
<p>参差荇菜，左右流之。窈窕淑女，寤寐求之。</p>
<p>求之不得，寤寐思服。悠哉悠哉，辗转反侧。</p>
<p>参差荇菜，左右采之。窈窕淑女，琴瑟友之。</p>
<p>参差荇菜，左右芼之。窈窕淑女，钟鼓乐之。</p>
<h3 id="蒹葭-秦风" tabindex="-1"><a class="header-anchor" href="#蒹葭-秦风"><span>蒹葭（秦风）</span></a></h3>
<p>蒹葭苍苍，白露为霜。所谓伊人，在水一方。</p>
<p>溯洄从之，道阻且长。溯游从之，宛在水中央。</p>
<p>蒹葭萋萋，白露未晞。所谓伊人，在水之湄。</p>
<p>溯洄从之，道阻且跻。溯游从之，宛在水中坻。</p>
<p>蒹葭采采，白露未已。所谓伊人，在水之涘。</p>
<p>溯洄从之，道阻且右。溯游从之，宛在水中沚。</p>
<h3 id="静女-邶风" tabindex="-1"><a class="header-anchor" href="#静女-邶风"><span>静女（邶风）</span></a></h3>
<p>静女其姝，俟我于城隅。爱而不见，搔首踟蹰。</p>
<p>静女其娈，贻我彤管。彤管有炜，说怿女美。</p>
<p>自牧归荑，洵美且异。匪女之为美，美人之贻。</p>
<h3 id="采薇-小雅" tabindex="-1"><a class="header-anchor" href="#采薇-小雅"><span>采薇（小雅）</span></a></h3>
<p>采薇采薇，薇亦作止。曰归曰归，岁亦莫止。靡室靡家，猃狁之故。不遑启居，猃狁之故。</p>
<p>采薇采薇，薇亦柔止。曰归曰归，心亦忧止。忧心烈烈，载饥载渴。我戍未定，靡使归聘。</p>
<p>采薇采薇，薇亦刚止。曰归曰归，岁亦阳止。王事靡盬，不遑启处。忧心孔疚，我行不来！</p>
<p>彼尔维何？维常之华。彼路斯何？君子之车。戎车既驾，四牡业业。岂敢定居？一月三捷。</p>
<p>驾彼四牡，四牡骙骙。君子所依，小人所腓。四牡翼翼，象弭鱼服。岂不日戒？猃狁孔棘！</p>
<p>昔我往矣，杨柳依依。今我来思，雨雪霏霏。行道迟迟，载渴载饥。我心伤悲，莫知我哀！</p>

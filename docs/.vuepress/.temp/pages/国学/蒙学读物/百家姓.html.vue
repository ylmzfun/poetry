<h2 id="简介" tabindex="-1"><a class="header-anchor" href="#简介"><span>简介</span></a></h2>
<p>《百家姓》是一部关于中文姓氏的作品，成书于北宋初年，原收集姓氏411个，后增补到504个，其中单姓444个，复姓60个。《百家姓》采用四言体例，句句押韵，虽然它的内容没有文理，但读来顺口，易学好记，与《三字经》、《千字文》并称&quot;三百千&quot;，是中国古代幼儿的启蒙读物。《百家姓》成书后，流传极广，几乎家喻户晓，但实际上是一本关于中文姓氏的书。</p>

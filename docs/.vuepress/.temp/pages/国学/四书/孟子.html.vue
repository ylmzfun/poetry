<h2 id="原文选录" tabindex="-1"><a class="header-anchor" href="#原文选录"><span>原文选录</span></a></h2>
<h3 id="梁惠王章句上" tabindex="-1"><a class="header-anchor" href="#梁惠王章句上"><span>梁惠王章句上</span></a></h3>
<p>孟子见梁惠王。王曰：&quot;叟不远千里而来，亦将有以利吾国乎？&quot;</p>
<p>孟子对曰：&quot;王何必曰利？亦有仁义而已矣。王曰'何以利吾国'，大夫曰'何以利吾家'，士庶人曰'何以利吾身'，上下交征利而国危矣。万乘之国弑其君者，必千乘之家；千乘之国弑其君者，必百乘之家。万取千焉，千取百焉，不为不多矣。苟为后义而先利，不夺不餍。未有仁而遗其亲者也，未有义而后其君者也。王亦曰仁义而已矣，何必曰利？&quot;</p>
<h3 id="梁惠王章句下" tabindex="-1"><a class="header-anchor" href="#梁惠王章句下"><span>梁惠王章句下</span></a></h3>
<p>齐宣王问曰：&quot;齐桓、晋文之事可得闻乎？&quot;</p>
<p>孟子对曰：&quot;仲尼之徒无道桓、文之事者，是以后世无传焉。臣未之闻也。无以，则王乎？&quot;</p>
<p>曰：&quot;德何如，则可以王矣？&quot;</p>
<p>曰：&quot;保民而王，莫之能御也。&quot;</p>
<h3 id="公孙丑章句上" tabindex="-1"><a class="header-anchor" href="#公孙丑章句上"><span>公孙丑章句上</span></a></h3>
<p>公孙丑问曰：&quot;夫子当路于齐，管仲、晏子之功，可复许乎？&quot;</p>
<p>孟子曰：&quot;子诚齐人也，知管仲、晏子而已矣。或问乎曾西曰：'吾子与子路孰贤？'曾西蹴然曰：'吾先子之所畏也。'曰：'然则吾子与管仲孰贤？'曾西艴然不悦，曰：'尔何曾比予于管仲？管仲得君，如彼其专也；行乎国政，如彼其久也；功烈，如彼其卑也。尔何曾比予于是？'&quot;</p>
<h3 id="告子章句上" tabindex="-1"><a class="header-anchor" href="#告子章句上"><span>告子章句上</span></a></h3>
<p>告子曰：&quot;性犹湍水也，决诸东方则东流，决诸西方则西流。人性之无分于善不善也，犹水之无分于东西也。&quot;</p>
<p>孟子曰：&quot;水信无分于东西，无分于上下乎？人性之善也，犹水之就下也。人无有不善，水无有不下。今夫水，搏而跃之，可使过颡；激而行之，可使在山。是岂水之性哉？其势则然也。人之可使为不善，其性亦犹是也。&quot;</p>
<p>告子曰：&quot;生之谓性。&quot;</p>
<p>孟子曰：&quot;生之谓性也，犹白之谓白与？&quot;</p>
<p>曰：&quot;然。&quot;</p>
<p>&quot;白羽之白也，犹白雪之白；白雪之白，犹白玉之白与？&quot;</p>
<p>曰：&quot;然。&quot;</p>
<p>&quot;然则犬之性，犹牛之性；牛之性，犹人之性与？&quot;</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<h3 id="梁惠王章句注释" tabindex="-1"><a class="header-anchor" href="#梁惠王章句注释"><span>梁惠王章句注释</span></a></h3>
<p><strong>叟</strong>：老人，这里是梁惠王对孟子的尊称。</p>
<p><strong>万乘之国</strong>：拥有万辆兵车的大国。乘，古代军制单位，一车四马为一乘。</p>
<p><strong>弑</strong>：下杀上，臣杀君、子杀父。</p>
<p><strong>交征利</strong>：互相争夺利益。征，争夺。</p>
<p><strong>餍</strong>：满足。</p>
<p><strong>保民而王</strong>：保护人民而成就王业。</p>
<h3 id="告子章句注释" tabindex="-1"><a class="header-anchor" href="#告子章句注释"><span>告子章句注释</span></a></h3>
<p><strong>湍水</strong>：急流的水。</p>
<p><strong>决</strong>：开口放水。</p>
<p><strong>搏而跃之</strong>：拍击使水跳跃。搏，拍击。</p>
<p><strong>过颡</strong>：超过额头。颡，额头。</p>
<p><strong>激而行之</strong>：冲击使水流动。</p>
<p><strong>生之谓性</strong>：生来就有的叫做性。</p>
<h2 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h2>
<h3 id="梁惠王章句译文" tabindex="-1"><a class="header-anchor" href="#梁惠王章句译文"><span>梁惠王章句译文</span></a></h3>
<p>孟子拜见梁惠王。梁惠王说：&quot;老先生不远千里而来，一定是有什么对我的国家有利的建议吧？&quot;</p>
<p>孟子回答说：&quot;大王何必说利呢？只要有仁义就够了。大王说'怎样对我的国家有利'，大夫说'怎样对我的家族有利'，士人和平民说'怎样对我个人有利'，上上下下都争夺利益，国家就危险了。在拥有万辆兵车的国家里，杀害君主的一定是拥有千辆兵车的大夫；在拥有千辆兵车的国家里，杀害君主的一定是拥有百辆兵车的大夫。万辆中取千辆，千辆中取百辆，不能说不多了。如果把义放在后面，把利放在前面，不夺取到手就不满足。从来没有仁爱的人却抛弃父母的，从来没有正义的人却怠慢君主的。大王也只要说仁义就够了，何必说利呢？&quot;</p>
<h3 id="告子章句译文" tabindex="-1"><a class="header-anchor" href="#告子章句译文"><span>告子章句译文</span></a></h3>
<p>告子说：&quot;人性就像湍急的流水一样，在东边开个口子就向东流，在西边开个口子就向西流。人性没有善恶的区别，就像水没有东西方向的区别一样。&quot;</p>
<p>孟子说：&quot;水确实没有东西方向的区别，但是没有上下的区别吗？人性向善，就像水往低处流一样。人没有不善良的，水没有不往下流的。现在这水，你拍击它，可以使它跳过额头；你冲击它，可以使它停留在山上。这难道是水的本性吗？是形势使它这样的。人可以使他做坏事，他的本性也是如此。&quot;</p>
<p>告子说：&quot;生来就有的叫做性。&quot;</p>
<p>孟子说：&quot;生来就有的叫做性，就像白的叫做白一样吗？&quot;</p>
<p>告子说：&quot;是的。&quot;</p>
<p>孟子说：&quot;白羽毛的白，就像白雪的白；白雪的白，就像白玉的白一样吗？&quot;</p>
<p>告子说：&quot;是的。&quot;</p>
<p>孟子说：&quot;那么狗的本性，就像牛的本性；牛的本性，就像人的本性一样吗？&quot;</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<p>《孟子》作为儒家经典，其思想价值主要体现在以下几个方面：</p>
<h3 id="_1-性善论" tabindex="-1"><a class="header-anchor" href="#_1-性善论"><span>1. 性善论</span></a></h3>
<p>孟子提出了著名的性善论，认为人性本善，这与荀子的性恶论形成对比。通过与告子的辩论，孟子用水往下流的比喻，生动地阐述了人性向善的观点。</p>
<h3 id="_2-仁政思想" tabindex="-1"><a class="header-anchor" href="#_2-仁政思想"><span>2. 仁政思想</span></a></h3>
<p>孟子继承和发展了孔子的仁学思想，提出了系统的仁政理论。他强调&quot;保民而王&quot;，认为统治者应该以仁爱之心治理国家，保护人民。</p>
<h3 id="_3-民本思想" tabindex="-1"><a class="header-anchor" href="#_3-民本思想"><span>3. 民本思想</span></a></h3>
<p>孟子提出了&quot;民为贵，社稷次之，君为轻&quot;的民本思想，强调人民在国家中的重要地位，这一思想对后世产生了深远影响。</p>
<h3 id="_4-义利之辨" tabindex="-1"><a class="header-anchor" href="#_4-义利之辨"><span>4. 义利之辨</span></a></h3>
<p>在与梁惠王的对话中，孟子明确提出了义利之辨，强调道德品质比物质利益更重要，体现了儒家重义轻利的价值观。</p>
<h3 id="_5-修养工夫" tabindex="-1"><a class="header-anchor" href="#_5-修养工夫"><span>5. 修养工夫</span></a></h3>
<p>孟子提出了&quot;养浩然之气&quot;的修养方法，强调通过道德修养来培养正大刚直的精神品质。</p>
<h3 id="_6-政治理想" tabindex="-1"><a class="header-anchor" href="#_6-政治理想"><span>6. 政治理想</span></a></h3>
<p>孟子的政治理想是建立一个以仁义为基础的理想社会，统治者应该具备高尚的道德品质，实行仁政，使人民安居乐业。</p>
<p>《孟子》的思想不仅丰富了儒家学说，也为中国传统文化的发展奠定了重要基础。其性善论、仁政思想、民本思想等至今仍具有重要的现实意义。</p>

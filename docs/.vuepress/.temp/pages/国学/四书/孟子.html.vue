<h1 id="孟子" tabindex="-1"><a class="header-anchor" href="#孟子"><span>孟子</span></a></h1>
<h2 id="简介" tabindex="-1"><a class="header-anchor" href="#简介"><span>简介</span></a></h2>
<p>《孟子》是记录孟子及其弟子政治、教育、哲学、伦理等思想观点和政治活动的书，由孟子及其弟子万章、公孙丑等人编撰。全书分为《梁惠王》、《公孙丑》、《滕文公》、《离娄》、《万章》、《告子》、《尽心》七篇，每篇分为上下两章，共十四卷。孟子被尊为&quot;亚圣&quot;，其思想对后世儒家学说发展产生了深远影响。</p>

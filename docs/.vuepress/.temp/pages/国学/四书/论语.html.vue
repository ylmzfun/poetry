<h2 id="原文选录" tabindex="-1"><a class="header-anchor" href="#原文选录"><span>原文选录</span></a></h2>
<h3 id="学而篇第一" tabindex="-1"><a class="header-anchor" href="#学而篇第一"><span>学而篇第一</span></a></h3>
<p>子曰：&quot;学而时习之，不亦说乎？有朋自远方来，不亦乐乎？人不知而不愠，不亦君子乎？&quot;</p>
<p>有子曰：&quot;其为人也孝弟，而好犯上者，鲜矣；不好犯上，而好作乱者，未之有也。君子务本，本立而道生。孝弟也者，其为仁之本与！&quot;</p>
<p>子曰：&quot;巧言令色，鲜矣仁！&quot;</p>
<p>曾子曰：&quot;吾日三省吾身：为人谋而不忠乎？与朋友交而不信乎？传不习乎？&quot;</p>
<p>子曰：&quot;弟子，入则孝，出则弟，谨而信，泛爱众，而亲仁。行有余力，则以学文。&quot;</p>
<h3 id="为政篇第二" tabindex="-1"><a class="header-anchor" href="#为政篇第二"><span>为政篇第二</span></a></h3>
<p>子曰：&quot;为政以德，譬如北辰，居其所而众星共之。&quot;</p>
<p>子曰：&quot;《诗》三百，一言以蔽之，曰：'思无邪'。&quot;</p>
<p>子曰：&quot;吾十有五而志于学，三十而立，四十而不惑，五十而知天命，六十而耳顺，七十而从心所欲，不逾矩。&quot;</p>
<p>孟懿子问孝。子曰：&quot;无违。&quot;樊迟御，子告之曰：&quot;孟孙问孝于我，我对曰，'无违'。&quot;樊迟曰：&quot;何谓也？&quot;子曰：&quot;生，事之以礼；死，葬之以礼，祭之以礼。&quot;</p>
<h3 id="八佾篇第三" tabindex="-1"><a class="header-anchor" href="#八佾篇第三"><span>八佾篇第三</span></a></h3>
<p>子曰：&quot;《关雎》，乐而不淫，哀而不伤。&quot;</p>
<h3 id="里仁篇第四" tabindex="-1"><a class="header-anchor" href="#里仁篇第四"><span>里仁篇第四</span></a></h3>
<p>子曰：&quot;里仁为美。择不处仁，焉得知？&quot;</p>
<p>子曰：&quot;不仁者不可以久处约，不可以长处乐。仁者安仁，知者利仁。&quot;</p>
<p>子曰：&quot;唯仁者能好人，能恶人。&quot;</p>
<h3 id="公冶长篇第五" tabindex="-1"><a class="header-anchor" href="#公冶长篇第五"><span>公冶长篇第五</span></a></h3>
<p>子谓公冶长：&quot;可妻也。虽在缧绁之中，非其罪也。&quot;以其子妻之。</p>
<p>子谓南容：&quot;邦有道，不废；邦无道，免于刑戮。&quot;以其兄之子妻之。</p>
<h3 id="雍也篇第六" tabindex="-1"><a class="header-anchor" href="#雍也篇第六"><span>雍也篇第六</span></a></h3>
<p>子曰：&quot;雍也可使南面。&quot;</p>
<p>仲弓问子桑伯子。子曰：&quot;可也，简。&quot;仲弓曰：&quot;居敬而行简，以临其民，不亦可乎？居简而行简，无乃大简乎？&quot;子曰：&quot;雍之言然。&quot;</p>
<p>哀公问：&quot;弟子孰为好学？&quot;孔子对曰：&quot;有颜回者好学，不迁怒，不贰过。不幸短命死矣。今也则亡，未闻好学者也。&quot;</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<h3 id="学而篇注释" tabindex="-1"><a class="header-anchor" href="#学而篇注释"><span>学而篇注释</span></a></h3>
<p><strong>学而时习之</strong>：学习并且按时复习。时，按时；习，复习、实习。</p>
<p><strong>说</strong>：通&quot;悦&quot;，高兴、愉快。</p>
<p><strong>愠</strong>：怨恨、恼怒。</p>
<p><strong>孝弟</strong>：孝顺父母，敬爱兄长。弟，通&quot;悌&quot;。</p>
<p><strong>犯上</strong>：冒犯在上位的人。</p>
<p><strong>鲜</strong>：少。</p>
<p><strong>务本</strong>：致力于根本。务，致力于。</p>
<p><strong>巧言令色</strong>：花言巧语，伪善的面容。令，美好。</p>
<p><strong>三省</strong>：多次反省。省，反省。</p>
<p><strong>谨而信</strong>：言行谨慎而诚实守信。</p>
<p><strong>泛爱众</strong>：广泛地爱护众人。</p>
<p><strong>亲仁</strong>：亲近有仁德的人。</p>
<h3 id="为政篇注释" tabindex="-1"><a class="header-anchor" href="#为政篇注释"><span>为政篇注释</span></a></h3>
<p><strong>北辰</strong>：北极星。</p>
<p><strong>共</strong>：通&quot;拱&quot;，环绕。</p>
<p><strong>思无邪</strong>：思想纯正。出自《诗经·鲁颂·駉》。</p>
<p><strong>而立</strong>：能够立身处世。</p>
<p><strong>不惑</strong>：不被外物所迷惑。</p>
<p><strong>知天命</strong>：了解天命，知道自己的使命。</p>
<p><strong>耳顺</strong>：听到什么都能理解其真意。</p>
<p><strong>从心所欲，不逾矩</strong>：随心所欲而不违背法度。</p>
<p><strong>无违</strong>：不要违背。</p>
<h2 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h2>
<h3 id="学而篇译文" tabindex="-1"><a class="header-anchor" href="#学而篇译文"><span>学而篇译文</span></a></h3>
<p>孔子说：&quot;学习并且按时复习，不是很愉快吗？有朋友从远方来，不是很快乐吗？别人不了解我，我也不怨恨，不也是君子吗？&quot;</p>
<p>有子说：&quot;一个人孝顺父母，敬爱兄长，却喜欢冒犯在上位的人，这种情况是很少的；不喜欢冒犯在上位的人，却喜欢造反的，是没有的。君子致力于根本，根本确立了，道就产生了。孝顺父母、敬爱兄长，这就是仁的根本吧！&quot;</p>
<p>孔子说：&quot;花言巧语，伪善的面容，这样的人很少有仁德！&quot;</p>
<p>曾子说：&quot;我每天多次反省自己：为别人办事是否尽心竭力了呢？与朋友交往是否诚实可信了呢？老师传授的知识是否复习了呢？&quot;</p>
<p>孔子说：&quot;弟子们，在家要孝顺父母，出外要敬爱兄长，言行要谨慎诚信，要广泛地爱护众人，而亲近有仁德的人。这样躬行实践之后，还有余力的话，就再去学习文献知识。&quot;</p>
<h3 id="为政篇译文" tabindex="-1"><a class="header-anchor" href="#为政篇译文"><span>为政篇译文</span></a></h3>
<p>孔子说：&quot;用道德来治理国政，就像北极星一样，安居在自己的位置上，而群星都围绕着它。&quot;</p>
<p>孔子说：&quot;《诗经》三百篇，用一句话来概括它，就是：'思想纯正'。&quot;</p>
<p>孔子说：&quot;我十五岁立志学习，三十岁能够立身处世，四十岁遇事不迷惑，五十岁懂得了天命，六十岁听到什么都能理解其真意，七十岁能随心所欲而不违背法度。&quot;</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<p>《论语》作为儒家思想的重要源泉，其价值体现在以下几个方面：</p>
<h3 id="_1-教育思想" tabindex="-1"><a class="header-anchor" href="#_1-教育思想"><span>1. 教育思想</span></a></h3>
<p>孔子提出了&quot;有教无类&quot;、&quot;因材施教&quot;等重要教育理念，强调学习的重要性和方法。&quot;学而时习之&quot;体现了理论与实践相结合的学习观。</p>
<h3 id="_2-道德修养" tabindex="-1"><a class="header-anchor" href="#_2-道德修养"><span>2. 道德修养</span></a></h3>
<p>《论语》强调仁、义、礼、智、信等道德品质，提出了&quot;君子&quot;的理想人格。&quot;仁者爱人&quot;、&quot;己所不欲，勿施于人&quot;等思想至今仍有重要意义。</p>
<h3 id="_3-政治理念" tabindex="-1"><a class="header-anchor" href="#_3-政治理念"><span>3. 政治理念</span></a></h3>
<p>孔子主张&quot;为政以德&quot;，强调道德在政治中的重要作用，提出了&quot;民为贵，社稷次之，君为轻&quot;的民本思想。</p>
<h3 id="_4-人生哲学" tabindex="-1"><a class="header-anchor" href="#_4-人生哲学"><span>4. 人生哲学</span></a></h3>
<p>通过&quot;吾十有五而志于学&quot;等名言，展现了孔子对人生不同阶段的深刻理解，体现了积极进取的人生态度。</p>
<h3 id="_5-语言艺术" tabindex="-1"><a class="header-anchor" href="#_5-语言艺术"><span>5. 语言艺术</span></a></h3>
<p>《论语》语言简洁明快，富有哲理，许多语句成为千古名言，对中国文学和语言发展产生了深远影响。</p>
<p>《论语》不仅是中国古代思想文化的瑰宝，也是世界文化遗产的重要组成部分，其思想精华至今仍具有重要的现实意义。</p>

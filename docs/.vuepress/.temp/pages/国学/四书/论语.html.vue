<h1 id="论语" tabindex="-1"><a class="header-anchor" href="#论语"><span>论语</span></a></h1>
<h2 id="简介" tabindex="-1"><a class="header-anchor" href="#简介"><span>简介</span></a></h2>
<p>《论语》是记录孔子及其弟子言行的语录体散文集，由孔子弟子及再传弟子编撰而成。全书共20篇，492章，是研究孔子思想和早期儒家学说的重要文献。《论语》内容涉及政治、教育、文学、哲学以及立身处世的道理等多个方面，被誉为&quot;东方的《圣经》&quot;。</p>
<h2 id="推荐版本" tabindex="-1"><a class="header-anchor" href="#推荐版本"><span>推荐版本</span></a></h2>

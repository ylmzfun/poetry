<h2 id="经典篇章选录" tabindex="-1"><a class="header-anchor" href="#经典篇章选录"><span>经典篇章选录</span></a></h2>
<h3 id="逍遥游-内篇第一" tabindex="-1"><a class="header-anchor" href="#逍遥游-内篇第一"><span>逍遥游（内篇第一）</span></a></h3>
<p><strong>原文节选</strong>：
北冥有鱼，其名为鲲。鲲之大，不知其几千里也。化而为鸟，其名为鹏。鹏之背，不知其几千里也；怒而飞，其翼若垂天之云。是鸟也，海运则将徙于南冥。南冥者，天池也。</p>
<p><strong>原文节选</strong>：
至人无己，神人无功，圣人无名。</p>
<h3 id="齐物论-内篇第二" tabindex="-1"><a class="header-anchor" href="#齐物论-内篇第二"><span>齐物论（内篇第二）</span></a></h3>
<p><strong>原文节选</strong>：
天地与我并生，而万物与我为一。既已为一矣，且得有言乎？既已谓之一矣，且得无言乎？一与言为二，二与一为三。自此以往，巧历不能得，而况其凡乎！故自无适有，以至于三，而况自有适有乎！无适焉，因是已。</p>
<p><strong>原文节选</strong>：
方生方死，方死方生；方可方不可，方不可方可；因是因非，因非因是。是以圣人不由，而照之于天，亦因是也。</p>
<h3 id="养生主-内篇第三" tabindex="-1"><a class="header-anchor" href="#养生主-内篇第三"><span>养生主（内篇第三）</span></a></h3>
<p><strong>原文节选</strong>：
吾生也有涯，而知也无涯。以有涯随无涯，殆已！已而为知者，殆而已矣！</p>
<p><strong>原文节选</strong>：
庖丁为文惠君解牛，手之所触，肩之所倚，足之所履，膝之所踦，砉然向然，奏刀騞然，莫不中音。合于《桑林》之舞，乃中《经首》之会。</p>
<h3 id="人间世-内篇第四" tabindex="-1"><a class="header-anchor" href="#人间世-内篇第四"><span>人间世（内篇第四）</span></a></h3>
<p><strong>原文节选</strong>：
颜回曰：&quot;回之家贫，唯不饮酒不茹荤者数月矣。如此，则可以为斋乎？&quot;曰：&quot;是祭祀之斋，非心斋也。&quot;回曰：&quot;敢问心斋。&quot;仲尼曰：&quot;若一志，无听之以耳而听之以心，无听之以心而听之以气。听止于耳，心止于符。气也者，虚而待物者也。唯道集虚。虚者，心斋也。&quot;</p>
<h3 id="德充符-内篇第五" tabindex="-1"><a class="header-anchor" href="#德充符-内篇第五"><span>德充符（内篇第五）</span></a></h3>
<p><strong>原文节选</strong>：
鲁有兀者王骀，从之游者与仲尼相若。常季问于仲尼曰：&quot;王骀，兀者也，从之游者与夫子中分鲁。立不教，坐不议，虚而往，实而归。固有不言之教，无形而心成者邪？是何人也？&quot;</p>
<h3 id="大宗师-内篇第六" tabindex="-1"><a class="header-anchor" href="#大宗师-内篇第六"><span>大宗师（内篇第六）</span></a></h3>
<p><strong>原文节选</strong>：
知天之所为，知人之所为者，至矣。知天之所为者，天而生也；知人之所为者，以其知之所知以养其知之所不知，终其天年而不中道夭者，是知之盛也。</p>
<h3 id="应帝王-内篇第七" tabindex="-1"><a class="header-anchor" href="#应帝王-内篇第七"><span>应帝王（内篇第七）</span></a></h3>
<p><strong>原文节选</strong>：
南海之帝为儵，北海之帝为忽，中央之帝为浑沌。儵与忽时相与遇于浑沌之地，浑沌待之甚善。儵与忽谋报浑沌之德，曰：&quot;人皆有七窍以视听食息，此独无有，尝试凿之。&quot;日凿一窍，七日而浑沌死。</p>

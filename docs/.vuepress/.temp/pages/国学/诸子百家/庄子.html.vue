<h2 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h2>
<h3 id="逍遥游译文" tabindex="-1"><a class="header-anchor" href="#逍遥游译文"><span>逍遥游译文</span></a></h3>
<p>北方的大海里有一条鱼，它的名字叫鲲。鲲的巨大，不知道有几千里。它变化成为鸟，名字叫鹏。鹏的脊背，不知道有几千里长；当它奋力飞翔的时候，翅膀就像悬挂在天空的云彩。这只鸟，当海水运动的时候，就要迁移到南方的大海。南方的大海，就是天然的大池。</p>
<p>至人没有自我的偏见，神人不追求功名，圣人不追求名声。</p>
<h3 id="齐物论译文" tabindex="-1"><a class="header-anchor" href="#齐物论译文"><span>齐物论译文</span></a></h3>
<p>天地与我同时产生，万物与我合为一体。既然已经合为一体了，还能够说什么呢？既然已经称它为一了，还能够不说什么吗？一和言合为二，二和一合为三。从此以后，即使最精巧的计算也不能得出结果，何况是一般的计算呢！所以从无到有，以至于三，何况从有到有呢！不要执着于什么，顺应事物本来的样子就行了。</p>
<h3 id="养生主译文" tabindex="-1"><a class="header-anchor" href="#养生主译文"><span>养生主译文</span></a></h3>
<p>我的生命是有限的，而知识是无限的。用有限的生命去追求无限的知识，真是危险啊！已经这样了还要追求知识，那就更危险了！</p>
<p>厨师为文惠君宰牛，手接触的地方，肩膀靠着的地方，脚踩着的地方，膝盖顶着的地方，都发出砉砉的声音，进刀时騞騞作响，没有不合音律的。合于《桑林》舞曲的节拍，又合于《经首》乐章的节奏。</p>
<h3 id="人间世译文" tabindex="-1"><a class="header-anchor" href="#人间世译文"><span>人间世译文</span></a></h3>
<p>颜回说：&quot;我家里贫穷，已经几个月不喝酒不吃荤了。这样，可以算是斋戒了吧？&quot;孔子说：&quot;这是祭祀时的斋戒，不是心斋。&quot;颜回说：&quot;请问什么是心斋？&quot;孔子说：&quot;专一你的意志，不要用耳朵听而要用心听，不要用心听而要用气听。听觉止于耳朵，心思止于符合。气，是虚静地等待事物的。只有道聚集在虚静中。虚静，就是心斋。&quot;</p>

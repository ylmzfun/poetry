<h1 id="庄子" tabindex="-1"><a class="header-anchor" href="#庄子"><span>庄子</span></a></h1>
<h2 id="简介" tabindex="-1"><a class="header-anchor" href="#简介"><span>简介</span></a></h2>
<p>《庄子》是战国时期庄周及其后学所著的哲学著作，是道家学派的重要经典。全书现存三十三篇，分为内篇七篇、外篇十五篇、杂篇十一篇。内篇一般认为是庄子本人所作，外篇和杂篇则可能是其后学所著。《庄子》继承和发展了老子的道家思想，以其独特的哲学观点、丰富的想象力和优美的文学语言，成为中国古代哲学和文学的双重经典。</p>
<h2 id="推荐版本" tabindex="-1"><a class="header-anchor" href="#推荐版本"><span>推荐版本</span></a></h2>

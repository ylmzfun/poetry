<li><strong>成玄英疏本</strong>：唐代成玄英疏解，道教色彩浓厚</li>
<li><strong>王先谦集解</strong>：清代王先谦集各家注释之大成</li>
<li><strong>现代注释</strong>：陈鼓应《庄子今注今译》，现代学者研究成果</li>
</ul>

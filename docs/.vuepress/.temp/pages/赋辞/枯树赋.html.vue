<h1 id="枯树赋" tabindex="-1"><a class="header-anchor" href="#枯树赋"><span>枯树赋</span></a></h1>
<p><strong>作者：</strong> 庾信<br>
<strong>体裁：</strong> 赋</p>
<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p>殷仲文风流儒雅，海内知名。世异时移，出为东阳太守。常忽忽不乐，顾庭槐而叹曰：&quot;此树婆娑，生意尽矣！&quot;</p>
<p>至如白鹿贞松，青牛文梓。根柢盘魄，山崖表里。桂何事而销亡？桐何为而半死？昔之三河徙植，九畹移根。开花建始之殿，落实睢阳之园。声含嶰谷，曲抱《云门》。将雏比翼，连理分根。</p>
<p>及至山河改色，宇宙迁移。亭皋木落，洞庭波起。彼岸花开，此岸花落。昔年种柳，依依汉南；今看摇落，凄怆江潭。树犹如此，人何以堪！</p>
<p>若夫松子古度，平仲今时。搴木兰于山椒，结芳兰于水湄。繁华易逝，盛年不再。昔日芳草，今为萧艾。人生如寄，多忧何为？</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<h3 id="注释-1" tabindex="-1"><a class="header-anchor" href="#注释-1"><span>注释</span></a></h3>
<p><strong>殷仲文</strong>：南朝宋文学家，名景仁，字仲文。</p>
<p><strong>婆娑</strong>：枝叶繁茂的样子。</p>
<p><strong>生意</strong>：生机，活力。</p>
<p><strong>白鹿贞松</strong>：传说中的神树。</p>
<p><strong>青牛文梓</strong>：传说中老子骑青牛出关，文梓指有花纹的梓木。</p>
<p><strong>根柢盘魄</strong>：树根盘结深固。</p>
<p><strong>三河徙植</strong>：指古代移植名贵树木。</p>
<p><strong>九畹移根</strong>：畹，古代田地单位，指大规模移植。</p>
<p><strong>建始之殿</strong>：汉成帝时宫殿名。</p>
<p><strong>睢阳之园</strong>：古代名园。</p>
<p><strong>嶰谷</strong>：古代音律名。</p>
<p><strong>云门</strong>：古代乐曲名。</p>
<p><strong>将雏比翼</strong>：幼鸟比翼双飞。</p>
<p><strong>连理分根</strong>：连理枝分离。</p>
<p><strong>亭皋</strong>：水边平地。</p>
<p><strong>搴</strong>：采取。</p>
<p><strong>山椒</strong>：山顶。</p>
<p><strong>水湄</strong>：水边。</p>
<p><strong>萧艾</strong>：蒿草，比喻衰败。</p>
<h3 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h3>
<p>殷仲文风流儒雅，在海内很有名声。世事变迁，时代更替，他出任东阳太守。常常郁郁不乐，看着庭院中的槐树叹息说：&quot;这棵树枝叶婆娑，但生机已经完全没有了！&quot;</p>
<p>至于那些白鹿贞松、青牛文梓，根系盘结深固，表里山崖都有。桂花为什么凋零？梧桐为什么半死？从前在三河移植，在九畹迁根。在建始殿开花，在睢阳园结果。声音包含着嶰谷的韵律，曲调怀抱着《云门》的旋律。幼鸟比翼双飞，连理枝分根而生。</p>
<p>等到山河改色，宇宙迁移。水边平地树叶凋落，洞庭湖波浪翻起。彼岸花开，此岸花落。昔年种柳，依依在汉水之南；今日看到摇落，凄怆在江边水潭。树尚且如此，人又怎么能承受！</p>
<p>至于松子古度、平仲今时这样的人物，在山顶采摘木兰，在水边结取芳兰。繁华容易逝去，盛年不会再来。昔日的芳草，今天变成了蒿艾。人生如同寄居，多忧又有什么用？</p>

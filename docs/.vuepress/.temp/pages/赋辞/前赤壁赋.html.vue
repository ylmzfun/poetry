<h1 id="前赤壁赋" tabindex="-1"><a class="header-anchor" href="#前赤壁赋"><span>前赤壁赋</span></a></h1>
<p><strong>作者：</strong> 苏轼（北宋）<br>
<strong>体裁：</strong> 赋<br>
<strong>创作时间：</strong> 元丰五年（1082年）</p>
<h2 id="简介" tabindex="-1"><a class="header-anchor" href="#简介"><span>简介</span></a></h2>
<p>《前赤壁赋》是苏轼被贬黄州期间的代表作，与《后赤壁赋》并称&quot;二赋&quot;。作品以游赤壁为线索，通过主客问答的形式，表达了作者对人生短暂与宇宙永恒的哲理思考，体现了苏轼豁达乐观的人生态度。</p>

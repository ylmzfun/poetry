<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p>壬戌之秋，七月既望，苏子与客泛舟游于赤壁之下。清风徐来，水波不兴。举酒属客，诵明月之诗，歌窈窕之章。少焉，月出于东山之上，徘徊于斗牛之间。白露横江，水光接天。纵一苇之所如，凌万顷之茫然。浩浩乎如冯虚御风，而不知其所止；飘飘乎如遗世独立，羽化而登仙。</p>
<p>于是饮酒乐甚，扣舷而歌之。歌曰：&quot;桂棹兮兰桨，击空明兮溯流光。渺渺兮予怀，望美人兮天一方。&quot;客有吹洞箫者，倚歌而和之。其声呜呜然，如怨如慕，如泣如诉，余音袅袅，不绝如缕。舞幽壑之潜蛟，泣孤舟之嫠妇。</p>
<p>苏子愀然，正襟危坐而问客曰：&quot;何为其然也？&quot;客曰：&quot;'月明星稀，乌鹊南飞'，此非曹孟德之诗乎？西望夏口，东望武昌，山川相缪，郁乎苍苍，此非孟德之困于周郎者乎？方其破荆州，下江陵，顺流而东也，舳舻千里，旌旗蔽空，酾酒临江，横槊赋诗，固一世之雄也，而今安在哉？况吾与子渔樵于江渚之上，侣鱼虾而友麋鹿，驾一叶之扁舟，举匏樽以相属。寄蜉蝣于天地，渺沧海之一粟。哀吾生之须臾，羡长江之无穷。挟飞仙以遨游，抱明月而长终。知不可乎骤得，托遗响于悲风。&quot;</p>
<p>苏子曰：&quot;客亦知夫水与月乎？逝者如斯，而未尝往也；盈虚者如彼，而卒莫消长也。盖将自其变者而观之，则天地曾不能以一瞬；自其不变者而观之，则物与我皆无尽也，而又何羡乎！且夫天地之间，物各有主，苟非吾之所有，虽一毫而莫取。惟江上之清风，与山间之明月，耳得之而为声，目遇之而成色，取之无禁，用之不竭，是造物者之无尽藏也，而吾与子之所共适。&quot;</p>
<p>客喜而笑，洗盏更酌。肴核既尽，杯盘狼籍。相与枕藉乎舟中，不知东方之既白。</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<ul>
<li><strong>壬戌之秋</strong>：元丰五年（1082年）秋。壬戌，干支纪年。</li>
<li><strong>七月既望</strong>：农历七月十六日。既望，农历每月十六日。</li>
<li><strong>属客</strong>：劝客人喝酒。属，劝酒。</li>
<li><strong>明月之诗</strong>：指《诗经·陈风·月出》。</li>
<li><strong>窈窕之章</strong>：指《诗经·周南·关雎》中&quot;窈窕淑女&quot;一章。</li>
<li><strong>斗牛</strong>：星宿名，指斗宿和牛宿。</li>
<li><strong>纵一苇之所如</strong>：任凭小船飘荡。苇，小船。</li>
<li><strong>冯虚御风</strong>：乘风而行。冯，通&quot;凭&quot;。</li>
<li><strong>桂棹兮兰桨</strong>：桂木做的船桨，兰木做的船桨。</li>
<li><strong>美人</strong>：指理想中的君主，此处指怀念朝廷。</li>
<li><strong>舳舻</strong>：船只首尾相接。</li>
<li><strong>酾酒</strong>：滤酒。</li>
<li><strong>横槊</strong>：横持长矛。</li>
<li><strong>匏樽</strong>：用葫芦做的酒器。</li>
<li><strong>蜉蝣</strong>：朝生暮死的小虫，比喻人生短暂。</li>
<li><strong>须臾</strong>：片刻，形容时间短暂。</li>
</ul>

<h1 id="病起书怀" tabindex="-1"><a class="header-anchor" href="#病起书怀"><span>病起书怀</span></a></h1>
<p>作者：陆游（南宋）
体裁：七言律诗</p>
<p>导言</p>
<ul>
<li>身世之感与家国之忧交织，语意悲壮。</li>
</ul>
<p>底本与版本</p>
<ul>
<li>推荐检索：中华书局《陆游集》点校本；旁证《全宋诗》。</li>
</ul>
<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p><strong>病起书怀</strong></p>
<p>病骨支离纱帽宽，孤臣万里客江干。
位卑未敢忘忧国，事定犹须待阖棺。
天地神灵扶庙社，京华父老望和銮。
出师一表通今古，夜半挑灯更细看。</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<h3 id="注释-1" tabindex="-1"><a class="header-anchor" href="#注释-1"><span>注释</span></a></h3>
<p><strong>病起书怀</strong>：病愈后书写心中的感怀。病起，病后初愈。书怀，书写心中的感怀。</p>
<p><strong>病骨支离</strong>：病后身体瘦弱。支离，瘦弱的样子。</p>
<p><strong>纱帽宽</strong>：官帽显得宽大了，暗示身体消瘦。纱帽，古代官员戴的帽子。</p>
<p><strong>孤臣</strong>：孤独的臣子，作者自指。</p>
<p><strong>万里客江干</strong>：在万里之外的江边作客。江干，江边。</p>
<p><strong>位卑未敢忘忧国</strong>：地位低微但不敢忘记忧虑国事。</p>
<p><strong>事定犹须待阖棺</strong>：事情的成败要等到死后才能定论。阖棺，盖棺，指死亡。</p>
<p><strong>天地神灵扶庙社</strong>：天地神灵保佑国家。庙社，宗庙和社稷，代指国家。</p>
<p><strong>京华父老望和銮</strong>：京城的父老百姓盼望皇帝回归。京华，京城。和銮，皇帝的车驾。</p>
<p><strong>出师一表通今古</strong>：诸葛亮的《出师表》古今传诵。</p>
<p><strong>夜半挑灯更细看</strong>：半夜里挑亮灯火仔细阅读。</p>
<h3 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h3>
<p>病后身体瘦弱，官帽都显得宽大了，孤独的臣子在万里之外的江边作客。地位虽然低微但不敢忘记忧虑国事，事情的成败要等到死后才能定论。</p>
<p>天地神灵保佑着国家，京城的父老百姓盼望着皇帝回归。诸葛亮的《出师表》古今传诵，我在半夜里挑亮灯火仔细阅读。</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<h3 id="创作背景" tabindex="-1"><a class="header-anchor" href="#创作背景"><span>创作背景</span></a></h3>
<p>这首诗作于1186年（淳熙十三年），陆游时年62岁，在严州（今浙江建德）任通判。当时他因病初愈，感慨身世，忧虑国事，遂作此诗。此时距离他被罢官已有多年，壮志难酬的痛苦更加深刻。</p>
<h3 id="主题思想" tabindex="-1"><a class="header-anchor" href="#主题思想"><span>主题思想</span></a></h3>
<ol>
<li><strong>忧国忧民</strong>：表达了深沉的爱国主义情感和对国家命运的担忧</li>
<li><strong>壮志难酬</strong>：感叹自己虽有报国之志却无法实现</li>
<li><strong>身世之感</strong>：表达了对个人遭遇的感慨</li>
<li><strong>精神追求</strong>：通过学习诸葛亮表达对忠臣良将的景仰</li>
</ol>
<h3 id="艺术特色" tabindex="-1"><a class="header-anchor" href="#艺术特色"><span>艺术特色</span></a></h3>
<ol>
<li><strong>情景交融</strong>：将个人的病后感受与国家忧患完美结合</li>
<li><strong>对比鲜明</strong>：个人的卑微与忧国的伟大形成对比</li>
<li><strong>用典精当</strong>：引用诸葛亮《出师表》，增强表达效果</li>
<li><strong>语言朴实</strong>：语言平实而深沉，感情真挚</li>
</ol>
<h3 id="表现手法" tabindex="-1"><a class="header-anchor" href="#表现手法"><span>表现手法</span></a></h3>
<ol>
<li><strong>直抒胸臆</strong>：直接表达内心的忧国情怀</li>
<li><strong>借古喻今</strong>：借诸葛亮的忠诚表达自己的志向</li>
<li><strong>细节描写</strong>：通过&quot;纱帽宽&quot;等细节表现病后状态</li>
<li><strong>虚实结合</strong>：既有实写的病后感受，又有虚写的忧国情怀</li>
</ol>
<h3 id="结构分析" tabindex="-1"><a class="header-anchor" href="#结构分析"><span>结构分析</span></a></h3>
<p><strong>首联</strong>：写病后的身体状况和客居他乡的孤独
<strong>颔联</strong>：表明忧国之志，强调忠诚不渝
<strong>颈联</strong>：写对国家的祈愿和对朝廷的期望
<strong>尾联</strong>：借诸葛亮表达自己的精神追求</p>
<h3 id="名句赏析" tabindex="-1"><a class="header-anchor" href="#名句赏析"><span>名句赏析</span></a></h3>
<ol>
<li>
<p><strong>&quot;病骨支离纱帽宽，孤臣万里客江干&quot;</strong>：形象地写出了病后的憔悴和客居的孤独。</p>
</li>
<li>
<p><strong>&quot;位卑未敢忘忧国，事定犹须待阖棺&quot;</strong>：这是全诗的名句，表达了不因地位低微而忘记忧国，体现了高尚的品格。</p>
</li>
<li>
<p><strong>&quot;天地神灵扶庙社，京华父老望和銮&quot;</strong>：表达了对国家的祈愿和对朝廷的期望。</p>
</li>
<li>
<p><strong>&quot;出师一表通今古，夜半挑灯更细看&quot;</strong>：借诸葛亮的《出师表》表达自己的精神追求和学习榜样。</p>
</li>
</ol>

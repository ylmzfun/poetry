<h1 id="书愤" tabindex="-1"><a class="header-anchor" href="#书愤"><span>书愤</span></a></h1>
<p>作者：陆游（南宋）
体裁：七言律诗</p>
<p>导言</p>
<ul>
<li>家国之忧与志节之怀，深沉激越。</li>
</ul>
<p>底本与版本</p>
<ul>
<li>推荐检索：中华书局《陆游集》点校本；旁证《全宋诗》。</li>
</ul>
<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p><strong>书愤</strong></p>
<p>早岁那知世事艰，中原北望气如山。
楼船夜雪瓜洲渡，铁马秋风大散关。
塞上长城空自许，镜中衰鬓已先斑。
出师一表真名世，千载谁堪伯仲间？</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<h3 id="注释-1" tabindex="-1"><a class="header-anchor" href="#注释-1"><span>注释</span></a></h3>
<p><strong>书愤</strong>：书写愤慨之情。书，写。愤，愤慨、愤懑。</p>
<p><strong>早岁</strong>：年轻的时候。</p>
<p><strong>那知</strong>：哪里知道。那，同&quot;哪&quot;。</p>
<p><strong>世事艰</strong>：世事的艰难。</p>
<p><strong>中原北望</strong>：向北眺望中原。中原，指被金人占领的北方地区。</p>
<p><strong>气如山</strong>：气概如山一样雄伟。</p>
<p><strong>楼船夜雪瓜洲渡</strong>：在雪夜里楼船停泊在瓜洲渡口。楼船，古代水军使用的大型战船。瓜洲渡，在今江苏扬州南，是长江渡口。</p>
<p><strong>铁马秋风大散关</strong>：在秋风中铁骑驰骋在大散关。铁马，披着铁甲的战马，借指骑兵。大散关，在今陕西宝鸡西南，是古代重要关隘。</p>
<p><strong>塞上长城空自许</strong>：空自把自己比作塞上的长城。塞上长城，这里用南朝名将檀道济的典故，檀道济被杀时说&quot;乃坏汝万里长城&quot;。空自许，空自夸许。</p>
<p><strong>镜中衰鬓已先斑</strong>：镜中看到自己的鬓发已经花白。衰鬓，衰老的鬓发。斑，花白。</p>
<p><strong>出师一表真名世</strong>：《出师表》真正名垂千古。指诸葛亮的《出师表》。</p>
<p><strong>千载谁堪伯仲间</strong>：千年来谁能和诸葛亮相提并论？伯仲间，兄弟之间，这里指同等地位。</p>
<h3 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h3>
<p>年轻时哪里知道世事的艰难，向北眺望中原时气概如山一样雄伟。记得在雪夜里楼船停泊瓜洲渡口，在秋风中铁骑驰骋大散关。</p>
<p>空自把自己比作塞上的长城，镜中看到衰老的鬓发已经花白。诸葛亮的《出师表》真正名垂千古，千年来谁能和他相提并论？</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<h3 id="创作背景" tabindex="-1"><a class="header-anchor" href="#创作背景"><span>创作背景</span></a></h3>
<p>这首诗作于1192年（绍熙三年），陆游时年68岁，居住在山阴（今浙江绍兴）。当时南宋朝廷对金妥协求和，陆游的抗金主张不被采纳，壮志难酬，遂作此诗抒发愤慨之情。</p>
<h3 id="主题思想" tabindex="-1"><a class="header-anchor" href="#主题思想"><span>主题思想</span></a></h3>
<ol>
<li><strong>爱国情怀</strong>：表达了深沉的爱国主义情感和收复失地的强烈愿望</li>
<li><strong>壮志难酬</strong>：感叹自己空有报国之志却无法实现</li>
<li><strong>英雄迟暮</strong>：表达了年华老去、功业未成的悲愤</li>
<li><strong>历史感慨</strong>：通过对诸葛亮的景仰，表达对忠臣良将的敬仰</li>
</ol>
<h3 id="艺术特色" tabindex="-1"><a class="header-anchor" href="#艺术特色"><span>艺术特色</span></a></h3>
<ol>
<li><strong>对比鲜明</strong>：青年时的豪情与老年时的现实形成强烈对比</li>
<li><strong>情景交融</strong>：将个人情感与历史场景完美结合</li>
<li><strong>用典精当</strong>：运用檀道济、诸葛亮等典故，增强表达效果</li>
<li><strong>语言沉郁</strong>：语言深沉有力，感情激越</li>
</ol>
<h3 id="表现手法" tabindex="-1"><a class="header-anchor" href="#表现手法"><span>表现手法</span></a></h3>
<ol>
<li><strong>今昔对比</strong>：通过今昔对比突出壮志难酬的悲愤</li>
<li><strong>借古讽今</strong>：借诸葛亮的忠诚反衬当时朝廷的昏庸</li>
<li><strong>虚实结合</strong>：既有实写的经历，又有虚写的感慨</li>
<li><strong>直抒胸臆</strong>：直接表达内心的愤慨和不平</li>
</ol>
<h3 id="结构分析" tabindex="-1"><a class="header-anchor" href="#结构分析"><span>结构分析</span></a></h3>
<p><strong>首联</strong>：总起，对比早年不知世事艰难与后来的深刻体验
<strong>颔联</strong>：回忆，追述当年参加抗金斗争的经历
<strong>颈联</strong>：感慨，对比理想与现实，表达壮志难酬的悲愤
<strong>尾联</strong>：议论，借诸葛亮抒发对忠臣良将的景仰</p>
<h3 id="名句赏析" tabindex="-1"><a class="header-anchor" href="#名句赏析"><span>名句赏析</span></a></h3>
<ol>
<li>
<p><strong>&quot;早岁那知世事艰，中原北望气如山&quot;</strong>：对比手法，突出了从天真到成熟的转变过程。</p>
</li>
<li>
<p><strong>&quot;楼船夜雪瓜洲渡，铁马秋风大散关&quot;</strong>：对仗工整，生动再现了当年参加抗金斗争的壮烈场面。</p>
</li>
<li>
<p><strong>&quot;塞上长城空自许，镜中衰鬓已先斑&quot;</strong>：强烈的对比，表达了理想与现实的巨大落差。</p>
</li>
<li>
<p><strong>&quot;出师一表真名世，千载谁堪伯仲间&quot;</strong>：借诸葛亮抒发对忠臣良将的景仰，暗含对当时朝廷的批评。</p>
</li>
</ol>
<h3 id="意象分析" tabindex="-1"><a class="header-anchor" href="#意象分析"><span>意象分析</span></a></h3>
<ol>
<li><strong>中原北望</strong>：象征对故土的思念和收复失地的愿望</li>
<li><strong>楼船夜雪</strong>：象征军旅生涯的艰苦和壮烈</li>
<li><strong>铁马秋风</strong>：象征战斗的激烈和军人的豪迈</li>
<li><strong>塞上长城</strong>：象征保卫国家的壮志</li>
<li><strong>镜中衰鬓</strong>：象征时光流逝和年华老去</li>
</ol>
<h3 id="语言特色" tabindex="-1"><a class="header-anchor" href="#语言特色"><span>语言特色</span></a></h3>
<ol>
<li><strong>沉郁顿挫</strong>：语言深沉有力，节奏顿挫有致</li>
<li><strong>对仗工整</strong>：颔联、颈联对仗工整，音韵和谐</li>
<li><strong>用词精练</strong>：每个字都恰到好处，无一冗余</li>
<li><strong>感情激越</strong>：字里行间充满激情和愤慨</li>
</ol>
<h3 id="思想意义" tabindex="-1"><a class="header-anchor" href="#思想意义"><span>思想意义</span></a></h3>
<ol>
<li><strong>爱国主义</strong>：体现了深沉的爱国主义精神</li>
<li><strong>忧患意识</strong>：表现了强烈的忧患意识</li>
<li><strong>英雄主义</strong>：体现了英雄主义的情怀</li>
<li><strong>历史责任感</strong>：表达了对历史责任的深刻认识</li>
</ol>
<h3 id="艺术成就" tabindex="-1"><a class="header-anchor" href="#艺术成就"><span>艺术成就</span></a></h3>
<ol>
<li><strong>情感真挚</strong>：感情真挚深沉，感人至深</li>
<li><strong>意境深远</strong>：营造了深远的意境</li>
<li><strong>技巧娴熟</strong>：运用各种艺术技巧，达到了很高的水平</li>

<h1 id="咏史-选" tabindex="-1"><a class="header-anchor" href="#咏史-选"><span>咏史（选）</span></a></h1>
<p>作者：左思（西晋）
体裁：组诗/七言古诗</p>
<p>导言</p>
<ul>
<li>借史抒怀，身世与史鉴互见，格调沉郁。</li>
</ul>
<p>底本与版本</p>
<ul>
<li>推荐检索：中华书局《左思集》或总集所录；旁证《全晋诗》。</li>
</ul>
<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<h3 id="其一" tabindex="-1"><a class="header-anchor" href="#其一"><span>其一</span></a></h3>
<p>弱冠弄柔翰，卓荦观群书。著论准过秦，作赋拟子虚。边城苦鸣镝，羽檄飞京都。虽非甲胄士，畴昔览军书。</p>
<h3 id="其二" tabindex="-1"><a class="header-anchor" href="#其二"><span>其二</span></a></h3>
<p>郁郁涧底松，离离山上苗。以彼径寸茎，荫此百尺条。世胄蹑高位，英俊沉下僚。地势使之然，由来非一朝。</p>
<h3 id="其八" tabindex="-1"><a class="header-anchor" href="#其八"><span>其八</span></a></h3>
<p>习习笼中鸟，举翮触四隅。落落穷巷士，抱影守空庐。出门无通路，枳棘塞中涂。计策弃不收，块然守故居。</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<p><strong>弱冠：</strong> 古代男子二十岁行冠礼，称弱冠。<br>
<strong>柔翰：</strong> 毛笔。翰，笔杆。<br>
<strong>卓荦：</strong> 超群出众。<br>
<strong>准过秦：</strong> 以贾谊《过秦论》为准则。<br>
<strong>拟子虚：</strong> 模拟司马相如《子虚赋》。<br>
<strong>鸣镝：</strong> 响箭，这里指战争。镝，箭头。<br>
<strong>羽檄：</strong> 插有羽毛的紧急军书。<br>
<strong>甲胄士：</strong> 穿盔甲的武士。<br>
<strong>畴昔：</strong> 从前，往日。</p>
<p><strong>郁郁：</strong> 茂盛的样子。<br>
<strong>离离：</strong> 稀疏的样子。<br>
<strong>径寸茎：</strong> 直径一寸的茎干。<br>
<strong>荫：</strong> 遮蔽。<br>
<strong>世胄：</strong> 世家子弟。胄，后代。<br>
<strong>蹑：</strong> 踏，这里指占据。<br>
<strong>英俊：</strong> 才能出众的人。<br>
<strong>下僚：</strong> 下级官吏。<br>
<strong>地势：</strong> 地位高低的形势。</p>
<p><strong>习习：</strong> 鸟振翅的声音。<br>
<strong>举翮：</strong> 举起翅膀。翮，鸟的翅膀。<br>
<strong>触四隅：</strong> 碰到笼子的四角。隅，角落。<br>
<strong>落落：</strong> 孤独寂寞的样子。<br>
<strong>穷巷士：</strong> 住在穷街陋巷的读书人。<br>
<strong>抱影：</strong> 孤独一人。<br>
<strong>空庐：</strong> 空房子。<br>
<strong>枳棘：</strong> 枳树和棘树，多刺的灌木。<br>
<strong>中涂：</strong> 道路中间。<br>
<strong>块然：</strong> 孤独的样子。</p>
<h2 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h2>
<h3 id="其一-1" tabindex="-1"><a class="header-anchor" href="#其一-1"><span>其一</span></a></h3>
<p>二十岁时就开始舞文弄墨，超群出众地阅读各种书籍。写文章以贾谊的《过秦论》为准则，作赋文模拟司马相如的《子虚赋》。边城战事频繁响箭呼啸，军情紧急的羽檄飞向京都。虽然我不是披甲执戈的武士，但从前也曾阅读过兵书。</p>
<h3 id="其二-1" tabindex="-1"><a class="header-anchor" href="#其二-1"><span>其二</span></a></h3>
<p>山涧底下的松树郁郁葱葱，山坡上面的小苗稀稀疏疏。凭着那直径一寸的细茎，却要遮蔽这百尺高的大树。世家子弟踏上了高官显位，英才俊杰却沉沦在下层官吏中。这是地势高低造成的结果，由来已久不是一朝一夕的事。</p>
<h3 id="其八-1" tabindex="-1"><a class="header-anchor" href="#其八-1"><span>其八</span></a></h3>
<p>笼中的鸟儿振翅欲飞，举起翅膀却碰到笼子的四角。穷街陋巷中的读书人，孤独一人守着空房子。出门没有通达的道路，荆棘堵塞了道路中间。才能和计策被抛弃不被采用，孤独地守在旧居里。</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<h3 id="创作背景" tabindex="-1"><a class="header-anchor" href="#创作背景"><span>创作背景</span></a></h3>
<p>《咏史》八首是左思的代表作，创作于西晋太康年间（280-289年）。当时西晋刚刚统一全国，但门阀制度森严，寒门士子很难出头。左思出身寒微，虽有才华却不得志，因此借咏史来抒发自己的愤懑之情。</p>
<h3 id="主题思想" tabindex="-1"><a class="header-anchor" href="#主题思想"><span>主题思想</span></a></h3>
<ol>
<li><strong>怀才不遇</strong>：表达了寒门士子怀才不遇的愤慨</li>
<li><strong>门第批判</strong>：批判门阀制度对人才的压制</li>
<li><strong>理想追求</strong>：表现了对建功立业的渴望</li>
<li><strong>现实关怀</strong>：反映了魏晋时期的社会现实</li>
</ol>
<h3 id="艺术特色" tabindex="-1"><a class="header-anchor" href="#艺术特色"><span>艺术特色</span></a></h3>
<ol>
<li><strong>借古讽今</strong>：通过咏史的形式来抒发现实感慨</li>
<li><strong>比兴手法</strong>：大量运用比喻和象征手法</li>
<li><strong>对比鲜明</strong>：通过对比来突出主题</li>
<li><strong>语言精练</strong>：用词准确，表达简洁有力</li>
</ol>
<h3 id="表现手法" tabindex="-1"><a class="header-anchor" href="#表现手法"><span>表现手法</span></a></h3>
<ol>
<li><strong>象征手法</strong>：用&quot;涧底松&quot;、&quot;山上苗&quot;象征人才与庸才的对比</li>
<li><strong>比喻手法</strong>：用&quot;笼中鸟&quot;比喻怀才不遇的处境</li>
<li><strong>对比手法</strong>：世胄与英俊、高位与下僚的对比</li>
<li><strong>用典手法</strong>：引用历史典故来表达思想</li>
</ol>
<h3 id="结构分析" tabindex="-1"><a class="header-anchor" href="#结构分析"><span>结构分析</span></a></h3>
<p><strong>其一</strong>：</p>
<ol>
<li><strong>前四句</strong>：写青年时期的才华和抱负</li>
<li><strong>后四句</strong>：写时局动荡，有志报国</li>
</ol>
<p><strong>其二</strong>：</p>
<ol>
<li><strong>前四句</strong>：用松苗对比比喻人才与庸才</li>
<li><strong>后四句</strong>：直接点出门第制度的弊端</li>
</ol>
<p><strong>其八</strong>：</p>
<ol>
<li><strong>前四句</strong>：用笼鸟比喻自己的处境</li>
<li><strong>后四句</strong>：写现实中的困顿和无奈</li>
</ol>
<h3 id="名句赏析" tabindex="-1"><a class="header-anchor" href="#名句赏析"><span>名句赏析</span></a></h3>
<ol>
<li>
<p><strong>&quot;郁郁涧底松，离离山上苗&quot;</strong>：用自然景象比喻社会现象，形象生动。</p>
</li>
<li>
<p><strong>&quot;以彼径寸茎，荫此百尺条&quot;</strong>：对比强烈，讽刺意味深刻。</p>
</li>
<li>
<p><strong>&quot;世胄蹑高位，英俊沉下僚&quot;</strong>：直接点出门第制度的不合理。</p>
</li>
<li>
<p><strong>&quot;地势使之然，由来非一朝&quot;</strong>：揭示了社会问题的根源和历史性。</p>
</li>
<li>
<p><strong>&quot;习习笼中鸟，举翮触四隅&quot;</strong>：比喻怀才不遇者的困境，形象贴切。</p>
</li>
</ol>
<h3 id="文学地位" tabindex="-1"><a class="header-anchor" href="#文学地位"><span>文学地位</span></a></h3>
<p>《咏史》诗在中国文学史上具有重要地位：</p>
<ol>
<li><strong>咏史诗典范</strong>：开创了咏史诗借古讽今的传统</li>
<li><strong>现实主义精神</strong>：体现了强烈的现实主义精神</li>
<li><strong>社会批判意义</strong>：对门阀制度进行了深刻批判</li>
<li><strong>艺术成就</strong>：在比兴手法的运用上达到了很高水平</li>
<li><strong>影响深远</strong>：对后世咏史诗的创作产生了重要影响</li>
</ol>

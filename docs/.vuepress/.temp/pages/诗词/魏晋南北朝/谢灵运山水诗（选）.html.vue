<h3 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h3>
<h4 id="登池上楼-1" tabindex="-1"><a class="header-anchor" href="#登池上楼-1"><span>登池上楼</span></a></h4>
<p>潜伏的龙以幽美的姿态取悦人，飞翔的鸿雁发出远方的声音。
我惭愧自己接近云霄却如云般飘浮，栖息在河川却惭愧于深渊的沉静。
修养德行是我智慧不足的地方，退隐耕作又力量不够。
追求俸禄反而到了偏远的海边，卧病对着空旷的树林。
被褥枕头让我不知季节变化，掀开窗帘暂时观看。
侧耳倾听波浪的声音，抬头远望崎岖的山峰。
初春的景色改变了冬天的风，春天的阳光改变了冬天的阴冷。
池塘生出春草，园中柳树变换了鸣禽。
官职的束缚超出了平常的心境，远大的胸怀舒展开来。</p>
<h4 id="石壁精舍还湖中作-1" tabindex="-1"><a class="header-anchor" href="#石壁精舍还湖中作-1"><span>石壁精舍还湖中作</span></a></h4>
<p>早晚变化着气候，山水含有清澈的光辉。
清澈的光辉能使人愉悦，游览的人安然忘记归去。
走出山谷时太阳还早，上船时阳光已经微弱。
树林山谷收敛暮色，云彩霞光收起晚霞。
菱花荷花相互映照茂盛，蒲草稗草相互依靠。
拨开草木走向南边的小路，愉快地靠在东边的门扉。
思虑淡泊万物自然轻松，心意满足道理没有违背。
寄语养生的人，试着用这个道理推广。</p>
<h4 id="过始宁墅-1" tabindex="-1"><a class="header-anchor" href="#过始宁墅-1"><span>过始宁墅</span></a></h4>
<p>年轻时怀有正直的品格，追逐外物于是被推移。
违背志向好像昨天一样，二十四年到了这一年。
品德受损失去清高旷达，疲惫衰弱惭愧于坚贞。
愚拙和疾病相互依靠，还得到安静的便利。
分竹符守卫海边，张帆经过故乡的山。
山中行走尽力攀登，涉水尽力逆流而上。
岩石陡峭山岭稠密重叠，洲渚萦绕小洲连绵。
白云环抱幽深的石头，绿色的竹子取悦清澈的水波。
修建房屋面临弯曲的江水，建造楼观建在重叠的山顶。
挥手告别乡里，三年期望归来。</p>

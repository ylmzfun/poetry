<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<h3 id="登池上楼" tabindex="-1"><a class="header-anchor" href="#登池上楼"><span>登池上楼</span></a></h3>
<p>潜虬媚幽姿，飞鸿响远音。
薄霄愧云浮，栖川怍渊沉。
进德智所拙，退耕力不任。
徇禄反穷海，卧疴对空林。
衾枕昧节候，褰开暂窥临。
倾耳聆波澜，举目眺岖嵚。
初景革绪风，新阳改故阴。
池塘生春草，园柳变鸣禽。
祇役出宿心，遥襟畅远襟。</p>
<h3 id="石壁精舍还湖中作" tabindex="-1"><a class="header-anchor" href="#石壁精舍还湖中作"><span>石壁精舍还湖中作</span></a></h3>
<p>昏旦变气候，山水含清晖。
清晖能娱人，游子憺忘归。
出谷日尚早，入舟阳已微。
林壑敛暝色，云霞收夕霏。
芰荷迭映蔚，蒲稗相因依。
披拂趋南径，愉悦偃东扉。
虑澹物自轻，意惬理无违。
寄言摄生客，试用此道推。</p>
<h3 id="过始宁墅" tabindex="-1"><a class="header-anchor" href="#过始宁墅"><span>过始宁墅</span></a></h3>
<p>束发怀耿介，逐物遂推迁。
违志似如昨，二纪及兹年。
缁磷谢清旷，疲薾惭贞坚。
拙疾相倚薄，还得静者便。
剖竹守沧海，枉帆过旧山。
山行穷登顿，水涉尽洄沿。
岩峭岭稠叠，洲萦渚连绵。
白云抱幽石，绿筱媚清涟。
葺宇临回江，筑观基曾巅。
挥手告乡曲，三载期归旋。</p>

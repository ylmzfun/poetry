</ul>
<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<h3 id="其一" tabindex="-1"><a class="header-anchor" href="#其一"><span>其一</span></a></h3>
<p>少无适俗韵，性本爱丘山。误落尘网中，一去三十年。羁鸟恋旧林，池鱼思故渊。开荒南野际，守拙归园田。方宅十余亩，草屋八九间。榆柳荫后檐，桃李满前园。暧暧远人村，依依墟里烟。狗吠深巷中，鸡鸣桑树颠。户庭无尘杂，虚室有余闲。久在樊笼里，复得返自然。</p>
<h3 id="其三" tabindex="-1"><a class="header-anchor" href="#其三"><span>其三</span></a></h3>
<p>种豆南山下，草盛豆苗稀。晨兴理荒秽，带月荷锄归。道狭草木长，夕露沾我衣。衣沾不足惜，但使愿无违。</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<p><strong>适俗韵：</strong> 适应世俗的性格。韵，本性，性格。<br>
<strong>丘山：</strong> 山丘，这里指田园。<br>
<strong>尘网：</strong> 指官场，比喻污浊的现实社会。<br>
<strong>羁鸟：</strong> 被关在笼中的鸟。羁，束缚。<br>
<strong>故渊：</strong> 原来的深水，比喻故乡。<br>
<strong>南野际：</strong> 南边田野的边上。际，边。<br>
<strong>守拙：</strong> 保持愚拙的本性，不与世俗同流合污。<br>
<strong>方宅：</strong> 住宅周围的土地。方，四方。<br>
<strong>榆柳：</strong> 榆树和柳树。<br>
<strong>后檐：</strong> 房屋后面的屋檐。<br>
<strong>暧暧：</strong> 昏暗不明的样子。<br>
<strong>依依：</strong> 轻柔缭绕的样子。<br>
<strong>墟里烟：</strong> 村落里的炊烟。墟里，村落。<br>
<strong>户庭：</strong> 门第庭院。<br>
<strong>尘杂：</strong> 世俗的杂事。<br>
<strong>虚室：</strong> 空旷的屋子，这里指心境空明宁静。<br>
<strong>樊笼：</strong> 关鸟兽的笼子，比喻官场生活。<br>
<strong>返自然：</strong> 回到自然的本性。</p>
<p><strong>种豆：</strong> 种植豆类。<br>
<strong>南山：</strong> 南边的山，指庐山。<br>
<strong>草盛豆苗稀：</strong> 野草茂盛而豆苗稀少。<br>
<strong>晨兴：</strong> 早晨起来。兴，起。<br>
<strong>理荒秽：</strong> 清理荒芜的田地。理，整治。荒秽，荒芜。<br>
<strong>荷锄：</strong> 扛着锄头。荷，扛，背。<br>
<strong>道狭：</strong> 道路狭窄。<br>
<strong>夕露：</strong> 傍晚的露水。<br>
<strong>但使：</strong> 只要。<br>
<strong>愿无违：</strong> 心愿不违背，指不违背归隐的志向。</p>
<h2 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h2>
<h3 id="其一-1" tabindex="-1"><a class="header-anchor" href="#其一-1"><span>其一</span></a></h3>
<p>少年时就没有适应世俗的性格，天性本来热爱山丘田园。错误地陷落在人世的罗网中，一去就是三十年。关在笼中的鸟儿依恋旧日的树林，养在池中的鱼儿思念原来的深潭。到南边的原野里去开荒，依着愚拙的本性回到田园。住宅四周有十多亩地，茅草房子有八九间。榆树柳树遮蔽着后屋檐，桃树李树罗列在房屋前面。远处的邻村景色模糊，村落里飘荡着袅袅炊烟。深巷中传来了几声狗叫，桑树顶有雄鸡啼鸣。门庭里没有世俗的杂事，空房中有的是空闲的时间。长久地困在笼子里面，现在总算又能够返回到大自然了。</p>
<h3 id="其三-1" tabindex="-1"><a class="header-anchor" href="#其三-1"><span>其三</span></a></h3>
<p>在南山下种植豆子，地里野草茂盛豆苗稀少。清晨早起下地铲除杂草，夜幕降临披着月光才回家。山径狭窄草木丛生，夜间露水沾湿了我的衣裳。衣裳被沾湿并不可惜，只希望不违背我归隐的心意。</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<h3 id="创作背景" tabindex="-1"><a class="header-anchor" href="#创作背景"><span>创作背景</span></a></h3>
<p>《归园田居》是陶渊明辞官归隐后创作的组诗，共五首。这组诗作于义熙元年（405年），当时陶渊明刚刚辞去彭泽县令职务，回到家乡柴桑隐居。诗人以此抒发了对田园生活的热爱和对官场生活的厌倦。</p>
<h3 id="主题思想" tabindex="-1"><a class="header-anchor" href="#主题思想"><span>主题思想</span></a></h3>
<ol>
<li><strong>归隐情怀</strong>：表达了诗人厌倦官场、向往田园的归隐情怀</li>
<li><strong>自然本性</strong>：强调回归自然、保持本真的人生态度</li>
<li><strong>田园之美</strong>：赞美田园生活的宁静美好和自然和谐</li>
<li><strong>人生哲理</strong>：体现了&quot;守拙&quot;、&quot;返自然&quot;的人生哲学</li>
</ol>
<h3 id="艺术特色" tabindex="-1"><a class="header-anchor" href="#艺术特色"><span>艺术特色</span></a></h3>
<ol>
<li><strong>语言质朴</strong>：用词平易自然，不事雕琢，体现了田园诗的本色</li>
<li><strong>意境深远</strong>：通过平淡的描写营造出深远的意境</li>
<li><strong>情景交融</strong>：将个人情感与田园景色完美融合</li>
<li><strong>对比鲜明</strong>：官场的&quot;尘网&quot;与田园的&quot;自然&quot;形成鲜明对比</li>
</ol>
<h3 id="表现手法" tabindex="-1"><a class="header-anchor" href="#表现手法"><span>表现手法</span></a></h3>
<ol>
<li><strong>比喻手法</strong>：用&quot;尘网&quot;、&quot;樊笼&quot;比喻官场，用&quot;羁鸟&quot;、&quot;池鱼&quot;自比</li>
<li><strong>白描手法</strong>：用朴素的语言描绘田园生活的真实画面</li>
<li><strong>对比手法</strong>：官场生活与田园生活的对比</li>
<li><strong>象征手法</strong>：田园象征理想的人生境界</li>
</ol>
<h3 id="结构分析" tabindex="-1"><a class="header-anchor" href="#结构分析"><span>结构分析</span></a></h3>
<p><strong>其一</strong>：</p>
<ol>
<li><strong>前四句</strong>：回顾官场生活，表达厌倦之情</li>
<li><strong>中间八句</strong>：描写田园生活的美好</li>
<li><strong>后四句</strong>：抒发归隐后的愉悦心情</li>
</ol>
<p><strong>其三</strong>：</p>
<ol>
<li><strong>前四句</strong>：描写劳动生活的真实情景</li>
<li><strong>后四句</strong>：表达对田园生活的坚定信念</li>
</ol>
<h3 id="名句赏析" tabindex="-1"><a class="header-anchor" href="#名句赏析"><span>名句赏析</span></a></h3>
<ol>
<li>
<p><strong>&quot;少无适俗韵，性本爱丘山&quot;</strong>：开篇点明归隐的根本原因是天性使然。</p>
</li>
<li>
<p><strong>&quot;羁鸟恋旧林，池鱼思故渊&quot;</strong>：用比喻表达对田园生活的向往，形象生动。</p>
</li>
<li>
<p><strong>&quot;久在樊笼里，复得返自然&quot;</strong>：总结全诗主旨，表达重获自由的喜悦。</p>
</li>
<li>
<p><strong>&quot;衣沾不足惜，但使愿无违&quot;</strong>：表现了诗人对田园生活的坚定信念和淡泊心境。</p>
</li>
</ol>
<h3 id="文学地位" tabindex="-1"><a class="header-anchor" href="#文学地位"><span>文学地位</span></a></h3>
<p>《归园田居》在中国文学史上具有重要地位：</p>
<ol>
<li><strong>田园诗鼻祖</strong>：开创了中国古代田园诗的先河</li>
<li><strong>隐逸文学典范</strong>：成为隐逸文学的经典之作</li>
<li><strong>人格精神象征</strong>：体现了中国文人理想的人格精神</li>
<li><strong>艺术成就</strong>：以平淡见真情，达到了很高的艺术境界</li>
<li><strong>文化影响</strong>：对后世文人的人生选择产生了深远影响</li>
</ol>

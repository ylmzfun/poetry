<h1 id="归园田居-选" tabindex="-1"><a class="header-anchor" href="#归园田居-选"><span>归园田居（选）</span></a></h1>
<p>作者：陶渊明（东晋）
体裁：组诗/田园诗</p>
<p>导言</p>
<ul>
<li>自然与人格之归依，语言质朴冲淡，意境悠远。</li>
</ul>
<p>底本与版本</p>
<ul>

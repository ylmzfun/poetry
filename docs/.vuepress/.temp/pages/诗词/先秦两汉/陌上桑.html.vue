<h1 id="陌上桑" tabindex="-1"><a class="header-anchor" href="#陌上桑"><span>陌上桑</span></a></h1>
<p>体裁：乐府叙事诗（汉）</p>
<p>导言</p>
<ul>
<li>叙事活泼，人物塑造生动；以罗敷形象见风俗之美。</li>
</ul>
<p>底本与版本</p>
<ul>
<li>推荐检索：《乐府诗集》《古诗源》相关卷；旁证历代选本。</li>
</ul>
<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p>日出东南隅，照我秦氏楼。秦氏有好女，自名为罗敷。罗敷喜蚕桑，采桑城南隅。青丝为笼系，桂枝为笼钩。头上倭堕髻，耳中明月珠。缃绮为下裙，紫绮为上襦。行者见罗敷，下担捋髭须。少年见罗敷，脱帽著帩头。耕者忘其犁，锄者忘其锄。来归相怨怒，但坐观罗敷。</p>
<p>使君从南来，五马立踟蹰。使君遣吏往，问是谁家姝。&quot;秦氏有好女，自名为罗敷。&quot;&quot;罗敷年几何？&quot;&quot;二十尚不足，十五颇有余。&quot;使君谢罗敷：&quot;宁可共载不？&quot;罗敷前致辞：&quot;使君一何愚！使君自有妇，罗敷自有夫。&quot;</p>
<p>&quot;东方千余骑，夫婿居上头。何用识夫婿？白马从骊驹。青丝系马尾，黄金络马头。腰中鹿卢剑，可值千万余。十五府小史，二十朝大夫，三十侍中郎，四十专城居。为人洁白皙，鬑鬑颇有须。盈盈公府步，冉冉府中趋。坐中数千人，皆言夫独殊。&quot;</p>

<h1 id="孔雀东南飞" tabindex="-1"><a class="header-anchor" href="#孔雀东南飞"><span>孔雀东南飞</span></a></h1>
<p><strong>作者：</strong> 佚名<br>
<strong>朝代：</strong> 汉末<br>
<strong>体裁：</strong> 乐府长篇叙事诗<br>
<strong>出处：</strong> 《乐府诗集》</p>
<h2 id="简介" tabindex="-1"><a class="header-anchor" href="#简介"><span>简介</span></a></h2>
<p>《孔雀东南飞》是中国文学史上第一首长篇叙事诗，也是乐府诗中的杰作。全诗共357句，1785字，通过刘兰芝与焦仲卿的爱情悲剧，反映了汉末封建礼教对妇女的摧残，歌颂了青年男女对爱情的忠贞和对封建势力的反抗。</p>

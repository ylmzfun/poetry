<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p>孔雀东南飞，五里一徘徊。</p>
<p>&quot;十三能织素，十四学裁衣，十五弹箜篌，十六诵诗书。十七为君妇，心中常苦悲。君既为府吏，守节情不移。贱妾留空房，相见常日稀。鸡鸣入机织，夜夜不得息。三日断五匹，大人故嫌迟。非为织作迟，君家妇难为！妾不堪驱使，徒留无所施。便可白公姥，及时相遣归。&quot;</p>
<p>府吏得闻之，堂上启阿母：&quot;儿已薄禄相，幸复得此妇。结发同枕席，黄泉共为友。共事二三年，始尔未为久。女行无偏斜，何意致不厚？&quot;</p>
<p>阿母谓府吏：&quot;何乃太区区！此妇无礼节，举动自专由。吾意久怀忿，汝岂得自由！东家有贤女，自名秦罗敷。可怜体无比，阿母为汝求。便可速遣之，遣去慎莫留！&quot;</p>
<p>府吏长跪告：&quot;伏惟启阿母，今若遣此妇，终老不复取！&quot;</p>
<p>阿母得闻之，槌床便大怒：&quot;小子无所畏，何敢助妇语！吾已失恩义，会不相从许！&quot;</p>
<p>府吏默无声，再拜还入户。举言谓新妇，哽咽不能语：&quot;我自不驱卿，逼迫有阿母。卿但暂还家，吾今且报府。不久当归还，还必相迎取。以此下心意，慎勿违吾语。&quot;</p>
<p>新妇谓府吏：&quot;勿复重纷纭。往昔初阳岁，谢家来贵门。奉事循公姥，进止敢自专？昼夜勤作息，伶俜萦苦辛。谓言无罪过，供养卒大恩；仍更被驱遣，何言复来还！妾有绣腰襦，葳蕤自生光；红罗复斗帐，四角垂香囊；箱帘六七十，绿碧青丝绳，物物各自异，种种在其中。人贱物亦鄙，不足迎后人，留待作遗施，于今无会因。时时为安慰，久久莫相忘！&quot;</p>
<p>（中间省略部分情节）</p>
<p>两家求合葬，合葬华山傍。东西植松柏，左右种梧桐。枝枝相覆盖，叶叶相交通。中有双飞鸟，自名为鸳鸯。仰头相向鸣，夜夜达五更。行人驻足听，寡妇起彷徨。多谢后世人，戒之慎勿忘！</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<ul>
<li><strong>孔雀东南飞</strong>：以孔雀比喻刘兰芝，东南飞暗示离别。</li>
<li><strong>五里一徘徊</strong>：每飞五里就徘徊一次，表现依恋不舍。</li>
<li><strong>织素</strong>：织白绢。</li>
<li><strong>箜篌</strong>：古代弦乐器。</li>
<li><strong>府吏</strong>：县府的小官吏，指焦仲卿。</li>
<li><strong>公姥</strong>：公公婆婆。</li>
<li><strong>薄禄相</strong>：命薄，福分浅。</li>
<li><strong>结发</strong>：古代男女成婚时把头发束在一起，表示夫妻关系。</li>
<li><strong>黄泉</strong>：地下，指死后。</li>
<li><strong>区区</strong>：拘泥，小气。</li>
<li><strong>秦罗敷</strong>：美女的代称。</li>
<li><strong>槌床</strong>：用手拍床。</li>
<li><strong>哽咽</strong>：声音哽塞。</li>
<li><strong>重纷纭</strong>：再纠缠。</li>
<li><strong>初阳岁</strong>：新年。</li>
<li><strong>谢家</strong>：刘兰芝的娘家。</li>
<li><strong>伶俜</strong>：孤独的样子。</li>
<li><strong>萦苦辛</strong>：缠绕着痛苦辛劳。</li>
<li><strong>绣腰襦</strong>：绣花的短袄。</li>
<li><strong>葳蕤</strong>：草木茂盛，这里形容华美。</li>
<li><strong>斗帐</strong>：形如斗的帐子。</li>
<li><strong>遗施</strong>：赠送。</li>
<li><strong>会因</strong>：机会。</li>
</ul>
<h2 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h2>
<p>孔雀向东南方向飞去，每飞五里就徘徊一阵。</p>
<p>&quot;我十三岁就能织白绢，十四岁学会了裁衣，十五岁会弹箜篌，十六岁能诵读诗书。十七岁做了您的妻子，心中常常感到痛苦悲伤。您既然做了府吏，我守节的情意不改变。我独守空房，和您相见的日子常常很少。鸡叫的时候就开始织布，夜夜都不能休息。三天织断五匹布，婆婆还故意嫌我织得慢。不是我织布慢，而是您家的媳妇难做啊！我不能忍受这样的驱使，白白留在这里也没有什么用处。您可以去禀告婆婆，趁早把我遣送回家。&quot;</p>
<p>府吏听到这些话，在堂上禀告母亲：&quot;儿子已经命薄福浅，幸而又得到这个妻子。结发时同床共枕，死后在黄泉也要做伴侣。共同生活了两三年，开始时间还不算长。这女子的行为没有什么不正当，为什么对她不厚道呢？&quot;</p>
<p>母亲对府吏说：&quot;你怎么这样糊涂！这个女人不懂礼节，行动自作主张。我心里早就怀着愤怒，你哪里能够自作主张！东邻有个贤惠的女子，自己叫秦罗敷，身体美得无与伦比，母亲为你去求婚。你可以快点休掉兰芝，休了以后千万不要挽留！&quot;</p>
<p>府吏长跪禀告：&quot;恭敬地禀告母亲，如果现在休掉这个妻子，我一辈子也不再娶妻了！&quot;</p>
<p>母亲听了这话，用手拍着床大发脾气：&quot;小子无所畏惧，怎么敢帮助妇人说话！我对她已经没有恩情了，决不会答应你的要求！&quot;</p>
<p>府吏沉默不说话，再拜了两拜就回到自己房里。开口对新妇说话，哽咽得说不成声：&quot;我自己不赶你走，但是受到母亲的逼迫。你只是暂时回娘家，我现在暂且回府里。不久我一定回来，回来后一定迎接你。因此你要顺从我的心意，千万不要违背我的话。&quot;</p>
<p>新妇对府吏说：&quot;不要再增加纷扰了。记得那新年时，我辞别娘家来到你家。侍奉公婆遵循规矩，一举一动哪里敢自作主张？白天黑夜勤恳地干活，孤孤单单地被痛苦辛劳缠绕。自以为没有过错，供养婆婆终于报答了大恩；但是仍然被驱遣，还有什么话可说再回来！我有绣花的齐腰短袄，上面光彩夺目；有红罗做的双层斗帐，四角挂着香囊；有箱子六七十个，都用碧绿的丝绳捆着。样样东西各不相同，种种器物都在其中。人卑贱东西也卑贱，不配给后来的人用，留着作为我赠送的纪念吧，从今以后再没有见面的机会了。时时刻刻作为安慰吧，希望你永远不要忘记我！&quot;</p>
<p>（结尾）两家要求合葬，把他们合葬在华山旁。东西两侧种上松柏，左右两边种上梧桐。（松柏梧桐的）枝枝相互覆盖，叶叶相互交通。中间有一对飞鸟，它们自己叫做鸳鸯。抬起头来相对鸣叫，夜夜啼叫直到五更。行路的人停下脚步听，寡妇听了从床上起来徘徊不定。多多劝告后世的人们，把这个教训记住千万不要忘！</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<p><strong>创作背景：</strong></p>
<p>《孔雀东南飞》产生于汉末建安年间，当时社会动荡，封建礼教森严。诗中反映的是东汉末年庐江郡（今安徽境内）的一个真实故事，通过这个悲剧揭露了封建家长制和封建礼教对青年男女的摧残。</p>
<p><strong>主题思想：</strong></p>
<ol>
<li>
<p><strong>反抗封建礼教</strong>：通过刘兰芝和焦仲卿的悲剧，揭露封建礼教的残酷。</p>
</li>
<li>
<p><strong>歌颂忠贞爱情</strong>：赞美了青年男女对爱情的忠贞不渝。</p>
</li>
<li>
<p><strong>同情妇女命运</strong>：深刻反映了封建社会妇女的悲惨处境。</p>
</li>
<li>
<p><strong>批判家长专制</strong>：抨击了封建家长制的专横和残暴。</p>
</li>
</ol>
<p><strong>艺术特色：</strong></p>
<ol>
<li>
<p><strong>叙事完整</strong>：情节完整，结构严谨，是中国第一首长篇叙事诗。</p>
</li>
<li>
<p><strong>人物鲜明</strong>：刘兰芝、焦仲卿、焦母等人物形象鲜明生动。</p>
</li>
<li>
<p><strong>语言生动</strong>：语言朴素自然，富有生活气息。</p>
</li>
<li>
<p><strong>对话精彩</strong>：大量运用对话，人物性格跃然纸上。</p>
</li>
</ol>
<p><strong>表现手法：</strong></p>
<ol>
<li>
<p><strong>对比手法</strong>：通过对比突出人物性格和矛盾冲突。</p>
</li>
<li>
<p><strong>细节描写</strong>：通过细节描写展现人物内心世界。</p>
</li>
<li>
<p><strong>象征手法</strong>：以孔雀东南飞象征离别，以鸳鸯象征忠贞。</p>
</li>
<li>
<p><strong>铺陈手法</strong>：详细铺陈，增强了叙事的生动性。</p>
</li>
</ol>
<p><strong>结构分析：</strong></p>
<ol>
<li>
<p><strong>开端</strong>：孔雀东南飞，奠定悲剧基调。</p>
</li>
<li>
<p><strong>发展</strong>：兰芝被遣，矛盾激化。</p>
</li>
<li>
<p><strong>高潮</strong>：双方殉情，悲剧达到顶点。</p>
</li>
<li>
<p><strong>结局</strong>：合葬化鸟，寄托美好愿望。</p>
</li>
</ol>
<p><strong>人物形象：</strong></p>
<ol>
<li>
<p><strong>刘兰芝</strong>：美丽、聪慧、坚强，是封建社会理想女性的典型。</p>
</li>
<li>
<p><strong>焦仲卿</strong>：忠诚、软弱、矛盾，体现了封建社会男子的复杂性格。</p>
</li>
<li>
<p><strong>焦母</strong>：专横、冷酷，是封建家长制的代表。</p>
</li>
</ol>
<p><strong>文学地位：</strong></p>
<ol>
<li>
<p><strong>叙事诗典范</strong>：中国古代叙事诗的典范之作。</p>
</li>
<li>
<p><strong>现实主义杰作</strong>：汉代现实主义文学的杰出代表。</p>
</li>
<li>
<p><strong>影响深远</strong>：对后世文学产生了深远影响。</p>
</li>
<li>
<p><strong>民间文学瑰宝</strong>：体现了民间文学的艺术成就。</p>
</li>
</ol>

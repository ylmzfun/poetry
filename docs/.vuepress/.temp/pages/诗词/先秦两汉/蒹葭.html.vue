<h1 id="蒹葭" tabindex="-1"><a class="header-anchor" href="#蒹葭"><span>蒹葭</span></a></h1>
<p><strong>作者：</strong> 佚名<br>
<strong>体裁：</strong> 四言诗<br>
<strong>出处：</strong> 《诗经·秦风》<br>
<strong>创作时间：</strong> 春秋时期</p>
<h2 id="简介" tabindex="-1"><a class="header-anchor" href="#简介"><span>简介</span></a></h2>
<p>《蒹葭》是《诗经·秦风》中的一首著名诗篇，是中国古代文学史上最优美的抒情诗之一。全诗以秋水伊人为主题，通过对蒹葭苍苍、白露为霜等秋景的描绘，表达了对心中理想人物的执着追求和可望而不可即的惆怅之情。诗歌运用了重章叠句的艺术手法，营造出朦胧优美的意境，被誉为中国古典诗歌中的经典之作。</p>

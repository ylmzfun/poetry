<h1 id="九歌-选" tabindex="-1"><a class="header-anchor" href="#九歌-选"><span>九歌（选）</span></a></h1>
<p>作者：屈原等（战国）
体裁：楚辞·祭祀歌辞</p>
<p>导言</p>
<ul>
<li>祭祀礼乐与抒情并行，巫风神思，意象瑰丽。</li>
</ul>
<p>底本与版本</p>
<ul>

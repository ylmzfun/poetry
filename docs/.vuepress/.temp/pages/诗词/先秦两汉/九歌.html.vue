<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<h3 id="东皇太一" tabindex="-1"><a class="header-anchor" href="#东皇太一"><span>东皇太一</span></a></h3>
<p>吉日兮辰良，穆将愉兮上皇。抚长剑兮玉珥，璆锵鸣兮琳琅。瑶席兮玉瑱，盍将把兮琼芳。蕙肴蒸兮兰藉，奠桂酒兮椒浆。扬枹兮拊鼓，疏缓节兮安歌。陈竽瑟兮浩倡，灵偃蹇兮姣服。芳菲菲兮满堂，合百草兮实庭。建雄虹兮采旄，五音纷兮繁会。君欣欣兮乐康！</p>
<h3 id="湘君" tabindex="-1"><a class="header-anchor" href="#湘君"><span>湘君</span></a></h3>
<p>君不行兮夷犹，蹇谁留兮中洲？美要眇兮宜修，沛吾乘兮桂舟。令沅湘兮无波，使江水兮安流。望夫君兮未来，吹参差兮谁思？驾飞龙兮北征，邅吾道兮洞庭。薜荔柏兮蕙绸，荪桡兮兰旌。望涔阳兮极浦，横大江兮扬灵。扬灵兮未极，女婵娟兮为余太息。横流涕兮潺湲，隐思君兮陫侧。桂棹兮兰枻，斵冰兮积雪。采薜荔兮水中，搴芙蓉兮木末。心不同兮媒劳，恩不甚兮轻绝。石濑兮浅浅，飞龙兮翩翩。交不忠兮怨长，期不信兮告余以不闲。朝骋骛兮江皋，夕弭节兮北渚。鸟何萃兮苹中，罾何为兮木上？沅有芷兮澧有兰，思公子兮未敢言。荒忽兮远望，观流水兮潺湲。麋何食兮庭中，蛟何为兮水裔？朝驰余马兮江皋，夕济兮西澨。</p>
<h3 id="山鬼" tabindex="-1"><a class="header-anchor" href="#山鬼"><span>山鬼</span></a></h3>
<p>若有人兮山之阿，被薜荔兮带女萝。既含睇兮又宜笑，子慕予兮善窈窕。乘赤豹兮从文狸，辛夷车兮结桂旗。被石兰兮带杜衡，折芳馨兮遗所思。余处幽篁兮终不见天，道险难兮独后来。表独立兮山之上，云容容兮而在下。杳冥冥兮羌昼晦，东风飘兮神灵雨。留灵修兮憺忘归，岁既晏兮孰华予？采三秀兮于山间，石磊磊兮葛蔓蔓。怨公子兮怅忘归，君思我兮不得闲。山中人兮芳杜若，饮石泉兮荫松柏。君思我兮然疑作，雷填填兮雨冥冥，猿啾啾兮狖夜鸣。风飒飒兮木萧萧，思公子兮徒离忧。</p>

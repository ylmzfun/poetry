<h1 id="古风-选" tabindex="-1"><a class="header-anchor" href="#古风-选"><span>古风（选）</span></a></h1>
<p>作者：李白（唐）
体裁：古风（选录若干篇）</p>
<p>导言</p>
<ul>
<li>古风以汉魏乐府体为宗，气势纵横，意象瑰丽。</li>
</ul>
<p>版本与检索</p>
<ul>
<li>推荐检索：中华书局《李白集》点校本“古风”诸篇；旁证《全唐诗》。</li>
</ul>
<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<h3 id="古风·其十五" tabindex="-1"><a class="header-anchor" href="#古风·其十五"><span>古风·其十五</span></a></h3>
<p>燕昭延郭隗，遂筑黄金台。
剧辛方赵至，邹衍复齐来。
奈何青云士，弃我如尘埃。
珠玉买歌笑，糟糠养贤才。
方知黄鹤举，千里独徘徊。</p>
<h3 id="古风·其三十九" tabindex="-1"><a class="header-anchor" href="#古风·其三十九"><span>古风·其三十九</span></a></h3>
<p>登高望四海，天地何漫漫！
霜被群物秋，风飘大荒寒。
荣华东流水，万事皆波澜。
白日掩徂辉，浮云无定端。
梧桐巢燕雀，枳棘栖鸳鸾。
且复归去来，剑歌行路难。</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<h3 id="古风·其十五注释" tabindex="-1"><a class="header-anchor" href="#古风·其十五注释"><span>古风·其十五注释</span></a></h3>
<p><strong>燕昭延郭隗</strong>：燕昭王礼遇郭隗。燕昭王，战国时燕国君主，为报齐国破燕之仇，广招贤才。郭隗，燕国人，向燕昭王献策招贤。</p>
<p><strong>黄金台</strong>：燕昭王为招贤而筑的高台，置黄金其上以示诚意。</p>
<p><strong>剧辛</strong>：战国时著名游士，原在赵国，后投奔燕国。</p>
<p><strong>邹衍</strong>：战国时齐国著名阴阳家，后投奔燕国。</p>
<p><strong>青云士</strong>：指志向高远的贤才。</p>
<p><strong>糟糠养贤才</strong>：用粗劣的食物供养贤才，比喻不重视人才。</p>
<p><strong>黄鹤举</strong>：典出《列仙传》，比喻贤才远去。</p>
<h3 id="古风·其三十九注释" tabindex="-1"><a class="header-anchor" href="#古风·其三十九注释"><span>古风·其三十九注释</span></a></h3>
<p><strong>四海</strong>：指天下。</p>
<p><strong>漫漫</strong>：无涯无际的样子。</p>
<p><strong>霜被群物秋</strong>：万物被霜覆盖而呈现秋色。被，覆盖。</p>
<p><strong>大荒</strong>：广阔的荒野。</p>
<p><strong>徂辉</strong>：落日的光辉。徂，往，去。</p>
<p><strong>梧桐巢燕雀，枳棘栖鸳鸾</strong>：比喻贤愚颠倒，小人得志而贤者受屈。梧桐本是凤凰栖息之处，现在却被燕雀占据；枳棘是有刺的灌木，鸳鸾这样的美鸟却栖息其上。</p>
<p><strong>归去来</strong>：用陶渊明《归去来兮辞》典故，表示归隐之意。</p>
<p><strong>剑歌行路难</strong>：用冯谖弹剑而歌的典故，表达怀才不遇的愤慨。</p>
<h3 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h3>
<p><strong>古风·其十五译文</strong>：
燕昭王礼遇郭隗，于是修筑了黄金台。剧辛从赵国前来，邹衍也从齐国而至。为什么那些志向高远的贤士，却把我抛弃如尘埃？他们用珠玉去买歌舞欢笑，却用糟糠来供养贤才。这才知道贤才如黄鹤远举，独自在千里之外徘徊。</p>
<p><strong>古风·其三十九译文</strong>：
我登上高处望向四海，天地是何等的广阔无边！严霜覆盖着万物呈现秋色，寒风吹过广阔的荒野。荣华富贵如东流之水一去不返，人间万事都如波澜起伏变化无常。白日掩藏了西下的光辉，浮云飘忽不定没有定所。如今燕雀竟在梧桐上筑巢，鸳鸯凤凰却栖息在荆棘之中。还是回去吧，学陶渊明归隐田园，我也要弹剑高歌感叹行路之难。</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<h3 id="主题思想" tabindex="-1"><a class="header-anchor" href="#主题思想"><span>主题思想</span></a></h3>
<p>《古风》五十九首是李白的重要诗歌组合，以&quot;世道之治乱&quot;为总纲 <mcreference link="https://baike.baidu.com/item/古风五十九首/4650696" index="2">2</mcreference>。这两首诗都表达了诗人怀才不遇的愤慨和对现实政治的不满。</p>
<p><strong>其十五</strong>主要感慨怀才不遇，通过燕昭王求贤的历史典故，对比当时朝廷不重视人才的现实，表达了诗人被冷落的愤懑之情 <mcreference link="https://www.gushiwen.cn/gushiwen_ae6386f9db.aspx" index="1">1</mcreference>。</p>
<p><strong>其三十九</strong>则通过登高远眺的秋景，抒发了诗人对人生无常、世态炎凉的感慨，以及决心归隐的愤懑之情。</p>
<h3 id="艺术特色" tabindex="-1"><a class="header-anchor" href="#艺术特色"><span>艺术特色</span></a></h3>
<ol>
<li>
<p><strong>用典精妙</strong>：两首诗都大量运用历史典故，如燕昭王筑台求贤、陶渊明归隐、冯谖弹剑等，增强了诗歌的文化内涵和表现力。</p>
</li>
<li>
<p><strong>对比鲜明</strong>：其十五中&quot;珠玉买歌笑，糟糠养贤才&quot;形成强烈对比；其三十九中&quot;梧桐巢燕雀，枳棘栖鸳鸾&quot;的对比更是精彩，形象地表现了贤愚颠倒的社会现实。</p>
</li>
<li>
<p><strong>情景交融</strong>：其三十九开篇的秋景描写，既是自然景象，又是社会现实的象征，情景交融，意境深远。</p>
</li>
<li>
<p><strong>语言风格</strong>：承继《诗经》风雅传统，语言古朴典雅，又具有李白特有的豪放气质 <mcreference link="https://baike.baidu.com/item/古风五十九首/4650696" index="2">2</mcreference>。</p>
</li>
</ol>
<h3 id="创作背景" tabindex="-1"><a class="header-anchor" href="#创作背景"><span>创作背景</span></a></h3>
<p>这两首诗创作于李白不同时期。其十五反映了诗人对当时朝廷用人制度的不满；其三十九约作于天宝三年（744年）秋，正值李白离开长安之时，表达了他对仕途失意的愤懑和归隐的决心 <mcreference link="https://m.gushiwen.cn/shiwenv_753174f781ae.aspx" index="4">4</mcreference>。</p>

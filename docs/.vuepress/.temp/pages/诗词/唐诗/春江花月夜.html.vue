<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p>春江潮水连海平，海上明月共潮生。<br>
滟滟随波千万里，何处春江无月明！<br>
江流宛转绕芳甸，月照花林皆似霰。<br>
空里流霜不觉飞，汀上白沙看不见。<br>
江天一色无纤尘，皎皎空中孤月轮。<br>
江畔何人初见月？江月何年初照人？<br>
人生代代无穷已，江月年年望相似。<br>
不知江月待何人，但见长江送流水。<br>
白云一片去悠悠，青枫浦上不胜愁。<br>
谁家今夜扁舟子？何处相思明月楼？<br>
可怜楼上月裴回，应照离人妆镜台。<br>
玉户帘中卷不去，捣衣砧上拂还来。<br>
此时相望不相闻，愿逐月华流照君。<br>
鸿雁长飞光不度，鱼龙潜跃水成文。<br>
昨夜闲潭梦落花，可怜春半不还家。<br>
江水流春去欲尽，江潭落月复西斜。<br>
斜月沉沉藏海雾，碣石潇湘无限路。<br>
不知乘月几人归，落月摇情满江树。</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<h3 id="注释-1" tabindex="-1"><a class="header-anchor" href="#注释-1"><span>注释</span></a></h3>
<ul>
<li><strong>滟滟</strong>：波光荡漾的样子。</li>
<li><strong>芳甸</strong>：芳草丰茂的原野。甸，郊外之地。</li>
<li><strong>霰</strong>：天空中降落的白色不透明的小冰粒。此处形容月光下春花晶莹洁白。</li>
<li><strong>流霜</strong>：飞霜，古人以为霜和雪一样，是从空中落下来的，所以叫流霜。此处形容月光皎洁，月色朦胧、流荡，所以不觉得有霜霰飞扬。</li>
<li><strong>汀</strong>：水边平地，小洲。</li>
<li><strong>纤尘</strong>：微细的灰尘。</li>
<li><strong>月轮</strong>：指月亮，因为月圆时像车轮，所以称为月轮。</li>
<li><strong>穷已</strong>：穷尽。</li>
<li><strong>望相似</strong>：一作&quot;只相似&quot;。</li>
<li><strong>但见</strong>：只见、仅见。</li>
<li><strong>悠悠</strong>：渺茫、深远。</li>
<li><strong>青枫浦</strong>：地名，今湖南浏阳县境内有青枫浦。这里泛指游子所在的地方，暗用《楚辞·招魂》&quot;湛湛江水兮上有枫，目极千里兮伤春心&quot;句意，隐含离别之意。</li>
<li><strong>扁舟子</strong>：飘荡江湖的游子。扁舟，小舟。</li>
<li><strong>明月楼</strong>：月夜下的闺楼。这里指闺中思妇。</li>
<li><strong>裴回</strong>：同&quot;徘徊&quot;，指月光偏照闺楼，徘徊不去，令人不胜其相思之苦。</li>
<li><strong>离人</strong>：此处指思妇。</li>
<li><strong>妆镜台</strong>：梳妆台。</li>
<li><strong>玉户</strong>：形容楼阁华丽，以玉石镶嵌。</li>
<li><strong>捣衣砧</strong>：捣衣石，捶布石。</li>
<li><strong>相闻</strong>：互通音信。</li>
<li><strong>逐</strong>：追随。<strong>月华</strong>：月光。</li>
<li><strong>文</strong>：同&quot;纹&quot;。</li>
<li><strong>闲潭</strong>：幽静的水潭。</li>
<li><strong>碣石</strong>：山名，在渤海边上。<strong>潇湘</strong>：湘江与潇水，在今湖南。这里两个地名一南一北，暗指路途遥远，相聚无望。</li>
<li><strong>无限路</strong>：极言离人相距之远。</li>
<li><strong>乘月</strong>：趁着月光。</li>
<li><strong>摇情</strong>：激荡情思，犹言牵情。</li>
</ul>
<h3 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h3>
<p>春天的江潮水势浩荡，与大海连成一片，一轮明月从海上升起，好像与潮水一起涌出来。月光照耀着春江，随着波浪闪耀千万里，所有地方的春江都有明亮的月光。江水曲曲折折地绕着花草丛生的原野流淌，月光照射着开遍鲜花的树林好像细密的雪珠在闪烁。月色如霜，所以霜飞无从觉察；洲上的白沙和月色融合在一起，看不分明。江水、天空成一色，没有一点微小灰尘，明亮的天空中只有一轮孤月高悬空中。江边上什么人最初看见月亮，江上的月亮哪一年最初照耀着人？人生一代代地无穷无尽，只有江上的月亮一年年地总是相像。不知江上的月亮等待着什么人，只见长江不断地一直运输着流水。游子像一片白云缓缓地离去，只剩下思妇站在离别的青枫浦不胜忧愁。哪家的游子今晚坐着小船在漂流？什么地方有人在明月照耀的楼上相思？可怜楼上不停移动的月光，应该照耀着离人的梳妆台。月光照进思妇的门帘，卷不走；照在她的捣衣砧上，拂不掉。这时互相望着月亮可是互相听不到声音，我希望随着月光流去照耀着您。鸿雁不停地飞翔，而不能飞出无边的月光；月照江面，鱼龙在水中跳跃，激起阵阵波纹。昨天夜里梦见花落闲潭，可惜的是春天过了一半自己还不能回家。江水带着春光将要流尽，水潭上的月亮又要西落。斜月慢慢下沉，藏在海雾里，碣石与潇湘的离人距离无限遥远。不知有几人能趁着月光回家，唯有那西落的月亮摇荡着离情，洒满了江边的树林。</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<p>这首诗是中国古典诗歌的巅峰之作，体现了唐代诗歌的最高艺术成就。</p>
<p><strong>艺术特色：</strong></p>
<ol>
<li>
<p><strong>意象丰富</strong>：春、江、花、月、夜五个核心意象贯穿全诗，构成了一个完整的艺术世界。</p>
</li>
<li>
<p><strong>结构严谨</strong>：全诗36句，可分为三个层次：写景（前8句）、哲理思考（中间8句）、抒情（后20句），层次分明，浑然一体。</p>
</li>
<li>
<p><strong>音韵和谐</strong>：全诗押韵规整，音调优美，读来朗朗上口，具有很强的音乐性。</p>
</li>
<li>
<p><strong>情景交融</strong>：景物描写与情感抒发完美结合，达到了&quot;情景交融&quot;的最高境界。</p>
</li>
</ol>
<p><strong>表现手法：</strong></p>
<ol>
<li>
<p><strong>象征手法</strong>：月亮象征着永恒，江水象征着时间的流逝，形成了永恒与变化的对比。</p>
</li>
<li>
<p><strong>对比手法</strong>：个体生命的短暂与宇宙的永恒形成对比，突出了人生的感慨。</p>
</li>
<li>
<p><strong>设问手法</strong>：连续的设问增强了诗歌的哲理性和感染力。</p>
</li>
<li>
<p><strong>虚实结合</strong>：既有具体的景物描写，又有抽象的哲理思考。</p>
</li>
</ol>
<p><strong>思想内容：</strong></p>
<ol>
<li>
<p><strong>宇宙意识</strong>：诗人通过对月亮和江水的描写，表达了对宇宙永恒的思考。</p>
</li>
<li>
<p><strong>人生感慨</strong>：面对永恒的自然，感慨人生的短暂和渺小。</p>
</li>
<li>
<p><strong>离别之情</strong>：通过游子思妇的形象，表达了深沉的离别之情。</p>
</li>
<li>
<p><strong>哲理思辨</strong>：将个人情感上升到哲理高度，体现了诗人深邃的思想。</p>
</li>
</ol>
<p><strong>文学地位：</strong></p>
<p>《春江花月夜》在中国文学史上具有极其重要的地位：</p>
<ol>
<li>
<p><strong>&quot;孤篇压全唐&quot;</strong>：这是对此诗最高的评价，说明其在唐诗中的地位。</p>
</li>
<li>
<p><strong>意境开创</strong>：开创了中国诗歌&quot;情景理&quot;三者完美结合的先河。</p>
</li>
<li>
<p><strong>影响深远</strong>：对后世诗歌创作产生了深远影响，成为咏月诗的典范。</p>
</li>
<li>
<p><strong>艺术典范</strong>：在意象运用、结构安排、音韵和谐等方面都达到了极高的艺术水准。</p>
</li>
</ol>

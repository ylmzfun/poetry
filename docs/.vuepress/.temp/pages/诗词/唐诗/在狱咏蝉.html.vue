<h1 id="在狱咏蝉" tabindex="-1"><a class="header-anchor" href="#在狱咏蝉"><span>在狱咏蝉</span></a></h1>
<p>作者：骆宾王（唐）
体裁：五言古诗</p>
<p>导言</p>
<ul>
<li>借蝉寓志，清峻悲凉，言外见骨。</li>
</ul>
<p>版本与检索</p>
<ul>
<li>推荐检索：中华书局《骆宾王集》点校本；旁证《全唐诗》。</li>
</ul>
<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p>西陆蝉声唱，南冠客思深。
那堪玄鬓影，来对白头吟。
露重飞难进，风多响易沉。
无人信高洁，谁为表予心？</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<h3 id="注释-1" tabindex="-1"><a class="header-anchor" href="#注释-1"><span>注释</span></a></h3>
<ul>
<li><strong>西陆</strong>：指秋天。《隋书·天文志》：&quot;日循黄道东行一日一夜行一度，三百六十五日有奇而周天。行东陆谓之春，行南陆谓之夏，行西陆谓之秋，行北陆谓之冬。&quot;</li>
<li><strong>南冠</strong>：楚冠，这里是囚徒的意思。用《左传·成公九年》，楚钟仪戴着南冠被囚于晋国军府事。</li>
<li><strong>那堪</strong>：怎能忍受。一作&quot;不堪&quot;。</li>
<li><strong>玄鬓</strong>：指蝉的黑色翅膀，这里比喻自己正当盛年。</li>
<li><strong>白头吟</strong>：乐府曲名。《乐府诗集》解题说是鲍照、张正见、虞世南诸作，皆自伤清直却遭诬谤。</li>
<li><strong>露重</strong>：秋露浓重。</li>
<li><strong>飞难进</strong>：是说蝉难以高飞。</li>
<li><strong>响</strong>：指蝉声。</li>
<li><strong>沉</strong>：沉没，掩盖。</li>
<li><strong>高洁</strong>：清高洁白。古人认为蝉栖高饮露，是高洁之物。作者因以自喻。</li>
<li><strong>予心</strong>：我的心。</li>
</ul>
<h3 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h3>
<p>深秋季节西墙外寒蝉不停地鸣唱，蝉声把我这囚徒的愁绪带到远方。怎堪忍受正当玄鬓盛年的好时光，独自吟诵白头吟这么哀怨的诗行。露重翅薄欲飞不能世态多么炎凉，风多风大声响易沉难保自身芬芳。无人知道我像秋蝉般的清廉高洁，有谁能为我表白冰晶玉洁的心肠？</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<h3 id="创作背景" tabindex="-1"><a class="header-anchor" href="#创作背景"><span>创作背景</span></a></h3>
<p>这首诗作于唐高宗仪凤三年（678年）。当年，屈居下僚十多年而刚升为侍御史的骆宾王因上疏论事触忤武则天，遭诬，以贪赃罪名下狱。此诗是骆宾王身陷囹圄之作。<mcreference link="https://baike.baidu.com/item/在狱咏蝉/9398893" index="1">1</mcreference></p>
<h3 id="主题思想" tabindex="-1"><a class="header-anchor" href="#主题思想"><span>主题思想</span></a></h3>
<p>此诗歌咏蝉的高洁品行，以蝉比兴，以蝉寓己，寓情于物，寄托遥深，蝉人浑然一体，抒发了诗人品行高洁却&quot;遭时徽纆&quot;的哀怨悲伤之情，表达了辨明无辜、昭雪沉冤的愿望。<mcreference link="https://m.gushiwen.cn/shiwenv_cecffc2a400d.aspx" index="3">3</mcreference></p>
<h3 id="艺术特色" tabindex="-1"><a class="header-anchor" href="#艺术特色"><span>艺术特色</span></a></h3>
<ol>
<li><strong>托物言志</strong>：全诗以蝉为喻，将蝉与人浑然一体，达到物我一体的境界。</li>
<li><strong>对比手法</strong>：用蝉的&quot;玄鬓&quot;对比自己的&quot;白头吟&quot;，突出时光流逝和遭遇不公的感慨。</li>
<li><strong>双关语意</strong>：表面咏蝉，实际抒发自己的处境和心境，语意双关。</li>
<li><strong>用典自然</strong>：运用&quot;南冠&quot;、&quot;白头吟&quot;等典故，贴切自然，不显雕琢。<mcreference link="https://www.pinshiwen.com/shiji/luobinwang/20200731281063.html" index="2">2</mcreference></li>
</ol>
<h3 id="名句赏析" tabindex="-1"><a class="header-anchor" href="#名句赏析"><span>名句赏析</span></a></h3>
<ul>
<li><strong>&quot;露重飞难进，风多响易沉&quot;</strong>：看似说蝉，却也是托物言己，把诗人多年来的坎坷经历全然呈现。&quot;露重&quot;、&quot;风多&quot;比喻周遭事物的不尽如人意；&quot;飞难进&quot;描写诗人难以在官场有所作为；&quot;响易沉&quot;暗喻自己的观点看法受到打压。</li>
<li><strong>&quot;无人信高洁，谁为表予心&quot;</strong>：以设问点明主旨，自己虽拥有蝉的高洁品质，但却含冤入狱，表达了对昭雪沉冤的渴望。</li>
</ul>

<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p><strong>琵琶行</strong>
<em>（并序）</em></p>
<p>元和十年，予左迁九江郡司马。明年秋，送客湓浦口，闻舟中夜弹琵琶者，听其音，铮铮然有京都声。问其人，本长安倡女，尝学琵琶于穆、曹二善才，年长色衰，委身为贾人妇。遂命酒，使快弹数曲。曲罢悯然，自叙少小时欢乐事，今漂沦憔悴，转徙于江湖间。予出官二年，恬然自安，感斯人言，是夕始觉有迁谪之恨。因为长句，歌以赠之，凡六百一十六言，命曰《琵琶行》。</p>
<p>浔阳江头夜送客，枫叶荻花秋瑟瑟。
主人下马客在船，举酒欲饮无管弦。
醉不成欢惨将别，别时茫茫江浸月。
忽闻水上琵琶声，主人忘归客不发。
寻声暗问弹者谁？琵琶声停欲语迟。
移船相近邀相见，添酒回灯重开宴。
千呼万唤始出来，犹抱琵琶半遮面。
转轴拨弦三两声，未成曲调先有情。
弦弦掩抑声声思，似诉平生不得志。
低眉信手续续弹，说尽心中无限事。
轻拢慢捻抹复挑，初为《霓裳》后《六幺》。
大弦嘈嘈如急雨，小弦切切如私语。
嘈嘈切切错杂弹，大珠小珠落玉盘。
间关莺语花底滑，幽咽泉流冰下难。
冰泉冷涩弦凝绝，凝绝不通声暂歇。
别有幽愁暗恨生，此时无声胜有声。
银瓶乍破水浆迸，铁骑突出刀枪鸣。
曲终收拨当心画，四弦一声如裂帛。
东船西舫悄无言，唯见江心秋月白。
沉吟放拨插弦中，整顿衣裳起敛容。
自言本是京城女，家在虾蟆陵下住。
十三学得琵琶成，名属教坊第一部。
曲罢曾教善才服，妆成每被秋娘妒。
五陵年少争缠头，一曲红绡不知数。
钿头银篦击节碎，血色罗裙翻酒污。
今年欢笑复明年，秋月春风等闲度。
弟走从军阿姨死，暮去朝来颜色故。
门前冷落鞍马稀，老大嫁作商人妇。
商人重利轻别离，前月浮梁买茶去。
去来江口守空船，绕船月明江水寒。
夜深忽梦少年事，梦啼妆泪红阑干。
我闻琵琶已叹息，又闻此语重唧唧。
同是天涯沦落人，相逢何必曾相识！
我从去年辞帝京，谪居卧病浔阳城。
浔阳地僻无音乐，终岁不闻丝竹声。
住近湓江地低湿，黄芦苦竹绕宅生。
其间旦暮闻何物？杜鹃啼血猿哀鸣。
春江花朝秋月夜，往往取酒还独倾。
岂无山歌与村笛？呕哑嘲哳难为听。
今夜闻君琵琶语，如听仙乐耳暂明。
莫辞更坐弹一曲，为君翻作《琵琶行》。
感我此言良久立，却坐促弦弦转急。
凄凄不似向前声，满座重闻皆掩泣。
座中泣下谁最多？江州司马青衫湿。</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<ul>
<li><strong>元和十年</strong>：公元815年。白居易因上书言事被贬为江州司马。</li>
<li><strong>左迁</strong>：降职，贬官。</li>
<li><strong>湓浦口</strong>：湓水入长江的渡口，在今江西九江。</li>
<li><strong>倡女</strong>：歌女，艺妓。</li>
<li><strong>穆、曹二善才</strong>：当时著名的琵琶演奏家。</li>
<li><strong>贾人</strong>：商人。</li>
<li><strong>枫叶荻花</strong>：秋天的典型景物。</li>
<li><strong>管弦</strong>：音乐。</li>
<li><strong>千呼万唤始出来</strong>：形容琵琶女的羞涩。</li>
<li><strong>轻拢慢捻抹复挑</strong>：琵琶的演奏技法。</li>
<li><strong>霓裳</strong>：《霓裳羽衣曲》，唐代著名乐曲。</li>
<li><strong>六幺</strong>：《六幺令》，唐代流行乐曲。</li>
<li><strong>大珠小珠落玉盘</strong>：形容琵琶声如珠玉相击。</li>
<li><strong>间关莺语</strong>：形容琵琶声如黄莺啼鸣。</li>
<li><strong>虾蟆陵</strong>：长安城内地名。</li>
<li><strong>教坊</strong>：唐代管理音乐的官署。</li>
<li><strong>善才</strong>：对琵琶演奏家的尊称。</li>
<li><strong>秋娘</strong>：当时对美女的通称。</li>
<li><strong>五陵年少</strong>：指长安富贵子弟。</li>
<li><strong>缠头</strong>：赏给歌女的财物。</li>
<li><strong>钿头银篦</strong>：妇女的装饰品。</li>
<li><strong>浮梁</strong>：今江西景德镇，当时茶叶集散地。</li>
<li><strong>红阑干</strong>：泪水和胭脂混合的样子。</li>
<li><strong>丝竹</strong>：音乐的代称。</li>
<li><strong>杜鹃啼血</strong>：传说杜鹃啼声如血。</li>
<li><strong>呕哑嘲哳</strong>：形容声音难听。</li>
<li><strong>青衫</strong>：八品、九品官员的服色，这里指江州司马。</li>
</ul>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<p>《琵琶行》是白居易叙事诗的代表作，也是中国古典诗歌中描写音乐最成功的作品之一。</p>
<p><strong>艺术特色：</strong></p>
<ol>
<li>
<p><strong>音乐描写精妙</strong>：诗人运用了大量比喻来形容琵琶声，如&quot;大珠小珠落玉盘&quot;、&quot;间关莺语花底滑&quot;等，将抽象的音乐形象化，达到了&quot;此时无声胜有声&quot;的艺术效果。</p>
</li>
<li>
<p><strong>结构严谨</strong>：全诗分为五个部分：江头送客遇琵琶女、琵琶女演奏、琵琶女自述身世、诗人感慨身世、再次演奏，结构完整，层次分明。</p>
</li>
<li>
<p><strong>人物形象鲜明</strong>：琵琶女的形象栩栩如生，从&quot;千呼万唤始出来，犹抱琵琶半遮面&quot;的羞涩，到自述身世的悲凉，人物性格丰满。</p>
</li>
<li>
<p><strong>情景交融</strong>：秋夜江头的萧瑟景色与人物的悲凉心境完美融合，营造了浓郁的悲剧氛围。</p>
</li>
</ol>
<p><strong>表现手法：</strong></p>
<ol>
<li>
<p><strong>对比手法</strong>：琵琶女昔日的繁华与今日的凄凉形成对比，突出了人生的无常。</p>
</li>
<li>
<p><strong>象征手法</strong>：琵琶女的遭遇象征着封建社会中下层知识分子和艺人的共同命运。</p>
</li>
<li>
<p><strong>烘托手法</strong>：通过环境描写烘托人物心情，如&quot;枫叶荻花秋瑟瑟&quot;。</p>
</li>
<li>
<p><strong>详略得当</strong>：对音乐描写详细，对其他内容适当简略，突出了主题。</p>
</li>
</ol>
<p><strong>思想内容：</strong></p>
<ol>
<li>
<p><strong>同情下层人民</strong>：通过琵琶女的遭遇，反映了封建社会中艺人和下层知识分子的悲惨命运。</p>
</li>
<li>
<p><strong>抒发贬谪之恨</strong>：诗人借琵琶女的身世抒发自己被贬的愤懑和孤独。</p>
</li>
<li>
<p><strong>揭示社会现实</strong>：揭露了封建社会的黑暗和不公，体现了现实主义精神。</p>
</li>
<li>
<p><strong>表达人生感慨</strong>：&quot;同是天涯沦落人，相逢何必曾相识&quot;成为千古名句，表达了对人生际遇的深刻感慨。</p>
</li>
</ol>
<p><strong>文学地位：</strong></p>
<p>《琵琶行》在中国文学史上具有重要地位：</p>
<ol>
<li>
<p><strong>叙事诗典范</strong>：是中国古典叙事诗的杰出代表，影响了后世无数作品。</p>
</li>
<li>
<p><strong>音乐描写典范</strong>：在音乐描写方面达到了前无古人的高度，被誉为&quot;音乐诗&quot;的典范。</p>
</li>
<li>
<p><strong>现实主义精神</strong>：体现了白居易&quot;文章合为时而著，歌诗合为事而作&quot;的现实主义创作主张。</p>
</li>
<li>
<p><strong>人文关怀</strong>：表现了对下层人民的深切同情，体现了诗人的人文关怀精神。</p>
</li>
</ol>

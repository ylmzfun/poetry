<h2 id="作者" tabindex="-1"><a class="header-anchor" href="#作者"><span>作者</span></a></h2>
<p>白居易</p>
<h2 id="体裁" tabindex="-1"><a class="header-anchor" href="#体裁"><span>体裁</span></a></h2>
<p>七言歌行（叙事长诗）</p>
<h2 id="简介" tabindex="-1"><a class="header-anchor" href="#简介"><span>简介</span></a></h2>
<p>白居易的《琵琶行》是中唐时期叙事诗的巅峰之作，通过描写诗人与琵琶女江上相逢的经历，以琵琶女的身世遭遇反映了封建社会中下层知识分子和艺人的悲惨命运。全诗以音乐描写见长，将抽象的音乐形象化，同时表达了&quot;同是天涯沦落人，相逢何必曾相识&quot;的深刻主题。</p>

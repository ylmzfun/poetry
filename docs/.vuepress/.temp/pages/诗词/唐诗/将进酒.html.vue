<h2 id="简介" tabindex="-1"><a class="header-anchor" href="#简介"><span>简介</span></a></h2>
<p>李白的《将进酒》是唐代诗歌中最具代表性的豪放之作，也是李白诗歌的巅峰之作之一。全诗以酒为媒介，抒发了诗人对人生短暂的感慨、对功名富贵的蔑视，以及对自由洒脱生活的向往。诗歌气势磅礴，感情奔放，体现了李白&quot;诗仙&quot;的浪漫主义风格。</p>

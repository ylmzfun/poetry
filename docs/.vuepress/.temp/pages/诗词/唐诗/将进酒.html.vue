<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p><strong>将进酒</strong></p>
<p>君不见，黄河之水天上来，奔流到海不复回。
君不见，高堂明镜悲白发，朝如青丝暮成雪。
人生得意须尽欢，莫使金樽空对月。
天生我材必有用，千金散尽还复来。
烹羊宰牛且为乐，会须一饮三百杯。
岑夫子，丹丘生，将进酒，杯莫停。
与君歌一曲，请君为我倾耳听。
钟鼓馔玉不足贵，但愿长醉不复醒。
古来圣贤皆寂寞，惟有饮者留其名。
陈王昔时宴平乐，斗酒十千恣欢谑。
主人何为言少钱，径须沽酒对君酌。
五花马，千金裘，呼儿将出换美酒，与尔同销万古愁。</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<ul>
<li><strong>将进酒</strong>：乐府旧题，意为&quot;劝酒歌&quot;。</li>
<li><strong>君不见</strong>：乐府诗中常用的呼告句式，用以引起注意。</li>
<li><strong>天上来</strong>：形容黄河源头高远，仿佛从天而降。</li>
<li><strong>高堂明镜</strong>：高大厅堂里的明镜。</li>
<li><strong>朝如青丝暮成雪</strong>：早晨还是黑发，晚上就变成了白发，形容时光飞逝。</li>
<li><strong>金樽</strong>：金制的酒杯。</li>
<li><strong>天生我材必有用</strong>：上天生下我，必然有用处。</li>
<li><strong>岑夫子，丹丘生</strong>：岑勋和元丹丘，都是李白的朋友。</li>
<li><strong>会须</strong>：应当。</li>
<li><strong>倾耳听</strong>：侧耳细听。</li>
<li><strong>钟鼓馔玉</strong>：指富贵人家的奢华生活。钟鼓，音乐；馔玉，美食。</li>
<li><strong>陈王</strong>：指曹植，曾被封为陈王。</li>
<li><strong>平乐</strong>：平乐观，汉代宫殿名。</li>
<li><strong>斗酒十千</strong>：一斗酒值十千钱，形容酒价昂贵。</li>
<li><strong>恣欢谑</strong>：尽情欢乐嬉戏。</li>
<li><strong>五花马</strong>：毛色作五花纹的名贵马匹。</li>
<li><strong>千金裘</strong>：价值千金的皮衣。</li>
</ul>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<p>这首诗是李白豪放诗风的典型代表，体现了盛唐诗歌的雄浑气象和浪漫主义精神。</p>
<p><strong>艺术特色：</strong></p>
<ol>
<li>
<p><strong>气势磅礴</strong>：开篇&quot;君不见，黄河之水天上来&quot;，以黄河的雄伟气势起兴，奠定了全诗豪放的基调。</p>
</li>
<li>
<p><strong>对比强烈</strong>：通过&quot;朝如青丝暮成雪&quot;的对比，突出时光飞逝；通过古今对比，表达人生感慨。</p>
</li>
<li>
<p><strong>感情奔放</strong>：全诗感情起伏跌宕，从感慨人生到及时行乐，从蔑视富贵到豪情万丈，情感变化丰富。</p>
</li>
<li>
<p><strong>语言豪迈</strong>：用词大胆，如&quot;千金散尽&quot;、&quot;一饮三百杯&quot;等，体现了诗人的豪放性格。</p>
</li>
</ol>
<p><strong>表现手法：</strong></p>
<ol>
<li>
<p><strong>夸张手法</strong>：如&quot;一饮三百杯&quot;、&quot;斗酒十千&quot;等，通过夸张表现豪放情怀。</p>
</li>
<li>
<p><strong>用典手法</strong>：引用曹植的典故，表达对古代才子的同情和自己的不平。</p>
</li>
<li>
<p><strong>反复咏叹</strong>：多次使用&quot;君不见&quot;、&quot;将进酒&quot;等句式，增强感染力。</p>
</li>
<li>
<p><strong>象征手法</strong>：以酒象征自由洒脱的生活态度，以黄河象征时间的永恒流逝。</p>
</li>
</ol>
<p><strong>思想内容：</strong></p>
<ol>
<li>
<p><strong>人生感慨</strong>：面对时光飞逝，表达了对人生短暂的深刻感慨。</p>
</li>
<li>
<p><strong>自信豪迈</strong>：&quot;天生我材必有用&quot;体现了诗人的自信和对自身价值的肯定。</p>
</li>
<li>
<p><strong>蔑视富贵</strong>：&quot;钟鼓馔玉不足贵&quot;表现了对世俗富贵的蔑视。</p>
</li>
<li>
<p><strong>及时行乐</strong>：主张&quot;人生得意须尽欢&quot;，体现了积极的人生态度。</p>
</li>
<li>
<p><strong>怀才不遇</strong>：通过对古代圣贤的感慨，暗示自己的怀才不遇。</p>
</li>
</ol>
<p><strong>文学地位：</strong></p>
<p>《将进酒》是李白诗歌的代表作之一，也是中国古典诗歌中豪放派的典范：</p>
<ol>
<li>
<p><strong>豪放典范</strong>：开创了中国诗歌豪放风格的先河，影响了后世无数诗人。</p>
</li>
<li>
<p><strong>浪漫主义</strong>：体现了盛唐浪漫主义诗歌的最高成就。</p>
</li>
<li>
<p><strong>人生哲学</strong>：表达了积极向上的人生态度和对自由的向往。</p>
</li>
<li>
<p><strong>艺术成就</strong>：在语言运用、情感表达、意境营造等方面都达到了很高的艺术水准。</p>
</li>
</ol>

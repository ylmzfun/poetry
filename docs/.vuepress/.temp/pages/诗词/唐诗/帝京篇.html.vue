<h1 id="帝京篇" tabindex="-1"><a class="header-anchor" href="#帝京篇"><span>帝京篇</span></a></h1>
<p>作者：骆宾王（唐）
体裁：古体诗</p>
<p>导言</p>
<ul>
<li>都城壮观的铺陈描写，辞采华美，气势宏阔。</li>
</ul>
<p>底本与版本</p>
<ul>
<li>推荐检索：中华书局《骆宾王集》点校本；旁证《全唐诗》。</li>
</ul>
<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p>山河千里国，城阙九重门。不睹皇居壮，安知天子尊。
皇居帝里崤函谷，鹑野龙山侯甸服。五纬连影集星躔，八水分流横地轴。
秦塞重关一百二，汉家离宫三十六。桂殿嶔岑对玉楼，椒房窈窕连金屋。
三条九陌丽城隈，万户千门平旦开。复道斜通鳷鹊观，交衢直指凤凰台。
剑履南宫入，簪缨北阙来。声名冠寰宇，文物象昭回。
钩陈肃兰戺，璧沼浮槐市。铜羽应风回，金茎承露起。
校文天禄阁，习战昆明水。朱邸抗平台，黄扉通戚里。
平台戚里带崇墉，炊金馔玉待鸣钟。小堂绮帐三千户，大道青楼十二重。
宝盖雕鞍金络马，兰窗绣柱玉盘龙。绣柱璇题粉壁映，锵金鸣玉王侯盛。
王侯贵人多近臣，朝游北里暮南邻。陆贾分金将宴喜，陈遵投辖正留宾。
赵李经过密，萧朱交结亲。丹凤朱城白日暮，青牛绀幰红尘度。
侠客珠弹垂杨道，倡妇银钩采桑路。倡家桃李自芳菲，京华游侠盛轻肥。
延年女弟双凤入，罗敷使君千骑归。同心结缕带，连理织成衣。
春朝桂尊尊百味，秋夜兰灯灯九微。翠幌珠帘不独映，清歌宝瑟自相依。
且论三万六千是，宁知四十九年非。古来荣利若浮云，人生倚伏信难分！
始见田窦相移夺，俄闻卫霍有功勋。未厌金陵气，先开石椁文。
朱门无复张公子，灞亭谁畏李将军。相顾百龄皆有待，居然万化咸应改。
桂枝芳气已销亡，柏梁高宴今何在？春去春来苦自驰，争名争利徒尔为。
久留郎署终难遇，空扫相门谁见知。当时一旦擅豪华，自言千载长骄奢。
倏忽抟风生羽翼，须臾失浪委泥沙。黄雀徒巢桂，青门遂种瓜。
黄金销铄素丝变，一贵一贱交情见。红颜宿昔白头新，脱粟布衣轻故人。
故人有湮沦，新知无意气。灰死韩安国，罗伤翟廷尉。
已矣哉，归去来！马卿辞蜀多文藻，扬雄仕汉乏良媒。
三冬自矜诚足用，十年不调几邅回。汲黯薪逾积，孙弘阁未开。
谁惜长沙傅，独负洛阳才？</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<p><strong>注释：</strong></p>
<ol>
<li>城阙：都城，京城。九重门：皇帝的住处有九重门，形容天子居处宫禁严密。</li>
<li>皇居：皇宫，亦指皇城。</li>
<li>帝里：犹言帝都，京都。崤函：崤山和函谷关，自古为险要的关隘，是长安东面的天然屏障。</li>
<li>鹑野：指秦地。古代天文称南方朱鸟七宿的前二宿井、鬼为鹑首，其分野为秦地。龙山：即龙首山，唐大明宫在龙首山。侯甸服：候服与甸服。古时候以京城为中心，方圆一千里叫王畿，其外方五百里叫侯服，侯服外方五百里叫甸服。</li>
<li>五纬：水、木、金、火、土五个行星合称为五纬。星躔：日月星辰运行的度次。</li>
<li>八水：即八川，指关内泾、渭、灞、泸、涝、淆、沣及滴八条河流。此用来借指关中地区。地轴：传说中大地的轴，泛指大地。</li>
<li>离宫：行官，帝王在都城之外的宫殿。三十六：虚指，形容多。</li>
<li>桂殿：即桂宫，汉武帝时所建。嶔岑：高耸深邃的样子。</li>
<li>椒房：即椒房殿，汉代时皇后居住的宫殿，以椒和泥涂在墙壁上，取温、香、多子之义，后泛指后妃居住的宫室。窈窕：深远的样子。金屋：泛指华美的宫室，典出汉武帝金屋藏娇事。</li>
<li>三条九陌：泛指帝都的纵横大道。三、九为虚指。城隈：城边。</li>
<li>平旦：清晨。</li>
<li>鳷鹊观：汉宫观名，在长安甘泉宫外，汉武帝建元中建。</li>
<li>交衢：指道路交错要冲之处。</li>
<li>剑履：即剑履上殿，古代得到帝王特许的大臣，可以佩着剑穿着鞋上朝，被视为极大的优遇。</li>
<li>簪缨：古代达官贵人的冠饰。后遂借以指高官显宦。</li>
<li>寰宇：犹天下。旧指国家全境。</li>
<li>昭回：谓星辰光耀回转。</li>
<li>钩陈：指后宫。兰戺：台阶的美称。</li>
<li>璧沼：即璧池，古代学宫前半月形的水池，借指太学和皇帝的选士之所。槐市：汉代长安读书人聚会、贸易之市。因其地多槐而得名。后借指学宫，学舍。</li>
<li>铜羽：即铜乌。</li>
<li>金茎：用以擎承露盘的铜柱。</li>
<li>校文：校勘文章。天禄阁：汉宫中藏书阁。</li>
<li>习战：练兵。昆明水：即昆明池，汉武帝在长安西南所凿的人工湖，用以训练水军。</li>
</ol>
<p><strong>译文：</strong>
国家山河千万里，都城门户九重。不曾目睹帝都皇宫的壮丽，哪里知道天子的尊贵？帝都坐落于崤山与函谷关，秦地龙山一带都属于侯服与甸服。五星连缀与日月依次运行，八川分流纵横于地轴之上。秦地关塞有一百二十重，汉家离宫有三十六座。高峻的宫殿对着华丽的楼宇，幽深的后宫连着金碧的屋室。帝都的角落也都是纵横大道，千门万户一到清晨次第开。凌空的复道斜着伸向鳷鹊观，道路要冲直接通向凤凰台。高官显宦们身佩宝剑，昂然从南宫北阙进进出出。美名扬于天下，形象题于画阁，业绩载入史册，光荣如同日月。</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<p><strong>创作背景：</strong>
此诗作于唐高宗上元三年（676）诗人从武功主簿调任明堂主簿时。据《旧唐书·文苑传》记载，这首诗又题《上吏部侍郎帝京篇》，诗的前面曾有一篇&quot;启&quot;，作者投赠给当时的吏部侍郎裴行俭，传遍京畿，&quot;以为绝唱&quot;。</p>
<p><strong>主题思想：</strong>
此诗描绘帝京长安的繁华，颇多壮词，显示出大唐帝国的强盛和蓬勃向上的时代风貌，提出了&quot;未厌金陵气，先开石椁文&quot;的居安思危的警示，抒发了怀才不遇的悲愤。</p>
<p><strong>艺术特色：</strong></p>
<ol>
<li>结构严谨：全诗分为四个段落，从描绘京城胜状到抒发个人感慨，层次分明。</li>
<li>语言华丽：运用大量典故和华美词藻，展现盛唐气象。</li>
<li>对比鲜明：通过对比手法，突出了社会的不公和个人的失意。</li>
<li>赋法运用：诗中运用赋法，为盛唐歌行的创作开了新生面。</li>
</ol>
<p><strong>名句赏析：</strong>
&quot;古来荣利若浮云，人生倚伏信难分&quot;：这句话深刻地揭示了人生的无常和荣辱得失的变化莫测，体现了诗人对人生的深刻思考。</p>

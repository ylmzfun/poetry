<h1 id="山居秋暝" tabindex="-1"><a class="header-anchor" href="#山居秋暝"><span>山居秋暝</span></a></h1>
<p>标题</p>
<ul>
<li>题名：山居秋暝</li>
<li>作者：王维</li>
<li>体裁：诗（五言律诗）</li>
<li>简介：空山新雨后，描绘山居秋夜的清美宁静，诗中有画。</li>
<li>推荐版本：中华书局《王维集》、全唐诗校注本。</li>
</ul>
<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p>空山新雨后，天气晚来秋。<br>
明月松间照，清泉石上流。<br>
竹喧归浣女，莲动下渔舟。<br>
随意春芳歇，王孙自可留。</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<p><strong>暝（míng）</strong>：日落，天色将晚。<br>
<strong>空山</strong>：空旷，空寂的山野。<br>
<strong>新</strong>：刚刚。<br>
<strong>清泉石上流</strong>：写的正是雨后的景色。<br>
<strong>竹喧</strong>：竹林中笑语喧哗。喧：喧哗，这里指竹叶发出沙沙声响。<br>
<strong>浣（huàn）女</strong>：洗衣服的姑娘。浣：洗涤衣物。<br>
<strong>随意</strong>：任凭。<br>
<strong>春芳</strong>：春天的花草。歇：消散，消失。<br>
<strong>王孙</strong>：原指贵族子弟，后来也泛指隐居的人。此处实亦自指。<br>
<strong>留</strong>：居。此句反用淮南小山《招隐士》：&quot;王孙兮归来，山中兮不可久留&quot;之意，反映出无可无不可的襟怀。</p>
<h2 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h2>
<p>空旷的群山沐浴了一场新雨，夜晚降临使人感到已是初秋。<br>
皎皎明月从松隙间洒下清光，清清泉水在山石上淙淙淌流。<br>
竹林喧响知是洗衣姑娘归来，莲叶轻摇想是上游荡下轻舟。<br>
春日的芳菲不妨任随它消歇，秋天的山中王孙自可以久留。</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<h3 id="创作背景" tabindex="-1"><a class="header-anchor" href="#创作背景"><span>创作背景</span></a></h3>
<p>这首诗描绘的是清秋薄暮雨后初晴时的山村图景，当作于王维隐居终南山下辋川别业时。<mcreference link="https://baike.baidu.com/item/山居秋暝/2794200" index="1">1</mcreference> 开元二十四年（736），唐玄宗时期最后一个开明的宰相张九龄被李林甫等排挤罢官，朝政日趋昏暗，王维的政治热情冷却下来，对政治抱着消极的态度。开元二十八年（740）后，他在终南山构筑了别墅，过着半官半隐的生活，此诗即作于这个时期。<mcreference link="https://baike.baidu.com/item/山居秋暝/2794200" index="1">1</mcreference></p>
<h3 id="主题思想" tabindex="-1"><a class="header-anchor" href="#主题思想"><span>主题思想</span></a></h3>
<p>此诗描绘了秋雨初晴后傍晚时分山村的旖旎风光和山居村民的淳朴风尚，表现了诗人寄情山水田园并对隐居生活怡然自得的满足心情，以自然美来表现人格美和社会美。<mcreference link="https://www.gushiwen.cn/shiwenv_44ba4afb80db.aspx" index="2">2</mcreference></p>
<h3 id="艺术特色" tabindex="-1"><a class="header-anchor" href="#艺术特色"><span>艺术特色</span></a></h3>
<p>这首诗为山水名篇，于诗情画意之中寄托着诗人高洁的情怀和对理想境界的追求。<mcreference link="https://meirishici.com/poetry/zLA" index="4">4</mcreference> 全诗将空山雨后的秋凉、松间明月的光照、石上清泉的声音以及浣女归来竹林中的喧笑声，渔船穿过荷花的动态，和谐完美地融合在一起，给人一种丰富新鲜的感受。它像一幅清新秀丽的山水画，又像一支恬静优美的抒情乐曲，体现了王维诗中有画的创作特点。<mcreference link="https://baike.baidu.com/item/山居秋暝/2794200" index="1">1</mcreference></p>
<h3 id="表现手法" tabindex="-1"><a class="header-anchor" href="#表现手法"><span>表现手法</span></a></h3>
<ol>
<li>
<p><strong>以动衬静</strong>：&quot;竹喧归浣女，莲动下渔舟&quot;，通过浣女的喧笑声和渔舟的动态，反衬出山中的宁静。<mcreference link="https://www.xueshu.com/haowen/112892.html" index="3">3</mcreference></p>
</li>
<li>
<p><strong>虚实结合</strong>：诗人先写&quot;竹喧&quot;&quot;莲动&quot;，因为浣女隐在竹林之中，渔舟被莲叶遮蔽，起初未见，等到听到竹林喧声，看到莲叶纷披，才发现浣女、莲舟。这样写更富有真情实感，更富有诗意。<mcreference link="https://meirishici.com/poetry/zLA" index="4">4</mcreference></p>
</li>
<li>
<p><strong>比兴手法</strong>：这首诗一个重要的艺术手法，是以自然美来表现诗人的人格美和一种理想中的社会之美。表面看来，这首诗只是用&quot;赋&quot;的方法模山范水，对景物作细致感人的刻画，实际上通篇都是比兴。<mcreference link="https://meirishici.com/poetry/zLA" index="4">4</mcreference></p>
</li>
</ol>
<h3 id="结构分析" tabindex="-1"><a class="header-anchor" href="#结构分析"><span>结构分析</span></a></h3>
<ul>
<li>
<p><strong>首联</strong>：&quot;空山新雨后，天气晚来秋。&quot;写雨后山中秋景，点出时间和环境。&quot;空山&quot;两字点出此处有如世外桃源，山雨初霁，万物为之一新。<mcreference link="https://meirishici.com/poetry/zLA" index="4">4</mcreference></p>
</li>
<li>
<p><strong>颔联</strong>：&quot;明月松间照，清泉石上流。&quot;侧重写物，以物芳而明志洁。天色已暝，却有皓月当空；群芳已谢，却有青松如盖。<mcreference link="https://meirishici.com/poetry/zLA" index="4">4</mcreference></p>
</li>
<li>
<p><strong>颈联</strong>：&quot;竹喧归浣女，莲动下渔舟。&quot;侧重写人，以人和而望政通。在这青松明月之下，在这翠竹青莲之中，生活着这样无忧无虑、勤劳善良的人们。<mcreference link="https://meirishici.com/poetry/zLA" index="4">4</mcreference></p>
</li>
<li>
<p><strong>尾联</strong>：&quot;随意春芳歇，王孙自可留。&quot;反用《楚辞·招隐士》的意思，表达了诗人对山中生活的向往和对隐居生活的满足。<mcreference link="https://meirishici.com/poetry/zLA" index="4">4</mcreference></p>
</li>
</ul>
<h3 id="名句赏析" tabindex="-1"><a class="header-anchor" href="#名句赏析"><span>名句赏析</span></a></h3>
<p>&quot;明月松间照，清泉石上流&quot;是全诗最著名的句子，写景如画，随意洒脱，毫不着力。山泉清冽，淙淙流泻于山石之上，有如一条洁白无瑕的素练，在月光下闪闪发光，生动表现了幽清明净的自然美。<mcreference link="https://meirishici.com/poetry/zLA" index="4">4</mcreference></p>
<h3 id="语言特色" tabindex="-1"><a class="header-anchor" href="#语言特色"><span>语言特色</span></a></h3>
<p>这首诗语言清新自然，意境深远。诗人用简洁的语言描绘出一幅生动的山水画卷，体现了王维&quot;诗中有画，画中有诗&quot;的艺术特色。<mcreference link="https://www.gushiwen.cn/shiwenv_44ba4afb80db.aspx" index="2">2</mcreference></p>
<h3 id="思想内容" tabindex="-1"><a class="header-anchor" href="#思想内容"><span>思想内容</span></a></h3>
<p>这纯洁美好的生活图景，反映了诗人过安静纯朴生活的理想，同时也从反面衬托出他对污浊官场的厌恶。诗人觉得&quot;山中&quot;比&quot;朝中&quot;好，洁净纯朴，可以远离官场而洁身自好，所以就决然归隐了。<mcreference link="https://meirishici.com/poetry/zLA" index="4">4</mcreference></p>
<h3 id="文学地位" tabindex="-1"><a class="header-anchor" href="#文学地位"><span>文学地位</span></a></h3>
<p>《山居秋暝》是王维山水诗的代表作之一，也是唐代山水诗的经典之作。它充分体现了王维&quot;诗中有画&quot;的艺术特色，被誉为山水诗的典范。<mcreference link="https://www.gushiwen.cn/shiwenv_44ba4afb80db.aspx" index="2">2</mcreference></p>

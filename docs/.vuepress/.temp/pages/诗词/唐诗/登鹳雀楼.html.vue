<h1 id="登鹳雀楼" tabindex="-1"><a class="header-anchor" href="#登鹳雀楼"><span>登鹳雀楼</span></a></h1>
<p>标题</p>
<ul>
<li>题名：登鹳雀楼</li>
<li>作者：王之涣</li>
<li>体裁：诗（七言绝句）</li>
<li>简介：借登楼写胸襟与志向，以阔远视野寄寓进取精神。</li>
<li>推荐版本：唐诗选本校注、中华书局相关总集。</li>
</ul>
<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p>白日依山尽，黄河入海流。
欲穷千里目，更上一层楼。</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<ol>
<li><strong>鹳雀楼</strong>：古名鹳鹊楼，因时有鹳鹊栖其上而得名，其故址在山西省永济市境内古蒲州城外西南的黄河岸边。楼高三层，前对中条山，下临黄河。</li>
<li><strong>白日</strong>：太阳。</li>
<li><strong>依</strong>：依傍。</li>
<li><strong>尽</strong>：消失。这句话是说太阳依傍山峦沉落。</li>
<li><strong>欲</strong>：想要得到某种东西或达到某种目的的愿望，但也有希望、想要的意思。</li>
<li><strong>穷</strong>：尽，使达到极点。</li>
<li><strong>千里目</strong>：眼界宽阔。</li>
<li><strong>更</strong>：再。</li>
</ol>
<h2 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h2>
<p>夕阳依傍着山峦渐渐下落，滔滔黄河朝着大海汹涌奔流。
若想把千里的风光景物看够，那就请再登上一层高楼。</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<p>《登鹳雀楼》是王之涣仅存的六首绝句之一，被誉为唐代五言诗的压卷之作。这首诗写诗人在登高望远中表现出来的不凡的胸襟抱负，反映了盛唐时期人们积极向上的进取精神。</p>
<h3 id="艺术特色" tabindex="-1"><a class="header-anchor" href="#艺术特色"><span>艺术特色</span></a></h3>
<ol>
<li>
<p><strong>景物描写的宏大气势</strong>：前两句&quot;白日依山尽，黄河入海流&quot;运用极其朴素、极其浅显的语言，既高度形象又高度概括地把进入广大视野的万里河山，收入短短十个字中，展现出缩万里于咫尺的艺术效果。</p>
</li>
<li>
<p><strong>时空交融的写法</strong>：首句写遥望一轮落日向着楼前一望无际、连绵起伏的群山西沉，这是天空景、远方景、西望景；次句写目送流经楼前下方的黄河奔腾咆哮、滚滚南来，又在远处折而东向，流归大海，这是由地面望到天边，由近望到远，由西望到东。</p>
</li>
<li>
<p><strong>虚实结合</strong>：就次句诗而言，诗人身在鹳雀楼上，不可能望见黄河入海，句中写的是诗人目送黄河远去天边而产生的意中景，是把当前景与意中景溶合为一的写法。</p>
</li>
<li>
<p><strong>动静结合</strong>：白日依山而尽，这仅仅是一个极短暂的过程；黄河向海而流，却是一种永恒的运动。这种景色是一种动态的美，充满了无限生机的活泼的美。</p>
</li>
</ol>
<h3 id="哲理内涵" tabindex="-1"><a class="header-anchor" href="#哲理内涵"><span>哲理内涵</span></a></h3>
<p>后两句&quot;欲穷千里目，更上一层楼&quot;写所想，表达了诗人一种无止境探求的愿望。&quot;千里&quot;&quot;一层&quot;，都是虚数，是诗人想象中纵横两方面的空间。&quot;欲穷&quot;&quot;更上&quot;词语中包含了多少希望，多少憧憬。这里有诗人的向上进取的精神、高瞻远瞩的胸襟，也道出了要站得高才看得远的哲理。</p>
<h3 id="表现手法" tabindex="-1"><a class="header-anchor" href="#表现手法"><span>表现手法</span></a></h3>
<ol>
<li>
<p><strong>景入理势</strong>：这首诗是日僧空海在《文镜秘府论》中所说的&quot;景入理势&quot;。把道理与景物、情事溶化得天衣无缝，使读者并不觉得它在说理，而理自在其中。</p>
</li>
<li>
<p><strong>全篇对仗</strong>：这是一首全篇用对仗的绝句。沈德潜在《唐诗别裁》中选录这首诗时曾指出：&quot;四语皆对，读来不嫌其排，骨高故也。&quot;前一联用的是正名对，语句极为工整，又厚重有力；后一联用的是流水对，虽两句相对，而没有对仗的痕迹。</p>
</li>
<li>
<p><strong>层次递进</strong>：从这后半首诗，可推知前半首写的可能是在第二层楼所见，而诗人还想进一步穷目力所及看尽远方景物，更登上了楼的顶层。</p>
</li>
</ol>
<h3 id="名句赏析" tabindex="-1"><a class="header-anchor" href="#名句赏析"><span>名句赏析</span></a></h3>
<p><strong>&quot;白日依山尽，黄河入海流&quot;</strong>：这两句把上下、远近、东西的景物，全都容纳进诗笔之下，使画面显得特别宽广，特别辽远。诗人用大手笔勾勒出一幅溢光流彩、金碧交辉的壮丽图画。</p>
<p><strong>&quot;欲穷千里目，更上一层楼&quot;</strong>：这是千古传诵的名句，既别翻新意，出人意表，又与前两句诗承接得十分自然、十分紧密。这两句诗发表议论，把诗篇推引入更高的境界，向读者展示了更大的视野。</p>
<h3 id="创作背景" tabindex="-1"><a class="header-anchor" href="#创作背景"><span>创作背景</span></a></h3>
<p>这首诗是王之涣早年及第，曾任过冀州衡水县主薄，不久因遭人诬陷而罢官后，在访友漫游的生活中所作。学者推测，创作年代约在开元十五年（727）至二十九年（741）之间，当时王之涣在弃官回乡时所作。</p>
<h3 id="文学地位" tabindex="-1"><a class="header-anchor" href="#文学地位"><span>文学地位</span></a></h3>
<ol>
<li><strong>唐代五言诗的压卷之作</strong>：被誉为唐代五言绝句的典范</li>
<li><strong>千古绝唱</strong>：清代诗评家认为：&quot;王诗短短二十字，前十字大意已尽，后十字有尺幅千里之势&quot;</li>
<li><strong>哲理诗的典范</strong>：成功地将哲理与景物融合，是运用形象思维来显示生活哲理的典范</li>
<li><strong>影响深远</strong>：王之涣因这首五言绝句而名垂千古，鹳雀楼也因此诗而名扬中华</li>
</ol>
<p>《梦溪笔谈》中曾指出，唐人在鹳雀楼所留下的诗中，&quot;惟李益、王之涣、畅当三篇，能状其景&quot;，但王之涣的这首诗独步千古，成为登楼诗的不朽名篇。这首诗以其深远的意境、精湛的技巧和深刻的哲理，体现了盛唐诗歌的雄浑气象和积极向上的时代精神。</p>

<h1 id="出塞" tabindex="-1"><a class="header-anchor" href="#出塞"><span>出塞</span></a></h1>
<p>标题</p>
<ul>
<li>题名：出塞</li>
<li>作者：王昌龄（常见《出塞·秦时明月汉时关》）</li>
<li>体裁：诗（七言绝句）</li>
<li>简介：边塞诗代表作，以沉雄之气写家国与征战之情。</li>
<li>推荐版本：全唐诗校注、中华书局《王昌龄集》。</li>
</ul>
<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p>秦时明月汉时关，万里长征人未还。
但使龙城飞将在，不教胡马度阴山。</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<ol>
<li><strong>出塞</strong>：乐府旧题，多写边塞征战之事。</li>
<li><strong>秦时明月汉时关</strong>：互文见义，即秦汉时的明月和关塞。指明月和雄关依然和秦汉时代一样。</li>
<li><strong>万里长征</strong>：指远征边塞。</li>
<li><strong>人未还</strong>：征人未归。</li>
<li><strong>但使</strong>：只要。</li>
<li><strong>龙城飞将</strong>：指汉朝名将李广。龙城是匈奴祭天集会的地方，李广曾在此大败匈奴，匈奴畏惧他的神勇，特称他为&quot;飞将军&quot;。</li>
<li><strong>不教</strong>：不让，不使。</li>
<li><strong>胡马</strong>：指敌人的战马，代指敌人的军队。</li>
<li><strong>度</strong>：越过。</li>
<li><strong>阴山</strong>：山名，在今内蒙古自治区，是我国北方的天然屏障。</li>
</ol>
<h2 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h2>
<p>依旧是秦汉时期的明月和边关，守边御敌鏖战万里征夫未回还。
倘若龙城的飞将李广如今还在，一定不会让敌人的铁蹄踏过阴山。</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<p>《出塞》是王昌龄早年赴西域时所作的边塞诗代表作，被明代诗人李攀龄誉为唐人七绝的&quot;压卷之作&quot;。这首诗以雄劲的笔触，对当时的边塞战争生活作了高度的艺术概括，慨叹边战不断以及国无良将，反映出当时人民要求平息边患、享受安定生活的美好愿望。</p>
<h3 id="艺术特色" tabindex="-1"><a class="header-anchor" href="#艺术特色"><span>艺术特色</span></a></h3>
<ol>
<li>
<p><strong>时空交错的宏大视野</strong>：首句&quot;秦时明月汉时关&quot;运用互文手法，将秦汉时代的明月和关塞并提，营造出时间的纵深感和空间的广阔感，暗示边塞战争的历史悠久。</p>
</li>
<li>
<p><strong>对比手法的运用</strong>：诗中将古代名将李广与当时将领形成对比，通过怀古讽今，表达对现实的不满和对良将的渴望。</p>
</li>
<li>
<p><strong>情景交融</strong>：冷月照边关的苍凉景象与征人未归的悲凉现实相互映衬，营造出深沉的悲壮氛围。</p>
</li>
<li>
<p><strong>语言凝练有力</strong>：全诗仅28字，却包含了丰富的历史内容和深刻的思想感情。</p>
</li>
</ol>
<h3 id="主题思想" tabindex="-1"><a class="header-anchor" href="#主题思想"><span>主题思想</span></a></h3>
<ol>
<li>
<p><strong>反战思想</strong>：通过对边塞战争持续不断的描写，表达了对战争的厌倦和对和平的渴望。</p>
</li>
<li>
<p><strong>忧国忧民</strong>：诗人关心国家边防，同情长期征战的士兵，体现了深厚的爱国情怀。</p>
</li>
<li>
<p><strong>怀古讽今</strong>：通过对古代名将的怀念，批评当时将领的无能，寄托了对贤能将领的期盼。</p>
</li>
</ol>
<h3 id="表现手法" tabindex="-1"><a class="header-anchor" href="#表现手法"><span>表现手法</span></a></h3>
<ol>
<li>
<p><strong>互文见义</strong>：&quot;秦时明月汉时关&quot;是典型的互文手法，秦、汉、明月、关塞四个词语交错使用，意思是秦汉时的明月和关塞。</p>
</li>
<li>
<p><strong>借古讽今</strong>：通过赞美汉代名将李广，暗讽当时边将的无能。</p>
</li>
<li>
<p><strong>虚实结合</strong>：前两句写实景，后两句是假设，虚实结合，增强了表达效果。</p>
</li>
</ol>
<h3 id="名句赏析" tabindex="-1"><a class="header-anchor" href="#名句赏析"><span>名句赏析</span></a></h3>
<p><strong>&quot;秦时明月汉时关&quot;</strong>：这是全诗最著名的句子，以其雄浑的气势和深远的意境成为千古名句。诗人用大笔勾勒，展现出一幅壮阔的边塞图画，同时赋予了万里边关悠久的历史感。</p>
<p><strong>&quot;但使龙城飞将在，不教胡马度阴山&quot;</strong>：表达了诗人的理想和愿望，如果有李广这样的名将镇守边关，就能够抵御外敌入侵，保卫国家安全。</p>
<h3 id="创作背景" tabindex="-1"><a class="header-anchor" href="#创作背景"><span>创作背景</span></a></h3>
<p>这首诗创作于盛唐时期，当时边塞战争频繁，人民不堪重负。王昌龄作为边塞诗人，深入边塞，了解边防实情，因此能够写出如此真切感人的作品。</p>
<h3 id="文学地位" tabindex="-1"><a class="header-anchor" href="#文学地位"><span>文学地位</span></a></h3>
<p>《出塞》在中国文学史上具有重要地位：</p>
<ol>
<li><strong>边塞诗的典范</strong>：被誉为边塞诗的代表作之一</li>
<li><strong>七绝的巅峰</strong>：被称为唐人七绝的压卷之作</li>
<li><strong>影响深远</strong>：千百年来广为传诵，影响了无数后人</li>
<li><strong>艺术价值</strong>：在短短四句中展现了高超的艺术技巧和深刻的思想内容</li>
</ol>
<p>这首诗以其深沉的历史感、强烈的现实关怀和精湛的艺术技巧，成为中国古典诗歌中的不朽名篇，体现了王昌龄作为&quot;七绝圣手&quot;的卓越才华。</p>

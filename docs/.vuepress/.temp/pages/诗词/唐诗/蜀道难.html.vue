<h1 id="蜀道难" tabindex="-1"><a class="header-anchor" href="#蜀道难"><span>蜀道难</span></a></h1>
<p>作者：李白（唐）
体裁：乐府古题</p>
<p>导言</p>
<ul>
<li>奇峭瑰丽，夸饰山川险峻，以景寓情。</li>
</ul>
<p>底本与版本</p>
<ul>

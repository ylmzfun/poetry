<h1 id="围炉夜话" tabindex="-1"><a class="header-anchor" href="#围炉夜话"><span>围炉夜话</span></a></h1>
<p>体裁：劝世文/短章</p>
<p>导言</p>
<ul>
<li>以德行修身、处世智慧为旨，短章劝诫，语近理深。</li>
</ul>

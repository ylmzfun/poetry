<h2 id="简介" tabindex="-1"><a class="header-anchor" href="#简介"><span>简介</span></a></h2>
<p>王永彬（1792-1869），字宜山，号梦庵，清代学者。江苏上元（今南京）人。道光年间举人，曾任知县。博学多才，精通经史，著有《围炉夜话》、《西岩赘语》等。</p>
<h2 id="选摘" tabindex="-1"><a class="header-anchor" href="#选摘"><span>选摘</span></a></h2>
<h3 id="第一则" tabindex="-1"><a class="header-anchor" href="#第一则"><span>第一则</span></a></h3>
<p>教子弟于幼时，便当有正大光明气象；检身心于平日，不可无忧勤惕厉工夫。</p>
<h3 id="第二则" tabindex="-1"><a class="header-anchor" href="#第二则"><span>第二则</span></a></h3>
<p>一信字是人生要紧关头，信得过自家，方能信得过人；信得过现在，方能信得过未来。</p>
<h3 id="第三则" tabindex="-1"><a class="header-anchor" href="#第三则"><span>第三则</span></a></h3>
<p>读书无疑者，须教有疑；有疑者，却要无疑，到这里方是长进。</p>
<h3 id="第四则" tabindex="-1"><a class="header-anchor" href="#第四则"><span>第四则</span></a></h3>
<p>为善最乐，是不求人知。为恶最苦，是惟恐人知。</p>
<h3 id="第五则" tabindex="-1"><a class="header-anchor" href="#第五则"><span>第五则</span></a></h3>
<p>心能辨是非，处事方能决断；人不忘廉耻，立身自不卑污。</p>
<h3 id="第六则" tabindex="-1"><a class="header-anchor" href="#第六则"><span>第六则</span></a></h3>
<p>守身必谨严，凡足以戕吾身者，宜戒之；养心须淡泊，凡足以累吾心者，勿为也。</p>
<h3 id="第七则" tabindex="-1"><a class="header-anchor" href="#第七则"><span>第七则</span></a></h3>
<p>处事要代人作想，读书须切己用功。</p>
<h3 id="第八则" tabindex="-1"><a class="header-anchor" href="#第八则"><span>第八则</span></a></h3>
<p>静坐常思己过，闲谈莫论人非。</p>
<h3 id="第九则" tabindex="-1"><a class="header-anchor" href="#第九则"><span>第九则</span></a></h3>
<p>教小儿宜严，严气足以平躁气；待小人宜敬，敬心可以化邪心。</p>
<h3 id="第十则" tabindex="-1"><a class="header-anchor" href="#第十则"><span>第十则</span></a></h3>
<p>志不可不高，志不高，则同流合污，无足有为矣；心不可太大，心太大，则舍近图远，难期有成矣。</p>
<h3 id="第十一则" tabindex="-1"><a class="header-anchor" href="#第十一则"><span>第十一则</span></a></h3>
<p>富贵易生祸端，必忠厚谦恭，才无大患；衣禄原有定数，必节俭简省，乃可久延。</p>
<h3 id="第十二则" tabindex="-1"><a class="header-anchor" href="#第十二则"><span>第十二则</span></a></h3>
<p>人品之不高，总为一利字看不破；学业之不进，总为一懒字丢不开。</p>
<h3 id="第十三则" tabindex="-1"><a class="header-anchor" href="#第十三则"><span>第十三则</span></a></h3>
<p>古今来许多世家，无非积德；天地间第一人品，还是读书。</p>
<h3 id="第十四则" tabindex="-1"><a class="header-anchor" href="#第十四则"><span>第十四则</span></a></h3>
<p>父兄有善行，子弟学之或不肖，犹贤于父兄有恶行，子弟学之而肖者也。</p>
<h3 id="第十五则" tabindex="-1"><a class="header-anchor" href="#第十五则"><span>第十五则</span></a></h3>
<p>薄福之人过享其福，必有忽然之祸；贫贱之人不安其贫贱，必有意外之忧。</p>
<h3 id="第十六则" tabindex="-1"><a class="header-anchor" href="#第十六则"><span>第十六则</span></a></h3>
<p>处父子骨肉之间，不可以恩掩义；在君臣朋友之际，不可以义遗恩。</p>
<h3 id="第十七则" tabindex="-1"><a class="header-anchor" href="#第十七则"><span>第十七则</span></a></h3>
<p>气性乖张，多是夭亡之子；语言深刻，终为薄福之人。</p>
<h3 id="第十八则" tabindex="-1"><a class="header-anchor" href="#第十八则"><span>第十八则</span></a></h3>
<p>觉人之诈，不形于言；受人之侮，不动于色。此中有无穷意味，亦有无穷受用。</p>
<h3 id="第十九则" tabindex="-1"><a class="header-anchor" href="#第十九则"><span>第十九则</span></a></h3>
<p>忠有愚忠，孝有愚孝，可知忠孝二字不是伶俐人做得来；仁有假仁，义有假义，可知仁义二字不是奸恶人做得来。</p>
<h3 id="第二十则" tabindex="-1"><a class="header-anchor" href="#第二十则"><span>第二十则</span></a></h3>
<p>与朋友交游，须将他好处留心学来，方能受益；对圣贤言语，必要我平时照样行去，才算读书。</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<h3 id="创作背景" tabindex="-1"><a class="header-anchor" href="#创作背景"><span>创作背景</span></a></h3>
<p>《围炉夜话》成书于清代道光年间，是王永彬在寒冬之夜围炉而作的人生感悟。全书共221则，以修身、齐家、治国、平天下为主线，融合儒家思想精髓，体现了中国传统文化中的人生智慧。</p>
<h3 id="主要内容" tabindex="-1"><a class="header-anchor" href="#主要内容"><span>主要内容</span></a></h3>
<ol>
<li><strong>修身养性</strong>：强调品德修养的重要性，提倡正心诚意</li>
<li><strong>处世智慧</strong>：传授为人处世的道理和方法</li>
<li><strong>教育理念</strong>：阐述教育子女和自我学习的原则</li>
<li><strong>人际关系</strong>：指导如何处理各种人际关系</li>
<li><strong>人生哲理</strong>：探讨人生的意义和价值</li>
</ol>
<h3 id="思想特色" tabindex="-1"><a class="header-anchor" href="#思想特色"><span>思想特色</span></a></h3>
<ol>

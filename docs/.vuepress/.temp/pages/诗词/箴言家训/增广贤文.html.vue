<h2 id="简介" tabindex="-1"><a class="header-anchor" href="#简介"><span>简介</span></a></h2>
<p>《增广贤文》又名《昔时贤文》、《古今贤文》，是中国明代时期编写的儿童启蒙书目。书名最早见于明万历年间的戏曲《牡丹亭》，据此可推知此书最迟写成于万历年间。后来，经过明、清两代文人的不断增补，才改成现在这个模样，称《增广昔时贤文》，通称《增广贤文》。</p>
<h2 id="选摘" tabindex="-1"><a class="header-anchor" href="#选摘"><span>选摘</span></a></h2>
<h3 id="人情世故类" tabindex="-1"><a class="header-anchor" href="#人情世故类"><span>人情世故类</span></a></h3>
<p>昔时贤文，诲汝谆谆。集韵增广，多见多闻。</p>
<p>观今宜鉴古，无古不成今。</p>
<p>知己知彼，将心比心。</p>
<p>酒逢知己饮，诗向会人吟。</p>
<p>相识满天下，知心能几人。</p>
<p>相逢好似初相识，到老终无怨恨心。</p>
<p>近水知鱼性，近山识鸟音。</p>
<p>易涨易退山溪水，易反易覆小人心。</p>
<p>运去金成铁，时来铁似金。</p>
<p>读书须用意，一字值千金。</p>
<p>逢人且说三分话，未可全抛一片心。</p>
<p>有意栽花花不发，无心插柳柳成荫。</p>
<p>画虎画皮难画骨，知人知面不知心。</p>
<p>钱财如粪土，仁义值千金。</p>
<p>流水下滩非有意，白云出岫本无心。</p>
<p>当时若不登高望，谁信东流海洋深。</p>
<p>路遥知马力，日久见人心。</p>
<p>两人一般心，无钱堪买金；一人一般心，有钱难买针。</p>
<p>相见易得好，久住难为人。</p>
<p>马行无力皆因瘦，人不风流只为贫。</p>
<p>饶人不是痴汉，痴汉不会饶人。</p>
<p>是亲不是亲，非亲却是亲。</p>
<p>美不美，乡中水；亲不亲，故乡人。</p>
<p>莺花犹怕春光老，岂可教人枉度春。</p>
<p>相逢不饮空归去，洞口桃花也笑人。</p>
<p>红粉佳人休使老，风流浪子莫教贫。</p>
<p>在家不会迎宾客，出外方知少主人。</p>
<p>黄金无假，阿魏无真。</p>
<p>客来主不顾，应恐是痴人。</p>
<p>贫居闹市无人问，富在深山有远亲。</p>
<p>谁人背后无人说，哪个人前不说人。</p>
<p>有钱道真语，无钱语不真。</p>
<p>不信但看筵中酒，杯杯先敬富贵人。</p>
<p>门前拴上高头马，不是亲来也是亲。</p>
<p>门前放根讨饭棍，亲戚故友不上门。</p>
<p>世上结交须黄金，黄金不多交不深。</p>
<p>纵令然诺暂相许，终是悠悠路行心。</p>
<p>有钱有酒多兄弟，急难何曾见一人。</p>
<h3 id="修身处世类" tabindex="-1"><a class="header-anchor" href="#修身处世类"><span>修身处世类</span></a></h3>
<p>胜者为王败者寇，只重衣冠不重人。</p>
<p>三贫三富不到老，十年兴败多少人。</p>
<p>在官三日人问我，离官三日我问人。</p>
<p>古人不见今时月，今月曾经照古人。</p>
<p>先到为君，后到为臣。</p>
<p>莫道君行早，更有早行人。</p>
<p>莫信直中直，须防仁不仁。</p>
<p>山中有直树，世上无直人。</p>
<p>自恨枝无叶，莫怨太阳偏。</p>
<p>大家都是命，半点不由人。</p>
<p>一年之计在于春，一日之计在于寅。</p>
<p>一家之计在于和，一生之计在于勤。</p>
<p>责人之心责己，恕己之心恕人。</p>
<p>守口如瓶，防意如城。</p>
<p>宁可人负我，切莫我负人。</p>
<p>再三须慎意，第一莫欺心。</p>
<p>虎生犹可近，人熟不堪亲。</p>
<p>来说是非者，便是是非人。</p>
<p>远水不救近火，远亲不如近邻。</p>
<p>有茶有酒多兄弟，急难何曾见一人。</p>
<p>人情似纸张张薄，世事如棋局局新。</p>
<p>山中也有千年树，世上难逢百岁人。</p>
<p>力微休负重，言轻莫劝人。</p>
<p>无钱休入众，遭难莫寻亲。</p>
<p>平生莫作皱眉事，世上应无切齿人。</p>
<p>士者国之宝，儒为席上珍。</p>
<p>若要断酒法，醒眼看醉人。</p>
<p>求人须求大丈夫，济人须济急时无。</p>
<p>渴时一滴如甘露，醉后添杯不如无。</p>
<p>久住令人贱，频来亲也疏。</p>
<p>酒中不语真君子，财上分明大丈夫。</p>
<p>出家如初，成佛有余。</p>
<p>积金千两，不如明解经书。</p>
<p>养子不教如养驴，养女不教如养猪。</p>
<p>有田不耕仓廪虚，有书不读子孙愚。</p>
<p>仓廪虚兮岁月乏，子孙愚兮礼义疏。</p>
<p>同君一夜话，胜读十年书。</p>
<p>人不通今古，马牛如襟裾。</p>
<p>茫茫四海人无数，哪个男儿是丈夫。</p>
<p>白酒红人面，黄金黑世心。</p>
<h3 id="因果报应类" tabindex="-1"><a class="header-anchor" href="#因果报应类"><span>因果报应类</span></a></h3>
<p>善恶到头终有报，只争来早与来迟。</p>
<p>黄河尚有澄清日，岂可人无得运时。</p>
<p>得宠思辱，安居虑危。</p>
<p>念念有如临敌日，心心常似过桥时。</p>
<p>英雄行险道，富贵似花枝。</p>
<p>人情莫道春光好，只怕秋来有冷时。</p>
<p>送君千里，终须一别。</p>
<p>但将冷眼观螃蟹，看你横行到几时。</p>
<p>见善如不及，见恶如探汤。</p>
<p>人贫志短，马瘦毛长。</p>
<p>自家心里急，他人未知忙。</p>
<p>贫无达士将金赠，病有高人说药方。</p>
<p>触来莫与说，事过心清凉。</p>
<p>秋至满山多秀色，春来无处不花香。</p>
<p>凡人不可貌相，海水不可斗量。</p>
<p>清清之水，为土所防。</p>
<p>济济之士，为酒所伤。</p>
<p>蒿草之下，或有兰香。</p>
<p>茅茨之屋，或有侯王。</p>
<p>无限朱门生饿殍，几多白屋出卿相。</p>
<p>醉后乾坤大，壶中日月长。</p>
<p>万事皆已定，浮生空白忙。</p>
<p>千里送鹅毛，礼轻仁义重。</p>
<p>一人传虚，万人传实。</p>
<p>世事明如镜，前程暗似漆。</p>
<p>光阴黄金难买，一世如驹过隙。</p>
<p>良田万顷，日食一升。</p>
<p>大厦千间，夜眠八尺。</p>
<p>千经万典，孝悌为先。</p>
<p>一字入公门，九牛拖不出。</p>
<p>衙门八字开，有理无钱莫进来。</p>
<p>富从升合起，贫因不算来。</p>
<p>家中无才子，官从何处来。</p>
<p>万事不由人计较，一生都是命安排。</p>
<p>急行慢行，前程只有多少路。</p>
<p>人间私语，天闻若雷。</p>
<p>暗室亏心，神目如电。</p>
<p>一毫之恶，劝人莫作。</p>
<p>一毫之善，与人方便。</p>
<p>欺人是祸，饶人是福。</p>
<p>天眼恢恢，报应甚速。</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<h3 id="成书背景" tabindex="-1"><a class="header-anchor" href="#成书背景"><span>成书背景</span></a></h3>
<p>《增广贤文》是在民间谚语、俗语的基础上，经过文人加工整理而成的。它汇集了从春秋战国到明清时期的格言、谚语、俗语等，内容涉及人生哲学、处世智慧、道德修养等方面，是中国传统文化的重要组成部分。</p>
<h3 id="主要特点" tabindex="-1"><a class="header-anchor" href="#主要特点"><span>主要特点</span></a></h3>
<ol>
<li><strong>内容丰富</strong>：涵盖人生的各个方面，从修身养性到处世哲学</li>
<li><strong>语言通俗</strong>：多用民间俗语，通俗易懂，朗朗上口</li>
<li><strong>哲理深刻</strong>：在简单的语言中蕴含深刻的人生道理</li>
<li><strong>实用性强</strong>：贴近生活实际，具有很强的指导意义</li>
<li><strong>传承性好</strong>：便于记忆和传播，世代相传</li>
</ol>

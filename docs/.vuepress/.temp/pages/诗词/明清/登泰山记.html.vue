<strong>南麓：</strong> 南面的山脚。<br>
<strong>砌石为磴：</strong> 用石头砌成台阶。磴，石阶。<br>
<strong>郦道元：</strong> 北魏地理学家，著有《水经注》。<br>
<strong>环水：</strong> 环绕的水流。<br>
<strong>循：</strong> 沿着。<br>
<strong>道少半：</strong> 走了一半路程。<br>
<strong>中岭：</strong> 山中的山岭。<br>
<strong>天门：</strong> 泰山著名景点。<br>
<strong>天门溪水：</strong> 古代对东谷的称呼。<br>
<strong>崖限当道：</strong> 悬崖阻挡道路。<br>
<strong>磴几不可登：</strong> 石阶几乎无法攀登。<br>
<strong>苍山负雪：</strong> 青山背负着白雪。<br>
<strong>明烛天南：</strong> 明亮地照耀着南天。<br>
<strong>徂徕：</strong> 山名，在泰山东南。<br>
<strong>若带然：</strong> 像带子一样。<br>
<strong>戊申晦：</strong> 农历十二月三十日。<br>
<strong>五鼓：</strong> 五更，约凌晨三至五时。<br>
<strong>日观亭：</strong> 泰山顶上观日出的亭子。<br>
<strong>云漫：</strong> 云雾弥漫。<br>
<strong>摴蒱：</strong> 古代博戏用具，这里形容山峰的形状。<br>
<strong>极天云一线异色：</strong> 天边云彩呈现一线异样的颜色。<br>
<strong>须臾：</strong> 片刻。<br>
<strong>正赤如丹：</strong> 正红如朱砂。<br>
<strong>绛皓驳色：</strong> 红白相间的颜色。绛，深红色。皓，白色。驳，杂色。<br>
<strong>若偻：</strong> 像驼背的人。偻，驼背。<br>
<strong>岱祠：</strong> 泰山神庙。岱，泰山的别称。<br>
<strong>碧霞元君祠：</strong> 供奉碧霞元君的祠堂。<br>
<strong>皇帝行宫：</strong> 皇帝出巡时的临时住所。<br>
<strong>显庆：</strong> 唐高宗年号（656-661）。<br>
<strong>漫失：</strong> 模糊消失。<br>
<strong>僻不当道：</strong> 偏僻不在道路上。<br>
<strong>石罅：</strong> 石头缝隙。<br>
<strong>平顶：</strong> 树顶平整。</p>
<h2 id="译文" tabindex="-1"><a class="header-anchor" href="#译文"><span>译文</span></a></h2>
<p>泰山的南面，汶水向西流；泰山的北面，济水向东流。南面的山谷流水都汇入汶水，北面的山谷流水都汇入济水，这就是所谓的分水岭。朱砂洞又在泰山西北一百多里，与长清县接界。我在乾隆三十九年十二月，从京城冒着风雪出发，经过齐河、长清，穿过泰山西北的山谷，越过长城的界限，到达泰安。这个月十七日，与知府朱孝纯子颍从南麓登山。四十五里路程，道路都是用石头砌成台阶，台阶有七千多级。</p>
<p>泰山正南面有三条山谷。中谷环绕泰安城下，这就是郦道元所说的环水。我开始沿着中谷进入，走了一半路程，越过中岭，又沿着西谷，于是到达山顶。古时候登山，沿东谷进入，路上有天门。东谷，古代叫做天门溪水，我没有到过那里。</p>
<p>现在所经过的中岭及山顶，悬崖阻挡道路的地方，世人都叫做天门。路上迷雾弥漫，冰雪湿滑，石阶几乎无法攀登。等到登上山后，青山背负着白雪，明亮地照耀着南天；远望夕阳照着城郭，汶水、徂徕山如画一般，而半山腰的云雾像带子一样环绕。</p>
<p>十二月三十日，五更时分，与子颍坐在日观亭，等待日出。大风扬起积雪打在脸上。亭子东面从脚下都是云雾弥漫。稍微看见云中白色的像博戏用具一样竖立着的几十座，那是山峰。天边云彩呈现一线异样的颜色，片刻间变成五彩。太阳升起，正红如朱砂，下面有红光，摇动着承托着它。有人说，这就是东海。回头看日观亭以西的山峰，有的照到阳光，有的没有，红白相间的颜色，都像驼背的人。</p>
<p>亭子西面有岱祠，又有碧霞元君祠；皇帝行宫在碧霞元君祠东面。这天观看道路中的石刻，从唐朝显庆年间以来的都有；那些更古老的石刻都模糊消失了。偏僻不在道路上的，都来不及前往。</p>
<p>山上多石头，少土壤；石头是苍黑色的，多数是方形的，少数是圆形的。少有杂树，多是松树，生长在石头缝隙中，都是平顶的。有冰雪，没有瀑布，没有鸟兽的声音和踪迹。到日观亭几里之内没有树木，而雪深及人的膝盖。</p>
<p>桐城姚鼐记。</p>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<h3 id="创作背景" tabindex="-1"><a class="header-anchor" href="#创作背景"><span>创作背景</span></a></h3>
<p>《登泰山记》写于乾隆三十九年（1774年），当时姚鼐38岁，正值壮年。他与泰安知府朱孝纯一同冬日登泰山，观赏日出奇景，写下了这篇游记。姚鼐是桐城派的重要代表人物，主张&quot;义法&quot;并重，此文体现了桐城派古文的特色。</p>
<h3 id="主题思想" tabindex="-1"><a class="header-anchor" href="#主题思想"><span>主题思想</span></a></h3>
<ol>
<li><strong>记游写景</strong>：详细记录登泰山的过程和所见景色</li>
<li><strong>赞美自然</strong>：表现泰山的雄奇壮丽和日出的壮观</li>
<li><strong>抒发情怀</strong>：通过登山观景，表达对自然的敬畏和赞美</li>
<li><strong>考证史迹</strong>：对泰山的地理、历史进行考证和记录</li>
</ol>
<h3 id="艺术特色" tabindex="-1"><a class="header-anchor" href="#艺术特色"><span>艺术特色</span></a></h3>
<ol>
<li><strong>结构严谨</strong>：全文层次分明，条理清晰</li>
<li><strong>叙议结合</strong>：既有生动的叙述，又有精当的议论</li>
<li><strong>详略得当</strong>：重点突出日出景象，其他内容简洁明了</li>
<li><strong>语言精练</strong>：用词准确，句式整齐，体现桐城派文风</li>
</ol>
<h3 id="表现手法" tabindex="-1"><a class="header-anchor" href="#表现手法"><span>表现手法</span></a></h3>
<ol>
<li><strong>移步换景</strong>：按照登山的路线，逐步展现不同的景色</li>
<li><strong>点面结合</strong>：既有全景描写，又有细节刻画</li>
<li><strong>动静结合</strong>：既写静态的山景，又写动态的日出</li>
<li><strong>虚实相生</strong>：实写所见，虚写所感</li>
</ol>
<h3 id="结构分析" tabindex="-1"><a class="header-anchor" href="#结构分析"><span>结构分析</span></a></h3>
<p>全文分为四个部分：</p>
<ol>
<li><strong>第一部分</strong>：介绍泰山地理位置和登山路线</li>
<li><strong>第二部分</strong>：记述登山过程和沿途景色</li>
<li><strong>第三部分</strong>：重点描写日出奇景</li>
<li><strong>第四部分</strong>：记录山上景物和历史遗迹</li>
</ol>
<h3 id="名句赏析" tabindex="-1"><a class="header-anchor" href="#名句赏析"><span>名句赏析</span></a></h3>
<ol>
<li>
<p><strong>&quot;泰山之阳，汶水西流；其阴，济水东流&quot;</strong>：开篇即以简洁的语言介绍泰山的地理位置，体现了作者严谨的治学态度。</p>
</li>
<li>

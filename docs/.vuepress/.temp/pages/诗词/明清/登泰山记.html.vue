<strong>汶水：</strong> 河流名，源出泰山，西流入黄河。<br>
<strong>其阴：</strong> 泰山的北面。<br>
<strong>济水：</strong> 河流名，古代四渎之一。<br>
<strong>阳谷：</strong> 山南面的山谷。<br>
<strong>阴谷：</strong> 山北面的山谷。<br>
<strong>分水脊：</strong> 分水岭。<br>
<strong>朱砂洞：</strong> 地名，在泰山西北。<br>
<strong>长清：</strong> 县名，在山东。<br>
<strong>乾隆三十九年：</strong> 公元1774年。<br>
<strong>齐河：</strong> 县名，在山东。<br>
<strong>越长城之限：</strong> 越过长城的界限。<br>
<strong>泰安：</strong> 县名，在泰山南麓。<br>
<strong>丁未：</strong> 农历十二月十七日。<br>
<strong>朱孝纯：</strong> 时任泰安知府。<br>

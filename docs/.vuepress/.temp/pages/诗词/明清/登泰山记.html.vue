<li>
<p><strong>&quot;大风扬积雪击面&quot;</strong>：写登山观日出时的恶劣天气，为下文日出的壮观做铺垫。</p>
</li>
<li>
<p><strong>&quot;极天云一线异色，须臾成五彩。日上，正赤如丹，下有红光，动摇承之&quot;</strong>：精彩地描写了日出的过程，色彩变化生动，富有动感。</p>
</li>
<li>
<p><strong>&quot;绛皓驳色，而皆若偻&quot;</strong>：写日出后山峰的景象，比喻生动，富有想象力。</p>
</li>
</ol>

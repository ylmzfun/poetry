<h1 id="登泰山记" tabindex="-1"><a class="header-anchor" href="#登泰山记"><span>登泰山记</span></a></h1>
<p><strong>作者：</strong> 姚鼐<br>
<strong>朝代：</strong> 清<br>
<strong>体裁：</strong> 散文<br>
<strong>出处：</strong> 《惜抱轩文集》</p>
<h2 id="简介" tabindex="-1"><a class="header-anchor" href="#简介"><span>简介</span></a></h2>
<p>《登泰山记》是清代散文家姚鼐的代表作，写于乾隆三十九年（1774年）。文章记述了作者与友人冬日登泰山观日出的经历，描绘了泰山雄奇壮丽的景色，特别是日出的壮观景象。全文结构严谨，叙议结合，体现了桐城派古文的特色，是清代游记散文的典范之作。</p>

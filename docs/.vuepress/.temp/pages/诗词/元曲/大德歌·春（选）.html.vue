<h2 id="原文" tabindex="-1"><a class="header-anchor" href="#原文"><span>原文</span></a></h2>
<p><strong>大德歌·春</strong></p>
<p>子规啼，不如归，道是春归人未归。
几日添憔悴，虚飘飘柳絮飞。
一春鱼雁无消息，则见双燕斗衔泥。</p>
<h2 id="注释" tabindex="-1"><a class="header-anchor" href="#注释"><span>注释</span></a></h2>
<ul>
<li><strong>子规</strong>：杜鹃鸟，传说为古蜀王杜宇的魂魄所化，啼声凄厉，常用来表达思归之情。</li>
<li><strong>不如归</strong>：杜鹃鸟的叫声，听起来像&quot;不如归去&quot;，故有此说。</li>
<li><strong>道是</strong>：说是，据说。</li>
<li><strong>春归人未归</strong>：春天回来了，但人还没有回来。</li>
<li><strong>憔悴</strong>：容颜消瘦，精神萎靡。</li>
<li><strong>虚飘飘</strong>：形容柳絮轻飘的样子，也暗示心情的空虚飘荡。</li>
<li><strong>鱼雁</strong>：指书信。古代传说鱼腹、雁足可以传书。</li>
<li><strong>双燕斗衔泥</strong>：燕子双双衔泥筑巢，暗示成双成对，反衬人的孤独。</li>
</ul>
<h2 id="赏析" tabindex="-1"><a class="header-anchor" href="#赏析"><span>赏析</span></a></h2>
<p>这首散曲是关汉卿的代表作之一，体现了元曲语言直白、情感真挚的特点。</p>
<p><strong>艺术特色：</strong></p>
<ol>
<li>
<p><strong>情景交融</strong>：以春景起兴，子规啼叫、柳絮飞舞、双燕衔泥，营造了浓郁的春日氛围，与主人公的离愁别绪形成对比。</p>
</li>
<li>
<p><strong>语言通俗</strong>：用词简洁明了，接近口语，如&quot;道是&quot;、&quot;则见&quot;等，体现了元曲贴近民间的特色。</p>
</li>
<li>
<p><strong>对比手法</strong>：春归与人未归、双燕成对与人的孤独形成鲜明对比，突出了思念之苦。</p>
</li>
<li>
<p><strong>声韵和谐</strong>：全曲押韵工整，读来朗朗上口，具有很强的音乐性。</p>
</li>
</ol>
<p><strong>思想内容：</strong></p>
<p>作品表达了对远方亲人的深切思念，春天的美好景色反而加重了离别的痛苦。&quot;一春鱼雁无消息&quot;道出了久无音信的焦虑，&quot;双燕斗衔泥&quot;更是以燕子的成双成对反衬出人的孤独寂寞。整首曲子情真意切，具有很强的感染力。</p>
